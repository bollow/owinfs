<h2>Examples of possibilities for complex search queries</h2>
<table width="50%">
  <tr>
    <th>Search query</th>
    <th>What it means</th>
  </tr>
  <tr>
    <td><code>"Duty-Free Quota-Free access for LDCs"</code></td>
    <td>Search for documents containing the exact phrase “Duty-Free Quota-Free access for LDCs”.</td>
  </tr>
  <tr>
    <td><code>digital UNCTAD</code></td>
    <td>Search for documents containing at least one of the words “digital” and “UNCTAD”.</td>
  </tr>
  <tr>
    <td><code>digital AND UNCTAD</code></td>
    <td>Search for documents containing both the words “digital” and “UNCTAD”.</td>
  </tr>
  <tr>
    <td><code>UNCTAD -WTO</code></td>
    <td>Search for documents containing the word “UNCTAD”, but not the word “WTO”.</td>
  </tr>
  <tr>
    <td><code>"UNCTAD WTO"~10</code></td>
    <td>Search for documents containing the words “UNCTAD” and “WTO” within 10 words of other.</td>
  </tr>
  <tr>
    <td><code>digital AND date:2021-04</code></td>
    <td>Search for documents from April 2021 containing the word “digital”.
        (Supported date formats are: “YYYY”, “YYYY-MM”, “YYYY-MM-DD”)</td>
  </tr>
  <tr>
    <td><code>language:es AND Stiglitz</code></td>
    <td>Search for Spanish-language documents containing the word “Stiglitz”.
        (Supported values for language are: “en”, “es”, “fr”, “other”)</td>
  </tr>
</table>
Search terms can be combined using “<code>AND</code>”, “<code>OR</code>”, “<code>NOT</code>”, “<code>(</code>” and “<code>)</code>” to construct
very complex queries.