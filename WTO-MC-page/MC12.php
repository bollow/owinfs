<p>
Previous to the emergence of the omicron variant, the WTO Director-General was for the longest time determined that this ministerial conference should take place November 30 - December 3, 2021 
despite a significant number of Ministers and their lead officials not being able to travel safely to Geneva for the MC12. Some would be unable to travel at all, because ongoing COVID-related travel disruptions mean that there are no commercial flights in and out of their countries or accessible transit points. Many are from countries that are basically unvaccinated. The obvious inequities would favour those with Swiss-recognised vaccinations, ease of travel, proximity and Geneva-based delegations – that is rich countries from the Global North.
 <p>
Unbelievably, the institution whose rules enforce vaccine apartheid, attempted to have a meeting under conditions of vaccine apartheid, without having first resolved that apartheid by agreeing to the TRIPS waiver. Even though the decision to hold a Ministerial when some Ministers are unable to attend would ensure that any decisions taken in this context lack any pretence of legitimacy. The WTO claims to be a consensus based organisation.
 <p>
The determination to proceed with the MC12 continued the long-standing efforts of rich and powerful WTO Members to control the WTO’s agenda and decisions, and to marginalise the governments of the Global South and the world’s peoples.
 <p>
Corporate boosters are advocating for expanding the failed model of the WTO through plurilateral agreements on
<a href="#investment"><b>investment facilitation</b></a>,
<a href="#services"><b>domestic regulation of services</b></a> and
<a href="#digital"><b>digital trade</b></a> (so-called e-commerce).
They are attempting to legitimize these illegal “Joint Statement Initiative” (JSI) plurilateral negotiations to make the WTO even more corporate-driven.
 <p>
Developed countries are also pursuing an agreement to discipline <a href="#fish"><b>fisheries subsidies</b></a>, but without the mandate on development.
 <p>
Instead, negotiators should focus on transforming the existing rules to allow countries to pursue development and shared prosperity, in areas such as <a href="#agriculture"><b>agriculture</b></a> and the <a href="#SDT"><b>development agenda</b></a>.
 <p>
The most important agenda for the WTO is to remove intellectual property barriers to resolve the COVID-19 pandemic, by agreeing to a waiver on Trade-Related Intellectual Property Rules (TRIPS) in the WTO.
