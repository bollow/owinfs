    <p align="center"><strong>FERMIAMO LA GLOBALIZZAZIONE DELLE MULTINAZIONALI</strong><br /> 
  <strong>UN ALTRO MONDO E&rsquo; POSSIBILE!</strong> 
</p><p align="center"><strong>Un documento di Unit&agrave; della</strong><br /> 
  <strong><span style="text-decoration: underline;">OUR WORLD IS NOT FOR SALE </span>Network</strong></p><p align="center"><strong>Rete Il Nostro Mondo Non E&rsquo; In Vendita</strong><br /> 
  <strong>La nostra Visione, i Principi ed il Programma</strong> 
</p><p><strong><span style="text-decoration: underline;">INTRODUZIONE: LA NOSTRA SFIDA</span></strong></p>
<p>&ldquo;Our World Is Not For Sale (OWINFS)&rdquo; &egrave; una rete globale di
organizzazioni, attivisti e movimenti sociali impegnati a sfidare gli
accordi commerciali e sugli investimenti che favoriscono gli interessi
delle pi&ugrave; potenti imprese multinazionali, a danno delle popolazioni e
dell&rsquo;ambiente.
</p><p>A questo processo di globalizzazione guidato dalle
multinazionali, opponiamo la visione di un&rsquo;economia globale costruita
sui principi della giustizia economica, della sostenibilit&agrave; ecologica e
della responsabilit&agrave; democratica, che anteponga gli interessi dei
popoli a quelli delle imprese. Un&rsquo;economia costruita intorno agli
interessi dei veri produttori e consumatori, quali i lavoratori, i
contadini, le famiglie di agricoltori, i pescatori, i piccoli e medi
produttori, ed intorno ai bisogni di chi &egrave; messo a margine dall&rsquo;attuale
sistema, come le donne ed i popoli indigeni.
</p><p>Crediamo che un sistema giusto debba proteggere, e non
compromettere, le diversit&agrave; culturali, biologiche, economiche e
sociali; mettere l&rsquo;enfasi sullo sviluppo di economie e sistemi
commerciali sani a livello locale; assicurare i diritti ambientali,
culturali, sociali e del lavoro riconosciuti a livello internazionale;
sostenere la sovranit&agrave; e l&rsquo;autodeterminazione dei popoli; e proteggere
i processi decisionali democratici a livello nazionale e locale.
</p><p>La democrazia non si riduce semplicemente nell&rsquo;organizzare
elezioni. Esiste&nbsp; democrazia quando non si &egrave; l&rsquo;ultimo anello di una
catena che riceve passivamente un processo calato dall&rsquo;alto, un sistema
di valori standardizzato, delle priorit&agrave; e delle politiche che vengono
imposte grazie ad organismi multilaterali quali l&rsquo;Organizzazione
Mondiale del Commercio (OMC &ndash; Wto). Esiste democrazia quando non si &egrave;
soggetti a processi decisionali non trasparenti e non condivisi, quali
quelli che caratterizzano l&rsquo;Organo di Risoluzione delle Dispute della
Wto. Esiste democrazia&nbsp; quando le persone hanno il controllo delle
forze che hanno impatti diretti sulle loro vite.
</p><p>Quando fu creata la Wto, nel 1995, il suo preambolo sosteneva
che lo scopo dell&rsquo;organizzazione era quello di portare maggiore
prosperit&agrave;, aumentare l&rsquo;occupazione, ridurre la povert&agrave;, diminuire le
disuguaglianze e promuovere lo sviluppo sostenibile nel mondo mediante
un maggiore &ldquo;libero commercio&rdquo;. A dieci anni di distanza &egrave; chiaro che
la Wto non ha raggiunto questi obiettivi ed ha avuto risultati
esattamente opposti.
</p><p>Il regime commerciale della Wto ha ostacolato misure che
avrebbero promosso lo sviluppo, alleviato la povert&agrave; ed aiutato la
sopravvivenza degli esseri umani e dell&rsquo;ambiente naturale, tanto a
livello locale quanto globale. Sotto la dicitura di &ldquo;libero commercio&rdquo;,
le regole della Wto sono state utilizzate per forzare l&rsquo;apertura di
nuovi mercati e per portarli sotto il controllo delle imprese
multinazionali.
</p><p>I grandi poteri commerciali hanno inoltre utilizzato la Wto per
incrementare e consolidare il controllo delle imprese multinazionali
sull&rsquo;economia e sulle attivit&agrave; sociali in aree ben al di l&agrave; di quelle
commerciali, come ad esempio in materia di sviluppo, investimenti,
concorrenza, diritti di propriet&agrave; intellettuale, nella fornitura dei
servizi essenziali, nella protezione dell&rsquo;ambiente e negli appalti
pubblici.
</p><p>Liberalizzazioni su larga scala in queste aree costringeranno i
paesi in via di sviluppo a rinunciare a molti degli strumenti economici
per lo sviluppo che i paesi industrializzati hanno utilizzato per
creare le loro economie e posti di lavoro. Gli accordi gi&agrave; approvati
nella Wto, inoltre, insieme a quelli attualmente in discussione,
porterebbero di fatto a &ldquo;mettere sotto chiave&ldquo; e rendere irreversibili
i programmi di aggiustamento strutturale della Banca Mondiale e del
Fondo Monetario Internazionale.
</p><p>Nel portare avanti gli interessi dei grandi poteri commerciali, inoltre, i metodi di<em>governance</em>e
decisionali utilizzati nella Wto fanno notoriamente affidamento sulle
minacce, l&rsquo;inganno, la manipolazione e la mancanza di trasparenza, in
maniera non democratica e secondo un processo non inclusivo.
</p><p>Sono le conseguenze distruttive dal punto di vista sociale,
politico e ambientale del modello neoliberista di globalizzazione delle
imprese ad avere favorito il sorgere della resistenza da parte di un
ampio spettro di organizzazioni della societ&agrave; civile e di movimenti
sociali in tutto il mondo, come si &egrave; manifestato ai summit della Wto a
Seattle, Doha, Cancun ed Hong Kong.
</p><p>La rete Our World Is Not For Sale &egrave; parte di questo movimento di resistenza globale.
</p><p>Dieci anni dopo la fondazione della Wto, per noi &egrave; diventato
chiaro che le possibilit&agrave; che la Wto si muova in direzione di riforme
positive sono minime, se non del tutto assenti. Un cambiamento &egrave;
assolutamente necessario. Al momento abbiamo un sistema in cui:</p>
<ul><li> 
    <p>le vite sono distrutte, i diritti umani ignorati,
la salute pubblica minacciata, l&rsquo;ambiente saccheggiato ed i sistemi
democratici vengono erosi;</p> 
  </li><li> 
    <p>le economie
locali sono minacciate, ed i lavoratori, i contadini, le famiglie di
agricoltori, i pescatori, i consumatori, le donne ed i popoli indigeni
sono particolarmente svantaggiati e sfruttati;</p> 
  </li><li> 
    <p>la
possibilit&agrave; per i governi di garantire l&rsquo;accesso agli aspetti
essenziali della vita, promuovere la salute, la sicurezza e la
sovranit&agrave; alimentare, e proteggere la diversit&agrave; culturale e biologica &egrave;
compromessa e talvolta eliminata.</p> 
  </li></ul>
<p>In tutto il mondo, gli effetti negativi dell&rsquo;attuale sistema
economico globale stanno spingendo i movimenti democratici &ndash; che
agiscono tramite le urne e nelle strade &ndash; a chiedere un cambiamento. I
politici eletti in molti paesi hanno perso la speranza nell&rsquo;attuale
sistema di<em>governance</em>economica globale. Un numero sempre
crescente di economisti e tecnocrati che hanno creato ed adottato
questo sistema stanno iniziando a porsi delle domande, in quanto i
risultati provano l&rsquo;opposto di quanto promesso. Tutto questo si sta
manifestando nel contesto di una crescente disuguaglianza, sia tra le
nazioni, sia al loro interno, e con un risorgere del militarismo.
</p><p>E&rsquo; necessario resistere ai tentativi della Wto di imporre una
liberalizzazione del commercio mondiale che colpisce la giustizia
economica, il benessere sociale, l&rsquo;equit&agrave; tra i generi e la
sostenibilit&agrave; ecologica. Il potere e l&rsquo;autorit&agrave; della Wto devono essere
ridimensionati in molte materie nelle quali l&rsquo;istituzione si &egrave; imposta,
quali ad esempio l&rsquo;agricoltura, i servizi ed i diritti di propriet&agrave;
intellettuale.
</p><p>Contemporaneamente dobbiamo ideare nuove istituzioni per
facilitare il commercio, la produzione e la distribuzione dei beni
comuni, se vogliamo evitare la crescente prospettiva di una catastrofe
sociale ed ecologica.
</p><p>L&rsquo;attuale regime commerciale, che include la Wto cos&igrave; come gli
accordi commerciali bilaterali e regionali e quelli sugli investimenti,
deve permettere un nuovo quadro commerciale per il XXI secolo
socialmente giusto ed ecologicamente sostenibile.</p>
<p><span style="text-decoration: underline;"><strong>I NOSTRI OBIETTIVI</strong></span></p>
<p>Sin dal 1998, i membri della rete OWINFS si sono confrontati per
condividere analisi, sviluppare strategie e coordinare azioni a livello
internazionale, in modo da promuovere lo sviluppo di un&rsquo;economia
alternativa, giusta e sostenibile.
</p><p>Siamo impegnati per sviluppare un nuovo sistema commerciale
democraticamente responsabile che faccia avanzare un&rsquo;economia di
giustizia, il benessere sociale, l&rsquo;equit&agrave; di genere e la sostenibilit&agrave;
ecologica, e che garantisca posti di lavoro dignitosi ed i beni e i
servizi necessari per tutti gli esseri umani.
</p><p>Sosteniamo lo sviluppo di economie locali floride ed i diritti
dei lavoratori, contadini, migranti, famiglie di agricoltori,
consumatori, donne e popoli indigeni. Crediamo che l&rsquo;autodeterminazione
dei popoli non debba essere subordinata ad impegni commerciali
internazionali. Tra le altre cose, questo significa che il processo
decisionale e l&rsquo;applicazione ad ogni livello della<em>governance</em>debbano essere democratici, trasparenti ed inclusivi.
</p><p>Riconosciamo che un sistema commerciale internazionale giusto
debba dare la priorit&agrave; ai diritti ed al welfare dei lavoratori,
contadini, migranti, pescatori, e famiglie di agricoltori che producono
i nostri prodotti, servizi e cibi.
</p><p>Chiediamo ai governi ed alle agenzie multilaterali di arrestare
i loro attacchi ai diritti fondamentali dei lavoratori, l&rsquo;arretramento
delle conquiste ottenute dalle lotte dei lavoratori, il compromettere
la sicurezza del lavoro e la corsa verso il basso dei salari, e di
rafforzare in tutto il mondo i diritti dei lavoratori.
</p><p>Ci opponiamo ad accordi e negoziati di liberalizzazione del
commercio che contribuiscono a togliere l&rsquo;accesso alle risorse naturali
a quelle popolazioni indigene e comunit&agrave; locali che da queste dipendono
per la propria sopravvivenza, e che invece danno questo accesso alle
imprese.
</p><p>Altri diritti umani fondamentali devono essere rispettati,
promossi e realizzati, a partire dall&rsquo;autodeterminazione dei popoli
indigeni e dalla fornitura dei bisogni e servizi sociali essenziali,
comprese l&rsquo;educazione, la sicurezza e la sovranit&agrave; alimentari,
l&rsquo;accesso universale ad acqua pulita per uso umano e la salute
pubblica.
</p><p>Allo stesso modo, l&rsquo;integrit&agrave; ecologica deve essere un
obiettivo di un mutato sistema commerciale globale. Questo significa,
tra le altre cose, che il commercio delle imprese e gli investimenti
devono essere regolati per arrestare il surriscaldamento globale; gli
accordi ambientali multilaterali devono avere la precedenza su quelli
commerciali; gli standard ambientali non devono essere ridotti a causa
di accordi commerciali; ed il diritto delle persone a rifiutare
organismi geneticamente modificati, di preservare la crescita delle
foreste secolari e la diversit&agrave; delle sementi dei contadini e la
promozione del benessere degli animali deve essere rispettata.</p>
<p><span style="text-decoration: underline;"><strong>LE NOSTRE RICHIESTE</strong></span></p>
<p><strong>Assicurare il diritto di scegliere delle persone: autodeterminazione, democrazia e sviluppo</strong></p>
<p><em>Ribadiamo il<strong>diritto fondamentale dei paesi di sviluppare
politiche economiche ed industriali che promuovano uno sviluppo
economico genuino, creino posti di lavoro dignitosi e proteggano la
sopravvivenza, e tutelino l&rsquo;ambiente</strong>. Tutti i paesi, ed in
particolare i pi&ugrave; poveri, devono avere il diritto di utilizzare opzioni
politiche (come politiche di contenuto locale) per incrementare la
capacit&agrave; dei loro propri settori produttivi, in particolare per le
piccole e medie imprese. I paesi devono anche tutelare la loro
possibilit&agrave; (spazio politico) di disegnare strategie economiche,
sociali ed ambientali che favoriscano i loro abitanti pi&ugrave; vulnerabili.
La ricerca della &ldquo;coerenza&rdquo; tra le istituzioni internazionali &egrave;
diventata un mezzo per negare questo spazio politico: il Fondo
Monetario Internazionale, la Banca Mondiale ed alcuni singoli paesi
donatori forzano i governi ad implementare politiche neoliberiste, e la
Wto e gli altri accordi commerciali e sugli investimenti rendono
praticamente irreversibili queste politiche.</em>Di conseguenza:</p>
<ul><li> 
    <p>Our World Is Not For Sale chiede la fine delle
pratiche segrete e coercitive che sono diventate il marchio di fabbrica
dei negoziati commerciali, in particolare nella Wto, dove pochi governi
pi&ugrave; potenti, spesso agendo per conto delle loro elite imprenditoriali,
sono capaci di forzare i governi pi&ugrave; deboli per raggiungere i loro
obiettivi.</p> 
  </li><li> 
    <p>Lo smantellamento delle tariffe
e delle altre misure commerciali non deve consentire di mettere le
economie locali, ed in particolare quelle dei paesi pi&ugrave; poveri e/o di
settori economici pi&ugrave; poveri, in balia delle imprese multinazionali, e
di minacciare lo sviluppo economico locale, le leggi e gli standard sul
lavoro, la salute e la sicurezza del pubblico e dei consumatori, e
l&rsquo;ambiente.</p> 
  </li><li> 
    <p>I negoziati sul &ldquo;libero
commercio&rdquo; nella Wto ed altrove non possono continuare ad essere
utilizzati come un Cavallo di Troia per assicurare regole favorevoli
alle imprese in materia di investimenti, concorrenza, appalti pubblici,
accesso al mercato, produzione agricola, regolamentazioni locali sui
servizi pubblici ed i diritti di propriet&agrave; intellettuale. Allo stesso
modo non possono proseguire le attuali dinamiche di potere, nelle quali
i ricchi paesi industrializzati impongono la loro agenda economica a
scapito dei paesi pi&ugrave; poveri.</p> 
  </li><li> 
    <p>L&rsquo;utilizzo
di aggiustamenti strutturali e del consolidamento del debito per
forzare la liberalizzazione del commercio nei paesi del terzo mondo
deve essere arrestata. Il Fondo Monetario Internazionale, la Banca
Mondiale e le banche di sviluppo regionali devono cancellare tutti i
debiti a loro dovuti da parte dei paesi in via di sviluppo ed in
transizione in modo che questi paesi possano allocare questi fondi per
soddisfare i bisogni urgenti della popolazione.</p> 
  </li></ul>
<p><strong>Promuovere il primato dei diritti sociali e dell&rsquo;ambiente</strong></p>
<p><em>Crediamo che la protezione e l&rsquo;avanzamento dei diritti sociali,
il soddisfacimento dei bisogni basilari, e la protezione del nostro
ambiente siano essenziali alla vita. E&rsquo; inaccettabile che queste siano
compromesse dalla Wto ed altre regole degli accordi &ldquo;commerciali&rdquo;.</em>Di conseguenza:</p>
<ul><li> 
    <p>nessun accordo commerciale o sugli investimenti
deve avere la precedenza, o compromettere, gli accordi internazionali
che promuovono la giustizia sociale, economica ed ambientale, tra i
quali alcuni sono</p> 
  </li></ul>
<ul><li>&nbsp;<ul><li>la dichiarazione dell&rsquo;Organizzazione
Internazionale del Lavoro (OIL) sui principi e sui diritti fondamentali
del lavoro (che include i quattro core labour standard);</li><li>la
Convenzione sulla biodiversit&agrave; ed il suo protocollo attuativo sulla
bio-sicurezza, e gli altri accordi multilaterali sull&rsquo;ambiente;</li><li>la
Dichiarazione sui diritti dell&rsquo;uomo delle Nazioni Unite e le sue
convenzioni associate, la Convenzione internazionale sui diritti
economici, sociali e culturali e la Convenzione internazionale sui
diritti civili e politici;</li><li>la prossima Dichiarazione delle Nazioni Unite sui diritti dei popoli indigeni;</li><li>la Convenzione per l&rsquo;eliminazione di tutte le forme di discriminazione contro le donne (CEDAW); e</li><li>La Convezione internazionale sulla protezione dei diritti di tutti i lavoratori migranti e dei membri delle loro famiglie.</li></ul></li></ul>
<ul><li> 
    <p>governi devono conservare il diritto sovrano di
determinare come regolare i servizi in modo da soddisfare i bisogni
della popolazione, l&rsquo;economia e la societ&agrave;, ed onorare i loro altri
obblighi internazionali e costituzionali, compresi quelli verso le
donne, i popoli indigeni, i giovani, gli anziani ed i poveri.</p> 
  </li><li> 
    <p>Il
diritto dei governi ad applicare il principio precauzionale per
proteggere la salute pubblica, l&rsquo;ambiente, e l&rsquo;agricoltura da rischi
sconosciuti deve avere la precedenza su qualunque accordo e clausola
commerciale.</p> 
  </li><li> 
    <p>La riduzione delle tariffe
che danneggiano l&rsquo;ambiente o lo sviluppo sostenibile aumentando un
commercio inappropriato delle risorse naturali e di altri prodotti
ambientalmente sensibili non dovrebbe essere attuata.</p> 
  </li><li> 
    <p>L&rsquo;indebolimento
delle Nazioni Unite da parte delle istituzioni pro-multinazionali di
Bretton Woods, della Wto, e dei grandi poteri deve essere arrestata, ed
il sistema di agenzie ed accordi delle Nazioni Unite deve essere
rafforzato.</p> 
  </li></ul>
<p><strong>Proteggere i servizi essenziali</strong></p>
<p><em>Sottoscriviamo il principio fondamentale secondo cui nessun
accordo commerciale o sugli investimenti dovrebbe violare il diritto
dei governi a garantire l&rsquo;accesso ad aspetti essenziali alla vita, a
promuovere la salute ed il benessere dei loro abitanti, e la protezione
dell&rsquo;ambiente.</em>Di conseguenza:</p>
<ul><li> 
    <p>i paesi non dovrebbero subire pressioni per
accettare regole commerciali che diminuiscono questa loro abilit&agrave;, sia
tramite l&rsquo;Accordo Generale sul Commercio dei Servizi (GATS) della Wto,
sia negli accordi bilaterali e regionali.</p> 
  </li><li> 
    <p>I
settori direttamente legati a quelli essenziali, quali la salute,
l&rsquo;educazione, la cultura audiovisiva, l&rsquo;assistenza sociale, i servizi
idrici ed energetici devono essere esplicitamente esclusi da tutti gli
accordi commerciali e sugli investimenti.</p> 
  </li><li> 
    <p>Le
regole riguardanti i regolamenti domestici, i sussidi e gli appalti
pubblici in materia di servizi per la loro stessa natura impattano su
questa abilit&agrave; e non dovrebbero pertanto essere inclusi in accordi
commerciali o sugli investimenti.</p> 
  </li></ul>
<p><em>I paesi stanno subendo enormi pressioni per sottomettere i loro
servizi essenziali alle regole del GATS, che hanno l&rsquo;effetto di
promuovere le privatizzazioni. Quando, inoltre, gli impegni presi in
questo negoziato sono adottati da paesi che sono o sono stati soggetti
a deregolamentazioni e privatizzazioni dei loro servizi essenziali
tramite le richieste di &ldquo;aggiustamento strutturale&rdquo;, le regole del GATS
servono per rendere queste privatizzazioni praticamente irreversibili.
In questa maniera il GATS promuove l&rsquo;apertura dei mercati locali alle
imprese multinazionali e l&rsquo;avanzamento del modello neoliberista.</em>Di conseguenza:</p>
<ul><li>Queste richieste di &ldquo;aggiustamento strutturale&rdquo; debbono
essere respinte, non rese irreversibili, e non devono essere una
condizione per i paesi che ricevono nuovi prestiti o aiuti, cos&igrave; come i
paesi non dovrebbero essere messi sotto pressione per sottomettere i
loro servizi essenziali alle regole del GATS.</li></ul>
<p><strong>Difendere i saperi, la cultura e le forme di vita come l&rsquo;essenza di una civilt&agrave;</strong></p>
<p><em>Consideriamo i saperi, la cultura e l&rsquo;educazione come le forze
che muovono la civilt&agrave;. Queste forze non possono essere ridotte a
prodotti commerciabili o propriet&agrave; privata.</em> 
</p><p><em>Non c&rsquo;&egrave; alcuna base per includere queste affermazioni sulla
propriet&agrave; intellettuale in un accordo commerciale. Tutte le nazioni,
inoltre, hanno la responsabilit&agrave; e l&rsquo;obbligo di proteggere la salute
pubblica ed il benessere delle loro popolazioni. Le attuali regole
sulla propriet&agrave; intellettuali in accordi commerciali, quali l&rsquo;accordo
sugli aspetti commerciali dei diritti di propriet&agrave; intellettuale
(TRIPs) della Wto, impediscono l&rsquo;accesso delle persone ai farmaci
essenziali, alle sementi ed alle necessit&agrave; vitali, mentre portano
all&rsquo;appropriazioni dei privati sulle forme di vita ed i saperi
tradizionali e la distruzione della biodiversit&agrave;. Impediscono inoltre
ai paesi pi&ugrave; poveri di migliorare i propri livelli di welfare economico
e sociale e di difendere le loro identit&agrave; e tradizioni uniche.</em>Di conseguenza:</p>
<ul><li> 
    <p>i governi devono conservare il loro
imprescindibile diritto a limitare la protezioni dei brevetti in modo
da proteggere gli interessi pubblici in queste aree, in particolare
riguardo le medicine, le sementi e le forme di vita.</p> 
  </li><li> 
    <p>La
brevettabilit&agrave; delle forme di vita, inclusi i microrganismi, deve
essere proibita in tutti i regimi nazionali ed internazionali.</p> 
  </li><li> 
    <p>Una
diversit&agrave; culturale genuina deve essere difesa dall&rsquo;impatto e
dall&rsquo;omogeneizzazione dei mercati globali e dai monopoli sui saperi,
sulla tecnologia e sulle telecomunicazioni.</p> 
  </li></ul>
<p><strong>Preservare e favorire la sovranit&agrave; alimentare e la sicurezza alimentare</strong></p>
<p><em>Affermiamo che il diritto al cibo &egrave; un diritto umano
fondamentale. L&rsquo;accordo sull&rsquo;agricoltura (AOA) della Wto subordina
questo diritto ai profitti delle imprese.</em>Il sistema alimentare
promosso dalla Wto &egrave; costruito su un&rsquo;agricoltura industrializzata con
grande utilizzo di capitali e guidata dalle esportazioni, che sta
contribuendo alla concentrazione delle imprese lungo la catena
alimentare e compromettendo la sopravvivenza, i diritti, la salute, le
condizioni di vita e di lavoro dei lavoratori nei settori
dell&rsquo;agricoltura e alimentare, e di conseguenza compromettendo la
sicurezza alimentare.
</p><p><em>Inoltre questo sistema non riconosce che il lavoro agricolo
&egrave; un modo di vita ed una base importante per la comunit&agrave; e la cultura.
Le politiche della Wto e di altri accordi commerciali favoriscono
quindi un&rsquo;ulteriore concentrazione ed un aumento del potere delle
