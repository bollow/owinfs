<p>
April 28, 2021<br/>
<br/>


<p>
Dear Heads of State:
<p>
In nations around the world, the regime of
hyper-globalized trade, investment and supply chains that the World
Trade Organization (WTO) has championed and implemented during its 25
years of existence is on the verge of collapse. The COVID-19 pandemic
has exposed how the WTO model exacerbates insecurity, inequality and
instability. Legitimate global commercial rules should facilitate the
improvement of the livelihoods, health and wellbeing of all people
around the world and the long-term survival of the planet. The WTO
system has not met these goals: It was never fit for purpose and
certainly is not now.
<p>
This reckoning comes at a time when the WTO
faces an existential crisis because, not only has it failed to make
people’s lives better, but in many countries it has done
significant damage by empowering pharmaceutical, agribusiness,
financial and other corporate interests in high-income countries to
dominate economies to the detriment of
workers in both high- and low-income countries. Its negotiating and
enforcement functions are paralysed, and the divisions were
spotlighted by the former WTO Director-General’s early departure.
<p>
We, the undersigned labour, environmental,
consumer and other organizations, call for the transformation of the
WTO into a completely new framework for
international trade that is fit for the 21st century
– which means it puts people and the planet first.
<p>
This demand is not new. As we predicted back in
1995, the WTO has functioned to establish rules for the world economy
that mainly benefit large transnational corporations at the expense
of national and local economies, workers, farmers and indigenous
peoples, our health and safety, and the environment. Without a labour
protection floor, a race to the bottom has repressed wage growth and
increased precarious work. The climate, biodiversity, and poverty
crises have been ignored, the needed solutions constrained by “trade”
rules. So has the documented rise in inequality within and between
nations, as governments have been stripped of essential tools to
pursue the wellbeing of their peoples and address the negative
impacts of hyper-globalization. 

<p>
Now, the deeply integrated, brittle supply
chains created by WTO rules over the past several decades have
undermined numerous countries’ fights against the global COVID-19
pandemic. Around the world, countries cannot make or obtain masks,
test kits, ventilators, medicines and other necessary equipment. That
is because decades of WTO rules have prioritized large corporations’
demands to concentrate global production to maximize their profits
and banned countries’ use of policy tools to ensure local
production capacity and diversity of import suppliers. WTO
intellectual property rules that are designed to maximize
pharmaceutical corporations’ profits instead of public health have
driven up prices for medicines that are essential to combat COVID-19
in scores of countries and could become a barrier to equitable and
universal access to vaccine and treatment supplies.
<p>
The WTO itself is fracturing. The former
Director-General abruptly departed. The WTO’s dispute settlement
system, whose tribunals have often ruled that governments must change
legitimate public policies meant to promote public health, encourage
development, protect the environment and fight climate crisis, or
face potentially crippling trade sanctions, was derailed at the end
of 2019. The body’s negotiating function has been on the ropes
since the first failed attempt to launch a new round at the 1999
Seattle WTO Ministerial meeting. Developing country consensus demands
were ignored, but attempts by rich countries to bully through an
agenda opposed by most nations also failed. Then a so-called Doha
“Development” Round was launched in 2001, invoking the need for
unity in the face of the 9/11 attacks in the United States.
Developing countries were promised that their need to use trade for
development would be central, but in the intervening 19 years, the
same WTO expansion agenda that most countries have always opposed has
been prioritized and the development agenda sidelined.
<p>
In light of the WTO’s dysfunction, rich and
powerful states have resorted to plurilateral negotiations of new
rules that prioritize corporate rights and profits. Those rules are
facing stiff opposition whether promoted at the WTO or elsewhere, and
their failures provide further evidence of a paradigm that has no
legitimacy. The Trade in Services Agreement (TiSA) and Transatlantic
Trade and Investment Partnership (TTIP) collapsed altogether; the
Trans-Pacific Partnership (TPP) could not win a U.S. congressional
majority for the year after it was signed and the U.S. stayed out of
that pact; and India withdrew from the Regional Comprehensive
Economic Partnership (RCEP), just to name a few examples.
<p>
Instead of learning from these mistakes, or
acknowledging the chasm between promised positive WTO outcomes and
reality, powerful interests at the WTO are doubling down to push more
of the same. In what would seem like a parody of the parable that
when one only sees nails, the answer is always a hammer, the WTO
answer for COVID-19 is to maintain and expand the same failed
liberalization policies, including an entirely counterproductive, new
tariff-zeroing pact for COVID-19-related goods. Right through the
pandemic, negotiations have continued on limiting domestic regulation
of the service sector, even as the concentration of services firms is
posing a major impediment to timely and cost-effective procurement
and distribution of essential goods. Negotiations to limit regulation
and vetting of foreign investors continue, despite a clear need for
production of personal protective equipment (PPE) and medicines to be
diversified. Negotiations that would give Big Tech more control over
peoples’ data and the digital economy that WTO member countries
explicitly rejected are continuing at a time when most people and
governments are clamouring for serious checks on Big Tech and their
unaccountable control of data.
<p>
This failure can no longer be ignored. The
original global trade body – the International Trade Organization
that was envisioned in the Havana Charter of 1948 in response to the
horrors and chaos of World War II – focused on full employment,
limiting corporate concentration, fair competition, protections for
workers and standards to ensure currency and other related policies
did not distort trade. That very different vision for a rules-based
global trading system – updated to recognise the climate crisis,
systemic inequality, and the unaccountable power of Big Tech –
remains attainable, but only if countries agree that global trade
rules are supposed to work for people around the world, not the
world’s largest corporations.
<p>
The choice is not between the status quo or no
trade. That is a straw man hawked by those who want nothing to
change. Change is happening. The question is what multilateral
framework can be inclusive, promote real sustainability, human rights
and prosperity for all, and deliver the benefits of expanded trade to
most people, while also providing our elected representatives the
policy space to promote the public interest. One example is the
<a href="https://unctad.org/en/pages/newsdetails.aspx?OriginalVersionID=2057"><u>Geneva
principles for a global Green New Deal</u></a>.
<p>
We call on governments to grasp this
opportunity for transformational change. 

