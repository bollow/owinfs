<b>Civil Society Calls for 12th Ministerial Conference of the World Trade Organization (WTO) to be Postponed, and for Immediate Agreement on a Waiver on Intellectual Property (IP) Rules to End the Pandemic</b>
<p>
Over
130 global, regional and national civil society organisations from
around the world have <a href="/2021/L_postpone_MC12.pdf">written to the Members of the World Trade
Organization (WTO)</a>, urging them to postpone the 12th Ministerial
Conference (MC12) that is being convened in Geneva in just over a
week, in the midst of a global pandemic with rising cases across the
globe. It is available in <a href="2021/L_postpone_MC12.pdf">English</a>, <a href="2021/L_postpone_MC12_ES.pdf">Spanish</a> and <a href="2021/L_postpone_MC12_FR.pdf">French</a>.
<p>Shamefully,
the proposed Ministerial Conference comes as a handful of powerful
governments, led by the European Union, the UK, Norway, and
Switzerland, continue to block a <a href="TRIPS">WTO waiver for Covid-related
intellectual property rights</a> under the WTO’s Agreement on
Trade-Related Intellectual Property Rights (TRIPS) that could ensure
access to treatments, diagnostics, and vaccines for all. The letter
calls on governments to immediately agree to the waiver, for which a
Ministerial meeting is not necessary, as waivers have been  agreed by
meetings of the General Council of the WTO, in Geneva. 

<p>Covid-19
has exposed the WTO’s systemic prioritiy of <a href="https://www.oxfam.org/en/press-releases/covid-vaccines-create-9-new-billionaires-combined-wealth-greater-cost-vaccinating">profits over people</a>
through the monopolies that are guaranteed to Big Pharma under TRIPS.


<p>As
the letter points out, these inequities are compounded by the impacts
of Covid-19 on the ability of Ministers and officials, especially
from the global South, to attend and participate fully, effectively
and safely in a Conference where their fundamental interests are at
stake. 

<p>Some
Ministers from poorly vaccinated countries, or those isolated from
international travel, may simply be unable to travel; Switzerland’s
rules on indoor gatherings exclude some forms of vaccination and
require onerous and impractical testing requirements. The health
risks, costs and quarantine requirements add to the burden,
especially for poor countries.
<p>Instead
of addressing these urgent crises, a number of WTO members are
pushing the hyperglobalisation agenda even further in the name of an
alleged Covid-19 recovery package, and without the waiver – the sham
“<a href="2021_L_Walker_Process">Walker process</a>” – with
the support of the European Union and the corporate beneficiaries of
the intellectual property monopolies.
<p>“The
institution whose rules enforce vaccine apartheid is, unbelieveably,
attempting to hold a meeting under conditions of vaccine apartheid,
without having first resolved that apartheid by agreeing to the TRIPS
waiver. Holding a Ministerial Conference in these circumstances is
unconscionable. We call for the TRIPS Waiver, now!” said Deborah
James of the global Our World Is Not for Sale (OWINFS) network, which
coordinated the letter.
<p>Non-government
organisations that provide essential support for delegations have
been denied space in the main venue, putting them out of reach of
many delegations. 

<p>To
proceed under these circumstances would further erode the WTO’s
legitimacy, and undermine the credibility of the new
Director-General, at a time when the Organisation’s credibility is
already at an all time low. The letter therefore calls on Members to
postpone the ministerial conference and direct all efforts to
reaching an urgent agreement on the TRIPS waiver.
<p>For
years now, the WTO has faced a crisis of legitimacy for its failure
to address, and its furthering of, systemic instability, inequalities
and indebtedness on poorer countries, workers, women, indigenous
peoples and vulnerable communities. The Covid-19 pandemic has laid
bare these deep ruptures between the global North and global South,
rich and poor, privileged and vulnerable.  

