This working session brings together leading experts and practitioners from international
organizations, academic institutions and non- governmental organizations, to critically review and
discuss national law responses and experiences addressing intellectual property challenges in a
global pandemic and the role of WTO. These would include national law and policy reform processes
in several countries, including Brazil, Russia, and the ongoing discussions in the European Union
concerning obstacles in using compulsory licensing; as well as the role of TRIPS waiver in a pandemic.
It intends to conclude with a discussion on how these experiences should inform the reform of
international trade rules beyond COVID19. It aims to offer specific recommendations on what steps
can be taken by different stakeholders.
