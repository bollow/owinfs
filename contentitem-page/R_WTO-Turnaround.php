<p>
<b>At
the Public Forum of the World Trade Organization (WTO) this week,
Civil Society Demands a “Turnaround at the WTO” in advance of the
12</b><sup><b>th</b></sup><b>
Ministerial Conference</b>
<p>(Geneva)
Today, 206 major civil society groups – including global union
federations, development advocates, women’s groups, consumer
organizations, and environmental groups – representing millions of
people from more than 170 countries delivered a statement
“Turnaround: New Multilateral Trade Rules for People-Centered
Shared Prosperity and Sustainable Development” - one of the most
detailed policy statements ever, from such as large group of
organizations - to members of the WTO. The statement was coordinated
by the Our World Is Not for Sale (OWINFS) global network and is
available in <u><a href="2021_WTO-Turnaround">English</a></u>,
<u><a href="2021_WTO-Turnaround_FR">French</a></u>,
and <u><a href="2021_WTO-Turnaround_ES">Spanish</a></u>.


<p>In
the statement, they called for “a new vision for multilateralism
[which] is necessary in order to create the necessary jobs,
infrastructure, and services that are essential to achieve the SDGs
[Sustainable Development Goals], and also to safeguard our planet for
future generations.” Civil society organizations will be taking
their messages to negotiators in Geneva this week through a <u><a href="https://ourworldisnotforsale.net/2021/CS-events_WTO-PF.pdf">dozen
events</a></u> on these
issues at the WTO Public Forum, which can be <u><a href="https://wtopf21.org/">accessed
for free virtually here</a></u>.


<p>Civil
society, and over 100 governments around the world, have called on
the WTO to focus on the urgent need to end the global Covid-19
pandemic by ensuring access to vaccines, treatments, and tests for
all, by ending the “vaccine apartheid” which is enforced by WTO
rules on intellectual property. Today’s statement reinforces that
demand, highlighting that myriad existing harmful rules in the WTO
must be transformed, while efforts to expand the failed WTO model
through corporate-driven “plurilaterals” must be halted.
Governments will take decisions on these matters at the 12<sup>th</sup>
Ministerial meeting of the WTO in Geneva, Switzerland, November 30 -
December 3, 2021.
<p>The
statement also states that “in the interim, governments must make
transformational changes to existing rules while a fundamentally new
institution is envisioned. The WTO rules too often limit governments’
ability to use traditional development policies – which were used
by all industrialized countries in their development – to promote
jobs and domestic industries. Despite hypocritical claims by
developed countries and global elites in the WTO, they have stalled
the resolution of the development agenda in the WTO for nearly 20
years. Special and Differential Treatment (S&amp;DT) rules must be
strengthened and operationalized for all developing countries,
focusing on freedom from existing harmful WTO rules which limit the
use of evidence-based development policies. Immediately, this would
mean that governments would enjoy policy space to ensure domestic
food security; to utilize technology transfer and data rights; to
make use of job-creating performance requirements such as local
content and local labor requirements; and have more flexibility to
ensure affordable access to medicines, among other urgent
priorities.”
<p>The
statement further calls for “an
immediate <i>halt to efforts to
expand</i> the scope and coverage
of existing harmful rules, or to expand them to new arenas. In
particular, our governments must halt the effort by Big Tech
corporations to use the WTO to gain new pro-corporate rights in the
digital economy, and to handcuff appropriate regulatory oversight. We
call for a stop to the efforts: to deregulate the entire digital
economy through the inappropriately named “e-commerce”
negotiations; to bring more disciplines on investment policies
through “investment facilitation” negotiations; to further
encroach on regulatory sovereignty through “domestic regulation”
negotiations; to restart talks on services liberalization; and to
efforts to limit development flexibilities in the fisheries subsidies
negotiations. Corporate boosters are undertaking these negotiations
through “plurilateral” negotiations which are illegal under the
WTO because they have no mandate, as most developing countries are
opposed to them - for good reason.”
<p>The
statement then details dozens of specific rules in the WTO that must
be urgently changed, in each of the areas of the WTO’s rulemaking.
The statement concludes that “there are many more changes that must
be made to the global trading system, including a fundamental review,
abrogation, or transformation of bilateral and regional trade and
investment agreements. The above represent a minimum of immediate
changes that must be made in the multilateral trade system to provide
more policy space for all countries to implement solutions towards
promoting shared prosperity for the benefit of all.”
