<p>
27 February 2024
<p>
<b>(Abu Dhabi) Civil society organizations, present at the WTO’s MC13, have the following
quotes about the current negotiations happening at the WTO.</b> Civil society, representing
communities affected by decisions made at the WTO, is being repressed at the WTO’s
MC13, as demonstrations, banners, and even leafletting have been banned. Yesterday
<b>“NGOs call for free speech to be restored at WTO Ministerial in UAE.”</b>
<a href="https://ourworldisnotforsale.net/2024-02-26_R_free_speech">https://ourworldisnotforsale.net/2024-02-26_R_free_speech</a>
<p>
More than 50 civil society representatives are in person at MC13 working with the global
Our World Is Not for Sale (OWINFS) network including farmers, fisherfolk, development
advocates, environmentalists, and public interest organizations, coming from 21 countries:
Argentina, Australia, Bangladesh, Belgium, Bolivia, Brazil, Germany, Ghana, India, Indonesia,
Kenya, the Netherlands, New Zealand, Norway, the Philippines, Spain, Switzerland, Uganda,
the United States, and Zimbabwe. A directory of contact information of civil society experts
present in Abu Dhabi for the WTO’s MC13 is <i>available, but redacted in this online version</i>.
<p>
<b>Africa Kiiza, African Trade Network, Uganda</b><br>
"The ultimate test of a success of the MC 13 will be whether it's outcomes act as a catalyst
for accelerating structural transformation, and industrial development in African countries
to enable them address inequality and exclusion."
<p>
<b>Parminder Jeet Singh, Just Net Coalition, India</b><br>
“The moratorium on levying custom duties on digitized products, called the E-Com
Moratorium, must be discontinued immediately by WTO MC 13 because countries need an
absolute right to tax digital economy at the borders – products, services and everything in-
between. This is especially so because in a digital economy the distinction between products
and services is increasingly thin, and everything increasingly has significant digital services
element in it. Very soon, digital flows will be the greatest part of value flows across border.
Countries need to preserve the right to tax these value flows at the border, in order to earn
revenues, but even more importantly to protect, shape and develop their domestic
economy, and digital industrialization, as per the best national interest.”
<p>
<b>Deborah James, Director of International Programs at Center for Economic and Policy
Research and Facilitator of Our World Is Not for Sale global network, USA</b><br>
“At MC13, we call on WTO members to deliver on developing countries’ demands for
flexibility from onerous WTO rules: a permanent solution for public stockholding for food
security; allow countries to tax Big Tech (let the moratorium expire); and the development
agenda. This MC13 should not be held hostage to a pro-corporate agenda of expandingWTO disciplines against public domestic regulation of services and investment through Joint
Statement Initiatives which are illegal under multilateral rules.”<br>
“Despite the WTO’s efforts to silence our voices, affected communities need to be heard in
the halls of negotiations. It is the WTO’s responsibility to guarantee that affected
communities and CSO experts maintain the rights of free speech within the WTO space,
whichever country is the host organization of the Ministerial Conference. We hold the WTO
responsible for this escalation of repression against the voices of civil society and demand
that our rights are restored.”
<p>
<b>Victor Menotti, Demand Climate Justice, Slovakia</b><br>
“WTO must move beyond its ideological rigidity of one-way trade liberalization if it is to
address today's converging climate and other crises it helped to create by thirty years of
free trade group-think...International cooperation needs to allow national and local
communities more policy space to deal with WTO's mess while supporting their efforts
towards just transitions to sustainable development.”
<p>
<b>Fikerman Saragih, KIARA fisherfolk organization, Indonesia</b><br>
“In Indonesia there are 2.4 million fisherfolk and 3.9 women involved in the sector. The
proposed subsidy prohibitions will be risk breaking Indonesian laws and regulations, which
clearly state that small-scale fishers are entitled to subsidies from the Government including
for fuel subsidies, fisheries insurance, life insurance, fishing risk guarantees and realizing fish
prices that benefit fishers. This proposed agreement will harm fisherfolk.”
<p>
<b>Ranja Sengupta, Senior Researcher with Third World Network, India,</b><br>
“Mandated issues such as a permanent solution, special safeguards against import surges
and cotton subsidies given by the west continue to languish and need a solution in MC13.”
“Developing countries need policy space through PSH, SSM, and disciplines on cotton
subsidies to develop their agriculture sector, not be forced to fully liberalise in the name of
food security solutions.”
<p>
<b>Kinda Mohamadieh, Senior Researcher & Legal Advisor with Third World Network,
Lebanon</b><br>
“Reforming the WTO cannot be decoupled from its ability to deliver for development. The
draft ministerial declaration is currently empty from anything substantive in this regard.
Reaffirming the longstanding mandate to strengthen special and differential treatment and
the request by the African group and other developing countries on policy space for
industrial policy have been relayed to an annex that might be completely dropped. If so,
there is no value in this declaration for developing countries.”
<p>
<b>Diego Lopez, International Trade Union Confederation (ITUC)</b><br>
“We need an updating of WTO rules to prioritise social justice and respect for labour
standards.”
<p>
<b>Jane Kelsey, Professor Emeritus, Auckland University, New Zealand</b><br>
“The old power brokers thought the rules they designed in 1995 would leave them in
control of the WTO. Now they are unable to get their way they are ignoring the rules and
resorting to “reform by doing”. That’s the subtext behind vague phrases in the draft
ministerial declaration. It’s all about regaining control.<br>
Most beachhead for “reform by doing” is negotiations by breakaway subgroups in what are
called Joint Statement initiatives. They are unmandated, outside the authorised WTO bodiesand therefore unlawful. Now they are using intense political pressure, devious tactics and
abuses of process to bypass opposition and get them in through the back door.”
