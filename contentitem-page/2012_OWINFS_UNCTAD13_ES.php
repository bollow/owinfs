<p>

<p>
Domingo, 22 de abril, 2012
<p>
Para: Gobiernos y negociadores presentes en la UNCTAD XIII en Doha,
Qatar
<p>
Desde el inicio de las crisis financiera y
económica, UNCTAD ha desempeñado un papel importante en la
identificación de las causas principales de las crisis, brindándoles
asistencia a los países en desarrollo en la búsqueda de soluciones
frente a los impactos de las crisis, y abogando por la reforma de las
políticas y la gobernanza mundial económica y financiera con el fin
de prevenir la recurrencia de crisis similares. Estos son todos roles
clave que ningún otro organismo económico multilateral ha cumplido
desde una perspectiva centrada en el desarrollo. De hecho, a UNCTAD
se le reconoce haber predicho la crisis con antelación, algo que es
digno de elogio, especialmente en vista de los recursos menguados con
los que cuenta, por contraste con otros organismos como el Fondo
Monetario Internacional (FMI), la Organización Mundial del Comercio
(OMC) y la Organización para la Cooperación y el Desarrollo
Económicos (OCDE), que no anticiparon las crisis. Esta visión
estratégica de futuro está fundada en una larga historia de
contribuciones de UNCTAD al establecimiento de políticas orientadas
al desarrollo, tales como el Sistema Generalizado de Preferencias, la
meta del 0,7 por ciento del ingreso nacional bruto (INB) para ayuda
al desarrollo, la anulación de la deuda, los acuerdos
internacionales sobre productos básicos, trato especial y
diferenciado en la OMC, y la preservación del espacio político
necesario para el establecimiento de políticas, entre muchas otras
contribuciones.
<p>
A pesar de sus importantes contribuciones, a lo
largo de las negociaciones en camino a la UNCTAD XIII, los países
desarrollados han tratado de revocar el mandato primordial de UNCTAD
para trabajar en el tema de las políticas macroeconómicas y
financieras mundiales, y particularmente para <i>participar
de la gobernanza mundial</i> en estos
temas, que son tan fundamentales para la prosperidad mundial. De otra
parte, además, la UE y “JUSSCKANNZ” (Japón, Estados Unidos,
Suiza, Canadá, Corea del Sur, Australia, Noruega, Nueva Zelanda y
Liechtenstein) han intentado imponerle a UNCTAD el mandato de forzar
o presionar a los países en desarrollo a que adopten políticas de
comercio y de protección de las inversiones acordes con los
intereses corporativos de las empresas de los países desarrollados,
en lugar que al servicio de la utilización exitosa del comercio y
las inversiones para promover un crecimiento sustentable e incluyente
en los propios países en desarrollo.
<p>
<b>Los resultados de la XIII Conferencia de UNCTAD
en Doha, Qatar, del 21 a 26 de abril de 2012, tienen  que contribuir
a las transformaciones de la economía mundial necesarias para lograr
un desarrollo verdaderamente incluyente y sustentable para todos: </b>

<p>


<ol>
	<li>Es
	de vital importancia que la Declaración de UNCTAD 2012 afirme, en
	lugar de renunciar a los avances logrados en la UNCTAD XII en Accra,
	Ghana, que incluyen acuerdos sobre la necesidad de un desarrollo
	tanto sustentable como incluyente, así como la necesidad de que
	UNCTAD trabaje sobre los temas interrelacionados de finanzas,
	tecnologías, inversiones y desarrollo sustentable, entre otros
	temas clave.
</ol>
<p>


<ol start="2">
	<li>El
	análisis colectivo de las políticas debe reconocer las raíces y
	causas subyacentes de la crisis mundial, sus impactos, y darle
	mandato a UNCTAD para que continúe su excelente trabajo de
	investigación económica y financiera y análisis crítico en esas
	áreas, para poder brindarles asistencia verdadera a los países en
	desarrollo  en la generación de soluciones a la crisis, en lugar de
	presionarlos a que sigan aplicando más de las mismas políticas de
	desregulación del comercio y las inversiones, que condujeron
	originalmente a las crisis mundiales.
</ol>
<p>


<ol start="3">
	<li>Por
	último, debe fortalecerse el papel de UNCTAD como voz de
	alternativa al paradigma del ‘Consenso de Washington’ –y como
	único organismo económico multilateral enfocado en el desarrollo—
	frente a la OMC, el FMI, el Banco Mundial, la OCDE y el G20 en
	materia de toma de decisiones sobre la gobernanza económica
	mundial.
</ol>
<p>
Para lograr estas transformaciones, exhortamos a
los países desarrollados a abandonar sus presiones sobre los
negociadores del G77, y a trabajar más bien conjuntamente con los
países en desarrollo para garantizar que UNCTAD disponga de un
mandato prospectivo progresista y con visión de futuro que debe,
entre otras cosas:
<p>


<ul>
	<li>
	Identificar específicamente los orígenes,
	propagación e impactos de las crisis mundiales, y darle un mandato
	a UNCTAD que incluya brindarles asistencia a los países en
	desarrollo y asimismo trabajo de incidencia en el ámbito de la
	gobernanza mundial a favor de un crecimiento sustentable e
	incluyente.
	<li>
	Afirmar el papel clave que desempeña la
	investigación y el análisis de UNCTAD en torno a temas
	macroeconómicos y financieros, entre ellos las tasas de cambio y
	los desequilibrios mundiales, así como las políticas fiscales
	contra-cíclicas que han contribuido a la estabilización de las
	economías en tiempos de crisis mundial.
	<li>
	Incluir análisis que reconozca tanto los costos
	como las oportunidades del comercio, y oriente a UNCTAD a brindarles
	asistencia a los países en desarrollo para que puedan utilizar el
	comercio para su propio desarrollo, en lugar que solamente
	aconsejarles que se sumen a la OMC y otros tratados de “libre”
	comercio.
	<li>
	Mandatar a UNCTAD para que defina los contornos
	de un régimen mundial de comercio que este verdaderamente orientado
	al desarrollo, y en consecuencia que identifique los cambios a la
	OMC y sus negociaciones en curso, necesarios para garantizar que los
	gobiernos dispongan del espacio político indispensable para
	utilizar el comercio como herramienta para un desarrollo sustentable
	e incluyente y para legislar a favor del interés público. 
	
	<li>
	Afirmar la importancia de una reglamentación y
	supervisión adecuadas de los mercados financieros, especialmente
	asociada a la prevención y solución de crisis, y mandatar a UNCTAD
	para que desempeñe un papel activo en garantizar que haya normas
	nacionales y mundiales robustas de regulación de las finanzas.
	<li>
	Reconocer los problemas inherentes a las
	disposiciones de protección a los inversionistas en los tratados de
	libre comercio e inversiones, y mandatar a UNCTAD para que les
	brinde asistencia a los países en desarrollo en el diseño de
	políticas de inversión que beneficien su propio desarrollo
	sustentable e incluyente, y para que abogue por mejores prácticas
	--orientadas al desarrollo-- en las políticas de inversión a nivel
	mundial.
	<li>
	Reconocer el enorme impacto de la crisis en el
	empleo, y mandatar a UNCTAD para que trabaje a nivel nacional con
	los países en desarrollo a favor de la creación de empleo, y a
	nivel internacional a favor de un Programa de Trabajo Digno en
	concertación con la OIT.
	<li>
	Reafirmar la necesidad de encontrar soluciones
	para el problema de la volatilidad de los mercados mundiales de
	productos básicos, y la necesidad de un comercio justo en el
	comercio mundial agropecuario en pos de la seguridad y la soberanía
	alimentarias, y mandatar a UNCTAD para que desempeñe un papel de
	investigación e incidencia en estos temas, junto con la FAO y
	particularmente su Comité de Seguridad Alimentaria.
	<li>
	Reafirmar los compromisos de los países
	desarrollados en materia de ayuda oficial al desarrollo (AOD) y
	Ayuda para el Comercio, así como el papel clave de UNCTAD en la
	identificación de la necesidad y el diseño de mecanismos para la
	elaboración de un mecanismo de renegociación sustentable de la
	deuda soberana, reuniendo a tal efecto a diversas partes interesadas
	con el fin de establecer principios relativos a la Responsabilidad
	de los Acreedores y los Deudores y continuar con los esfuerzos de
	anulación de la deuda.
	<li>
	Presentar análisis claros sobre los impactos del
	cambio climático en el desarrollo sustentable e incluyente, y
	mandatar a UNCTAD para que contribuya a las iniciativas mundiales en
	pos de la realización del objetivo de un desarrollo sustentable e
	incluyente de cara al cambio climático. 
	
</ul>
<p>
Estos son solo algunos de los temas clave que
deben figurar como base de la declaración oficial que habrá de
orientar el papel de UNCTAD en los próximos cuatro años. En la
Declaración de la Sociedad Civil a la UNCTAD XIII se presentan
análisis más completos y detallados sobre cada uno de estos temas.
<p>
A la luz de los discursos en torno al compromiso
en pos de un sistema de gobernanza mundial más abierto, democrático
y participativo que se han convertido en lugar común en los últimos
años, consideramos que el enfoque semi-colonial de los países
desarrollados en las negociaciones de la UNCTAD es verdaderamente
vergonzoso e indignante. Hacemos publico nuestro reconocimiento a los
ex directores y ex funcionarios de UNCTAD que levantaron su voz la
semana pasada contra esta situación inadmisible, así como al G77
por su declaración pidiéndole cuentas a la UE y JUSSCKANNZ por su
intransigencia.
<p>
Como representantes de países desarrollados y en
desarrollo en muy distintas etapas de desarrollo, sabemos bien que
nuestra prosperidad está profundamente ligada al desarrollo
sustentable de todos, y por eso exhortamos a todos los gobiernos del
Norte y del Sur a actuar mancomunadamente para reforzar el papel de
UNCTAD en su trabajo en pos de un desarrollo sustentable e incluyente
para todos.
<p>
Firman, 

