<p>

<p>
Dimanche, 22 Avril 2012 		
<p>
<b>Aux: Gouvernements et Négociateurs de la
CNUCED XIII à Doha, au Qatar</b>
<p>
Depuis le début de la crise financière et
économique mondiale, la CNUCED  a joué un important rôle en
identifiant les causes majeures de ces crises, en apportant son
assistance aux pays en développement dans la recherche des solutions
aux impacts de ces crises et en plaidant pour une réforme de
l’économie mondiale, des politiques financières et de la
gouvernance afin d’éviter que des crises similaires ne se
reproduisent. Il s’agit là des rôles clés qu'aucune autre
institution multilatérale économique n’a remplis dans une logique
de développement. 

<p>
En réalité, la CNUCED a eu le mérite d’avoir
prédit la crise à l'avance et doit en être félicité, en
particulier compte tenu de ses ressources limitées par rapport à
d’autres institutions telles que le Fonds monétaire international
(FMI), l'Organisation mondiale du commerce (OMC), et l'Organisation
de coopération et de développement économiques (OCDE), qui n’ont
pas pu faire la même chose. Cette capacité d’anticipation et la
clairvoyance dont la CNUCED a fait preuve résultent d’une longue
histoire de contributions positives axées sur le développement des
politiques, au nombre desquelles on peut citer&nbsp;: le Système
généralisé de préférences, les 0,7% du Revenu National Brut des
pays développés à consacrer à l’aide au développement,
l'annulation de la dette, les accords internationaux sur les matières
premières, le traitement spécial et différencié à l'OMC, et
l'espace politique.
<p>
En dépit de ces importantes contributions, tout
au long des négociations qui ont abouti à la CNUCED XIII, les pays
développés ont tenté d'annuler l'important mandat de la CNUCED
pour travailler sur les questions de politiques macro-économiques
mondiales et de la finance, et en particulier de participer à la
gouvernance mondiale sur ces questions essentielles pour la
prospérité mondiale. En outre, l'UE et les Etats du groupe
&quot;JUSSCKANNZ&quot; (Japon, États-Unis, Suisse, Canada, Corée du
Sud, Australie, Norvège, Nouvelle-Zélande, et le Lichtenstein) ont
cherché à imposer un mandat de la CNUCED destiné à pousser les
pays en développement à adopter la protection des investisseurs et
les  politiques commerciales qui promeuvent les intérêts des
entreprises des pays développés, au lieu de mettre le commerce et
l'investissement au service d’une croissance durable et inclusive
dans les pays en développement. <b>Les
résultats de la CNUCED XIII du 21-26 Avril 2012 à Doha, au Qatar
doivent contribuer à des transformations de l'économie mondiale qui
sont nécessaires pour un véritable développement inclusif et
durable pour tous: </b>

<p>
1. Il est essentiel que la Déclaration de 2012 de
la CNUCED réaffirme, plutôt que de réexaminer, les progrès
réalisés à la CNUCED XII à Accra. Cela comprend un accord sur la
nécessité d’une croissance inclusive et d’un développement
durable, ainsi que la nécessité pour la CNUCED de travailler sur
les questions en relation avec la finance, la technologie,
l'investissement et le développement durable, entre autres questions
clés. 

<p>
2. L'analyse politique collective doit reconnaître
les causes profondes de la crise mondiale et ses impacts, et mandater
la CNUCED de poursuivre ses excellentes recherches dans les domaines
économique et financière soutenues par une analyse critique, afin
d’aider véritablement les pays en développement à trouver des
solutions appropriées aux crises, au lieu de les pousser à mettre
en œuvre pratiquement les mêmes politiques de dérégulation du
commerce et des investissements qui avaient conduit, en premier lieu,
aux crises mondiales.
<p>
3. Enfin, le rôle de la CNUCED en tant que voix
alternative au paradigme du «Consensus de Washington&quot; – comme
la seule institution multilatérale orientée vers les questions de
développement économique - doit être renforcé vis-à-vis de
l'OMC, du FMI, de la Banque mondiale, de l'OCDE, et du G20 en matière
de prise de décision sur la gouvernance économique mondiale.
<p>
Pour la réalisation de ces transformations, nous
lançons un appel aux pays développés afin qu’ils cessent de
mettre la pression sur les négociateurs du G77, et qu’ils
travaillent plutôt ensemble avec les pays en développement afin
d'assurer un mandat d'avenir pour la CNUCED, lequel mandat doit,
entre autres questions : 

<p>


<ul>
	<li>
	Prendre en compte spécialement les origines, les
	manifestations et les impacts de la crise mondiale, et mandater la
	CNUCED pour jouer un rôle qui inclut aussi bien l’assistance
	technique aux pays en développement que le plaidoyer dans la sphère
	de la gouvernance mondiale pour la promotion d’une croissance
	inclusive et durable&nbsp;; 
	
	<li>
	Réaffirmer le rôle clé de la recherche et de
	l'analyse sur les politiques macroéconomiques et les questions
	financières  (y compris les taux de change et les déséquilibres
	mondiaux, ainsi que des politiques budgétaires anticycliques)
	menées par la CNUCED, qui ont contribué à stabiliser les
	économies pendant la crise mondiale&nbsp;; 
	
	<li>
	Inclure l'analyse qui met l’accent sur les
	coûts ainsi que les opportunités liées au commerce, et orienter
	la CNUCED dans son assistance aux pays en développement en vue de
	mettre le commerce au service de leur développement, plutôt que de
	les conseiller simplement à adhérer à l'OMC et à d'autres
	accords de libre échange.
</ul>
<ul>
	<li>
	Déterminer les contours du cadre commercial
	mondial qui soit véritablement orienté vers le développement, et
	identifier les changements actuels au sein de l’OMC et les
	négociations en cours ou qui se poursuivent qui sont nécessaires,
	pour assurer que les gouvernements ont l’espace politique pour
	utiliser le commerce au service d’un développement inclusif et
	durable et de faire la régulation dans l’intérêt public&nbsp;; 
	
	
	<li>
	Réaffirmer l’importance d’une supervision et
	d’une  régulation adéquate des marchés financiers,
	particulièrement en ce qui concerne la prévention et la résolution
	des crises et lui permettre de jouer un rôle actif pour s’assurer
	que les règles rigoureuses de régulation financière au niveau
	national et régional soient observées&nbsp;; 
	
	<li>
	Reconnaitre les problèmes de la protection des
	investisseurs dans les accords de commerce et d’investissement et
	renforcer son rôle d’appui aux pays en développement pour
	formuler des politiques d’investissement qui leur permettront de
	bénéficier des fruits d’une croissance inclusive et durable et
	faire également du plaidoyer sur des bonnes pratiques en matière
	de politique d’investissement en général&nbsp;;  
	
	<li>
	Reconnaître les impacts importants de la crise
	sur l’emploi et lui permettre de travailler au niveau national
	dans les pays en développement en faveur de la création d’emploi
	et au niveau international en faveur de l’agenda sur le travail
	décent en partenariat avec <b>l’OIT&nbsp;;
	</b>
	
	<li>
	Réaffirmer les besoins de trouver des solutions
	au problème de la volatilité des prix des produits de base au
	niveau mondial et la nécessité d’un commerce équitable dans le
	commerce agricole mondial qui garantit la sécurité et la
	souveraineté alimentaire&nbsp;; puis encourager la recherche et le
	plaidoyer sur ces questions de concert avec la FAO, notamment son
	Comité sur la Sécurité Alimentaire&nbsp;;
	<li>
	Réaffirmer les engagements des pays développés
	en matière d’aide publique au développement et d’aide au
	commerce de même que son rôle clé dans l’identification des
	besoins et le développement de mécanismes de sortie durable de la
	dette souveraine en mettant ensemble les parties prenantes pour
	créer des principes responsables en matière de prêt et d’emprunt
	tout en poursuivant les efforts vers l’annulation de la dette.&nbsp;;
	<li>
	Mettre en place une analyse claire de l’impact
	des changements climatiques sur le développement inclusif et
	durable et contribuer à l’effort mondial pour la réalisation des
	objectifs de développement inclusif et durable en matière de
	changement climatique&nbsp;;
</ul>
<p>
Ce qui précède ne représente que quelques
éléments des questions clés qui doivent constituer la  base de la
déclaration officielle sur le rôle de la CNUCED durant les quatre
prochaines années. Une analyse plus claire de chacune de ces
questions est détaillée dans la déclaration officielle de la
société civile pour la CNUCEDXIII.
<p>
Au vu de la rhétorique entourant les engagements
pour un système de gouvernance mondiale ouvert, transparent et
démocratique qui est devenu une préoccupation commune ces dernières
années, nous trouvons l’approche semi-coloniale des pays
développés dans les négociations avec la CNUCED outrageuse. Nous
soutenons la position des anciens dirigeants et du personnel de la
CNUCED qui ont dénoncé cette situation inacceptable la semaine
dernière, de même que la déclaration du G77 qui appelait l’UE et
le groupe JUSSCKANNZ à rendre compte de leur position
intransigeante.   

<p>
En tant que représentants des pays dévelopés et
en développement à different stades de développement, nous savons
que notre prospérité est profondément ancrée dans le
développement durable de tous et nous appelons les gouvernements des
pays du nord et du sud de s’unir pour réaffirmer un rôle
important de la CNUCED dans les efforts vers un développement
inclusif et durable pour tous.  

<p>
