<p>28
de abril de 2021
<p>Estimados
Jefes de Estado:
<p>En
naciones alrededor del mundo, el sistema hiperglobalizado de
comercio, inversiones y cadenas de suministro propugnado e
implementado por la Organización Mundial del Comercio (OMC) durante
sus 25 años de existencia está al borde del colapso. La pandemia
del COVID-19 ha expuesto cómo el modelo de la OMC exacerba la
inseguridad, la desigualdad, y la inestabilidad. Unas reglas de
comercio legítimas deberían facilitar el mejoramiento de la vida,
salud y estabilidad de la humanidad y la supervivencia del planeta.
El sistema de la OMC no ha cumplido estos objetivos: Nunca ha sido
apto para este propósito y con seguridad no lo es ahora.
<p>Esta
revelación viene durante una crisis existencial para la OMC porque
en los últimos años no solo ha fallado en mejorar vidas, sino que
en muchos países ha generado daño substancial al empoderar a
intereses farmacéuticos, agroindustriales, financieros y otros
intereses corporativos en países de ingresos altos para dominar las
economías en detrimento de trabajadores en países de ingresos altos
y bajos. Sus funciones de negociación y cumplimiento están
paralizadas y las divisiones se hicieron evidentes por la salida
anticipada del antiguo Director General de la organización.
<p>Nosotros,
las organizaciones laborales, ambientales, de consumidores, y otras,
exigimos una transformación de la OMC que lleve a una estructura de
comercio internacional completamente nueva ajustada al siglo XXI que
priorice la humanidad y el planeta.
<p>Esta
exigencia no es nueva. Como predijimos en 1995, la OMC ha funcionado
para establecer reglas para la economía global que principalmente
han beneficiado corporaciones transnacionales en detrimento de
economías locales y nacionales, trabajadores, campesinos y
poblaciones indígenas, nuestra salud y seguridad y el medioambiente.
Sin un piso de protección laboral, la carrera de mínimos ha
reprimido el crecimiento salarial y ha aumentado el trabajo precario.
Las crisis del clima, la biodiversidad, y la pobreza han sido
ignoradas y sus soluciones han sido restringidas por “reglas de
comercio”. El documentado crecimiento de la desigualdad dentro de y
entre países ha sido también ignorado, mientras tanto los gobiernos
han perdido las herramientas esenciales para mejorar el bienestar de
la población y combatir los efectos negativos de la
hiperglobalización.
<p>En
la actualidad, las cadenas de suministro frágiles e integradas que
fueron creadas por las reglas de la OMC han afectado las luchas de
varios países contra el COVID-19. Alrededor del mundo, los países
no pueden crear u obtener tapabocas, kits de diagnóstico,
ventiladores, medicinas y otro equipo médico necesario. Esto es
porque décadas de reglas de la OMC han priorizado las exigencias de
corporaciones para concentrar la producción global en aras de
maximizar ganancias, y han prohibido el uso de herramientas de
política pública para asegurar producción local y diversidad de
fuentes de importación. Las reglas de propiedad intelectual de la
OMC están diseñadas para maximizar las ganancias de corporaciones
farmacéuticas, en vez de la salud pública, aumentando los precios
de medicinas que son esenciales para combatir el COVID-19 y pueden
crear barreras para el acceso universal a vacunas y tratamientos.
<p>La
OMC en sí misma se está fracturando. El antiguo Director General
renunció abruptamente. El sistema de resolución de disputas se
deshizo al final de 2019; este sistema ha forzado que países cambien
sus políticas legítimas para proteger la salud pública, fortalecer
desarrollo, proteger el medioambiente y combatir la crisis climática,
al riesgo de enfrentar sanciones comerciales. Sus funciones de
negociación han estado en riesgo desde su primer intento de empezar
una nueva ronda en la reunión Ministerial de Seattle 1999. Consensos
entre países en desarrollo fueron ignorados, pero los esfuerzos de
países ricos para imponer su agenda en contra de la oposición la
mayoría de países, también fallaron. Después, un nuevo intento
comenzó en 2001, con la llamada Ronda de Desarrollo de Doha, la cual
invocaba una necesidad de unidad siguiendo los ataques del 9/11 en
Estados Unidos. Los países en desarrollo recibieron promesas de
protecciones a sus intereses; sin embargo, en los últimos 19 años,
la misma agenda expansiva de la OMC ha sido priorizada y los
objetivos de desarrollo fueron marginalizados.
<p>Dada
la disfuncionalidad de la OMC, naciones ricas y poderosas han
empezado negociaciones plurilaterales de nuevas reglas que priorizan
las ganancias y derechos de las corporaciones. Estas reglas se
encuentran con oposición cuando se promueven en la OMC u otras
organizaciones, y su fracaso demuestra un paradigma deslegitimado. El
Acuerdo sobre Comercio de Servicios (Trade in Services Agreement -
TiSA) y el Acuerdo Transatlántico sobre Comercio e Inversión
(Transatlantic Trade and Investment Partnership - TTIP) han colapsado
completamente; el Acuerdo Transpacífico de Cooperación Económica
(Trans-Pacific Partnership - TPP) no pudo ganar una mayoría en el
congreso estadounidense durante un año después de su suscripción y
los EE. UU. Finalmente no entraron en el acuerdo; e India se retiró
de la Asociación Económica Integral Regional (Regional
Comprehensive Economic Partnership - RCEP), para nombrar algunos
ejemplos.
<p>En
vez de aprender de estos errores, o por lo menos reconocer las
promesas rotas, intereses poderosos en la OMC están redoblando para
lograr más de lo mismo. En lo que pareciera una parodia de un
martillo que solo ve puntillas, la respuesta de la OMC frente al
COVID-19 es mantener y expandir las mismas políticas de
liberalización, incluyendo un acuerdo absolutamente contraproducente
para reducir a cero los aranceles sobre bienes relacionado con el
COVID-19. Justo en el contexto de la pandemia, las negociaciones han
seguido limitando la regulación doméstica en el sector de
servicios, incluso cuando la concentración de empresas de servicios
pone un impedimento a la adquisición y distribución de suministros
esenciales. Negociaciones para limitar regulación de inversores
extranjeros siguen, a pesar de la necesidad demostrada de
diversificar la producción de equipo de protección personal y
medicinas. Negociaciones que darían a corporaciones digitales más
control sobre los datos personales de la población y la economía
digital siguen, mientras que varios países están trabajando en
limitar el poder de estas empresas y a pesar de que estas
negociaciones han sido explícitamente rechazadas.
<p>Estos
fracasos no se pueden ignorar. El cuerpo global original de comercio
– la Organización Internacional de Comercio que fue estructurada
en la Carta de la Habana de 1948 en respuesta a los horrores y el
caos de la Segunda Guerra Mundial - se focalizaba en el pleno empleo,
limitar concentración corporativa, competencia justa, protecciones
para trabajadores y requerimientos para que la política cambiaria u
otras no distorsionen el comercio global. Esa visión diferente para
un sistema de reglas para el comercio global – actualizada para que
reconozca la crisis climática, la desigualdad sistémica y el poder
de las corporaciones digitales - todavía es alcanzable, pero solo si
los países concuerdan en que las reglas de comercio global tienen
que proteger a la humanidad, no a las corporaciones más poderosas.
<p>La
decisión no es entre el statu quo y ausencia de comercio. Esa es la
cortina de humo que corren quienes quieren que nada cambie. El cambio
está sucediendo ahora. La pregunta es qué tipo de estructura
multilateral puede ser incluyente, promover sustentabilidad real,
derechos humanos y prosperidad para todos, y crear beneficios de
comercio expandido para la mayoría de la humanidad, facilitando a
nuestros representantes el espacio regulatorio para promover el
interés público. Un ejemplo serían los <u><a href="https://unctad.org/en/pages/newsdetails.aspx?OriginalVersionID=2057">principios
de Ginebra para un “Green New Deal” global</a></u>.
<p>Invitamos
a los gobiernos para que usen esta oportunidad para implementar un
cambio realmente transformativo.
