<p>

<p>Septembre
2021
<p>1.
Les populations du monde entier ont besoin d'une économie qui
facilite une prospérité partagée avec des droits de l'homme, du
travail, sociaux et économiques qui facilitent, au minimum, la mise
en œuvre de l'Agenda 2030 pour le développement durable, adopté
par les États membres des Nations unies en septembre 2015 en tant
qu'Objectifs de développement durable (ODD). Au lieu de contribuer à
cette vision, le système commercial multilatéral est en crise parce
qu'il n'a pas réussi à assurer une prospérité partagée et bafoue
les droits universels des personnes. En effet les élites des
entreprises ont détourné le &quot;commerce&quot; et truqué les
règles du système multilatéral pour distribuer les revenus à la
hausse, tout en réduisant les protections des personnes qui
travaillent. La promesse d'un accroissement des échanges commerciaux
qui crée des situations gagnant - gagnant s'est révélée être un
mensonge. Au lieu de cela, la majorité des gens ont vécu
l'accroissement des échanges comme une course vers le bas en matière
de salaires, de précarité, de services publics, de normes de
consommation et environnementales. La crise du préjudice réel que
l'ordre économique néolibéral, encapsulé par l'OMC, et les
politiques nationales d'austérité font subir aux populations du
monde entier a entraîné des soulèvements sur quatre continents,
des migrations massives et l'élection de gouvernements populistes de
droite dans de nombreux pays. L'instabilité sociale, qui touche
particulièrement les femmes, les populations marginalisées, les
pauvres et les peuples indigènes, est le résultat de ces échecs,
tout comme la crise de l'OMC est de sa propre responsabilité. Les
gouvernements doivent s'attaquer aux défauts fondamentaux du système
actuel de mondialisation dirigé par les entreprises, et non étendre
et consolider les déséquilibres du pouvoir. La solution aux
attaques actuelles contre la politique commerciale dominée par les
entreprises n'est pas un faux nationalisme, mais une nouvelle vision
du multilatéralisme qui aboutira à une stabilité écologique et à
une prospérité partagée. Un modèle pour parvenir à une telle
économie est décrit dans &quot;Un nouveau multilatéralisme pour
une prospérité partagée&quot; : Principes de Genève pour un New
Deal vert.”<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym"><sup>1</sup></a></sup>
 

<p>2.
La pandémie de COVID-19 a révélé comment le modèle de l'OMC
exacerbe l'insécurité, l'inégalité et l'instabilité.
Aujourd'hui, les chaînes d'approvisionnement profondément intégrées
créées par les règles de l'OMC au cours des dernières décennies
ont sapé la lutte de nombreux pays contre la pandémie de COVID-19.
Dans le monde entier, les pays ne peuvent pas fabriquer ou obtenir
des masques, des kits de test, des ventilateurs, des médicaments,
des vaccins et d'autres équipements nécessaires. Des décennies de
règles de l'OMC ont donné la priorité aux demandes des grandes
entreprises de concentrer la production mondiale pour maximiser leurs
profits et ont interdit aux pays d'utiliser des outils politiques
pour garantir la capacité de production locale et la diversité des
fournisseurs d'importation. Les règles de l'OMC sur la propriété
intellectuelle, conçues pour maximiser les profits des entreprises
pharmaceutiques plutôt que la santé publique, ont fait grimper les
prix et créé des pénuries de médicaments essentiels à la lutte
contre le COVID-19 dans de nombreux pays, entraînant des décès et
une augmentation des maladies dans le monde entier qui auraient pu
être évités.
<p>3.
Alors qu'un système multilatéral qui régit le commerce et
discipline le comportement des entreprises est essentiel, le système
spécifique de règles incarné par l'OMC devrait être attaqué car,
au cours de ses 25 années d'existence, il n'a pas réussi à assurer
le développement, à promouvoir une prospérité partagée et à
garantir la durabilité du commerce. Les prédictions d'une
croissance partagée dans le cadre du système de l'OMC ont échoué
lamentablement, comme l'avaient prédit nombre de nos organisations
au moment de la création de l’organisation, tandis que les
inégalités ont grimpé en flèche, appauvrissant des centaines de
millions de personnes. La plupart des pays en développement qui ont
tiré profit du commerce l'ont fait en exportant vers la Chine, un
pays dont la croissance est généralement attribuée à ses
divergences avec le modèle de l'OMC, notamment en ce qui concerne la
politique industrielle. Alors que certains pays en développement ont
augmenté leur part dans le commerce mondial, leur part des revenus
issus du commerce mondial a été réduite par les sociétés
transnationales (STN) qui gardent pour elles les segments à haute
valeur, tout en externalisant les opérations à faible valeur vers
les pays en développement. Ceux-ci se retrouvent pris au piège de
stratégies de développement menées dans des ateliers de misère.
Ainsi, dans la plupart des pays en développement, l'élargissement
de la participation aux marchés mondiaux n'a pas permis d'atteindre
le développement économique promis.
<p>4.
L'OMC a été fondée sur le mandat que le commerce doit élever le
niveau de vie et garantir le plein emploi. Cependant, alors même que
la productivité a grimpé en flèche depuis la création de l'OMC,
les salaires des travailleurs et les revenus des personnes qui
travaillent sont restés aux mieux stables, voire ont diminué par
rapport au capital. Cela s'explique par le fait que les entreprises
ont utilisé les profits accrus qu'elles ont réalisés pour investir
dans la domination du processus d'élaboration des politiques, en
particulier par le biais de l'OMC ainsi que des accords commerciaux
bilatéraux et régionaux et des accords d'investissement, afin de
s'assurer que les politiques façonnent l'économie pour leur
distribuer des revenus. Il ne faudrait pas se contenter de dépendre
des politiques nationales de redistribution pour &quot;compenser les
perdants&quot; des règles néfastes des accords commerciaux. Les STN
doivent être tenues, en vertu du droit national et international, à
intégrer la diligence raisonnable en matière de droits de l'homme
dans toute la chaîne d'approvisionnement, et à garantir des
mécanismes de réclamation, l'accès aux recours et la pleine mise
en œuvre des Principes directeurs des Nations unies sur les
entreprises et les droits de l'homme et des Principes et droits
fondamentaux au travail de l'OIT en tant que norme minimale. Les
politiques de développement qui favorisent les bons emplois et
l'industrialisation ne doivent pas être entravées par les règles
du commerce mondial. Nous avons besoin d'un nouveau contrat social
avec un plancher de protection du travail qui comprend un travail
décent, des services publics universels de qualité et une
couverture de protection sociale, une diligence raisonnable en
matière de droits de l'homme et une responsabilité des entreprises,
un dialogue social et une transition juste vers une économie à zéro
carbone, ainsi que de nouvelles politiques antitrust au niveau
mondial, qui ne doivent pas être sapées par les règles du commerce
mondial.
<p>5.
En même temps, l'avenir des humains sur terre est menacé par notre
propre surconsommation de carbone, qui a entraîné un changement
climatique catastrophique, lequel se traduit actuellement par des
calamités environnementales causant des pertes dévastatrices de la
vie humaine, animale et végétale. Le commerce est un facteur majeur
du changement climatique, et les règles de l'OMC qui facilitent les
systèmes de production à forte teneur en carbone - tout en limitant
le transfert de technologies vertes et les subventions en faveur
d'une économie énergétique durable - ne sont plus viables.
<p>6.
Les entreprises, par le biais d'accords &quot;commerciaux&quot;,
accumulent des droits aux dépens des personnes qui travaillent, mais
elles le font aussi aux dépens des États démocratiques. Il en
résulte une érosion de la démocratie, car les États ne respectent
pas le contrat social. Les entreprises ont influencé les règles
commerciales de l'OMC pour permettre la facturation abusive des
échanges, ce qui leur a permis d'éviter ou d'éluder les taxes
nécessaires au bon fonctionnement de la société. Cette situation,
combinée à une plus grande concentration de la richesse facilitée
par les règles commerciales, a créé des concentrations de pouvoir
inacceptables qui alimentent des niveaux d'inégalité dangereux. En
exigeant des engagements de déréglementation et de propriété
étrangère des services, les règles commerciales contribuent
également à la privatisation qui a érodé la fourniture des
services publics, et limitent le droit des États à réglementer les
STN - deux éléments qui augmentent encore le pouvoir des
entreprises privées sur tous les aspects de la vie humaine.
L'influence excessive des entreprises est également de plus en plus
évidente dans d'autres institutions telles que les Nations unies et
en particulier dans l’ODD 17 qui vise à faciliter une plus grande
influence des entreprises sur la gouvernance mondiale.
<p>7.
L'État a l'obligation première de réaliser les droits de l'homme
de ses citoyens. Sa capacité à le faire dépend de son aptitude à
s'adapter aux défis futurs, à exercer ses prérogatives politiques,
à établir, maintenir et développer les services publics et à
réguler librement le capital. Le régime commercial actuel ne
sauvegarde pas ces fonctions essentielles de l'État et les détourne
de la logique et des besoins du commerce.   

<p>8.
Les règles de l'OMC sur l'agriculture limitent la capacité des pays
à protéger leur propre développement rural, les moyens de
subsistance des agriculteurs, ainsi que la sécurité et la
souveraineté alimentaires partout dans le monde. Elles ont contribué
à de multiples crises alimentaires et à l'érosion de la sécurité
alimentaire nette dans de nombreux pays.
<p>9.
La stabilité et la prévisibilité financières et économiques sont
essentielles pour une prospérité partagée. Toutefois, les faits
montrent que si les règles actuelles de l'OMC ont peut-être créé
une plus grande prévisibilité pour les investisseurs et les
sociétés commerciales, les travailleurs et les communautés sont
confrontés à une précarité croissante du travail, à
l'externalisation, au démantèlement des services publics et à une
course vers le bas dans laquelle ils doivent faire face à des
bouleversements sans précédent dans l'économie mondiale. Ceux-ci
sont souvent causés par des crises financières et des changements
rapides dans la structure des échanges, alors que très peu d'entre
eux bénéficient d'une protection sociale adéquate.
<p>10.
L'OMC est unique parmi les organisations multilatérales en ce
qu'elle ne facilite absolument pas la participation de la société
civile, des syndicats et des communautés de base qui sont affectées
négativement par ses politiques. Ce alors même que de nombreux
représentants d'entreprises ont un accès étroit au processus
d'élaboration des politiques, y compris à la rédaction de nombreux
textes d'accord. Les membres de l'OMC sont de plus en plus discrets
sur les textes et les négociations, ce qui exclut toute
responsabilité et tout débat public. Maintenant le Secrétariat
s'engage activement dans des partenariats avec la communauté
mondiale des entreprises pour accroître son influence, tout en ne
parvenant pas à remédier à l'exclusion des représentants de la
société civile du processus de délibération à l'OMC. De manière
encore plus flagrante, le Secrétariat plaide ouvertement et
publiquement en faveur d'un élargissement des disciplines de l'OMC
dans des domaines tels que l'économie numérique, au profit de
quelques membres et des STN de haute technologie basées dans ces
pays. En même temps, il manque complètement à son obligation de
faciliter la conclusion de négociations longtemps bloquées afin
d'améliorer les règles existantes de l'OMC, qui sont
préjudiciables, pour permettre au commerce de promouvoir un
développement réel, la durabilité et une prospérité partagée.
Enfin, la véritable crise du mécanisme de règlement des différends
(MRD) réside dans le fait qu'il statue sur des affaires en utilisant
des règles qui privilégient les droits commerciaux des entreprises
par rapport à l'intérêt public : dans 80 affaires impliquant des
politiques d'intérêt public, l'OMC a statué en faveur du droit des
entreprises à commercer 73 fois.<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym"><sup>2</sup></a></sup>
En outre, les pays puissants ont été beaucoup plus à même de
faire valoir leurs droits au sein de l'OMC grâce au MRD que les pays
pauvres, un fait qui est rarement mentionné dans les débats sur la
branche judiciaire de l'OMC. Il est tout à fait inapproprié
d'envisager d'étendre la portée du modèle de l'OMC, qui a échoué,
sur la vie humaine.
<p><b>Ainsi,
en tant que membres et alliés du réseau mondial de la société
civile Notre monde n'est pas à vendre (OWINFS), engagés dans un
système commercial multilatéral durable, socialement juste,
démocratique et responsable, nous appelons à un nouveau système de
règles multilatérales mondiales.</b>
<p>11.
Nombre d’organisations ci-dessous ont longtemps critiqué les
règles de l'OMC, les jugeant nuisibles et inappropriées pour la
gouvernance du commerce mondial. À l'heure actuelle, les résultats
de près de 25 ans d'existence de l'OMC ont donné raison à ses
détracteurs&nbsp;: ce n'est pas le bon système pour gouverner le
commerce en vue d'une prospérité partagée. Il faut donc envisager
un nouveau système de règles commerciales multilatérales qui fera
passer la prospérité partagée, la durabilité environnementale et
la réalisation des Objectifs de Développement Durable de l'Agenda
2030 avant les intérêts commerciaux des puissantes entreprises.
Compte tenu de l'histoire de l'OMC, qui n'a pas réussi à résoudre
les intérêts des pays en développement, qui a fait passer les
droits des STN avant les droits des citoyens et des travailleurs et
les obligations environnementales, et le Secrétariat, qui a joué un
rôle biaisé en faisant passer les intérêts des membres et des
élites puissantes avant les intérêts des pays en développement et
des travailleurs, rien ne prouve que l'OMC elle-même soit capable
d'apporter les changements transformationnels nécessaires au système
commercial mondial. Une nouvelle vision du multilatéralisme est donc
nécessaire afin de créer les emplois, les infrastructures et les
services indispensables à la réalisation des ODD, mais aussi pour
sauvegarder notre planète pour les générations futures. Ce nouveau
multilatéralisme doit impliquer un nouvel ensemble de règles pour
régir le commerce mondial.
<p>12.
En attendant, les gouvernements doivent apporter des <i>changements
transformationnels aux règles existantes</i>.
Les règles de l'OMC limitent trop souvent la capacité des
gouvernements à utiliser les politiques de développement
traditionnelles - qui ont été utilisées par tous les pays
industrialisés dans leur développement - pour promouvoir l'emploi
et les industries nationales. Malgré les affirmations hypocrites des
pays développés et des élites mondiales à l'OMC, ceux-ci ont
bloqué la résolution de l'agenda de développement à l'OMC pendant
près de 20 ans. Les règles du Traitement spécial et différencié
(TSD) doivent être renforcées et rendues opérationnelles pour tous
les pays en développement, en mettant l'accent sur l'exonération
des règles de l'OMC préjudiciables qui limitent le recours à des
politiques de développement fondées sur des preuves. Dans
l'immédiat, cela signifierait que les gouvernements disposeraient
d'une marge de manœuvre politique pour&nbsp;: assurer la sécurité
alimentaire nationale, utiliser le transfert de technologies et les
droits sur les données, recourir à des exigences de performance qui
créent des emplois, telles que les exigences de contenu local et de
main-d'œuvre locale, et disposer d'une plus grande flexibilité pour
garantir un accès abordable aux médicaments, entre autres
priorités.
<p>13.
Nous demandons également l'arrêt immédiat des efforts visant à
étendre la portée et la couverture des règles nuisibles, ou à les
étendre à de nouveaux domaines. En particulier, nos gouvernements
doivent mettre un terme aux efforts déployés par les entreprises de
haute technologie pour utiliser l'OMC afin d'obtenir de nouveaux
droits en faveur des entreprises dans l'économie numérique, et pour
entraver la surveillance réglementaire appropriée. Nous appelons à
l'arrêt des efforts : pour déréglementer l'ensemble de l'économie
numérique par le biais des négociations sur le &quot;commerce
électronique&quot;, qui portent un nom inapproprié ; pour
introduire plus de disciplines dans les politiques d'investissement
par le biais de négociations sur la &quot;facilitation des
investissements&quot; ; pour empiéter davantage sur la souveraineté
réglementaire par le biais de négociations sur la &quot;réglementation
intérieure&quot; ; pour relancer les discussions sur la
libéralisation des services ; et pour limiter les flexibilités en
matière de développement dans les négociations sur les subventions
à la pêche. Les promoteurs des entreprises entreprennent ces
négociations par le biais de négociations &quot;plurilatérales&quot;
qui sont illégales dans le cadre de l'OMC car elles n'ont pas de
mandat, la plupart des pays en développement s'y opposant - pour de
bonnes raisons.
<p>14.
Les règles actuelles de l'OMC confèrent des droits au commerce et
limitent la manière dont celui-ci peut être réglementé. Mais les
gouvernements ne font pas de commerce, ce sont les entreprises qui en
font. Ainsi, les &quot;droits&quot; de l'OMC sont exercés par les
intérêts des entreprises, tandis que les disciplines de l'OMC
limitent le pouvoir des gouvernements de réglementer ce commerce
dans l'intérêt du public. &quot;Rédiger des règles commerciales&quot;
est en fait un terme impropre pour &quot;restreindre la
réglementation tout en conférant des privilèges aux entreprises&quot;.
Ce n'est pas la bonne approche pour &quot;réglementer&quot; le
commerce. Nous avons plutôt besoin d'un système qui favorise une
prospérité partagée tout en sauvegardant les droits de l'homme,
les droits du travail, les droits économiques et sociaux. Et qui
laisse aux pays la marge de manœuvre politique nécessaire pour
poursuivre des stratégies fondées sur des preuves en vue de la
création d'emplois décents et de l'industrialisation, et qui tienne
les entreprises responsables, sous peine de sanctions, de tous les
abus dans leurs chaînes d'approvisionnement et leurs pratiques
commerciales. Nous appelons, dans le cadre de la nouvelle
architecture multilatérale, à l'adoption immédiate d'un traité
contraignant sur les sociétés transnationales et les droits de
l'homme, actuellement en cours de négociation aux Nations unies.
<p>Plus
précisément, nous appelons les gouvernements à transformer le
système commercial mondial par les moyens suivants :
<p>


<ol>
	<li>
	<b>Nous
	exigeons un travail décent et un espace politique pour le
	développement industriel</b>
</ol>
<p>Plutôt
que d'utiliser le commerce pour accroître l'emploi et la prospérité,
les règles de l'OMC se concentrent sur la réduction des droits de
douane et l’abaissement de la réglementation et sur d'autres
politiques qui augmentent le pouvoir de négociation du capital au
détriment du travail. Cela pousse les travailleurs vers une
concurrence déloyale, qui se traduit par l'absence de salaire
minimum vital et de protection du travail, une précarité accrue de
l'emploi, l'exploitation des femmes et des environnements de travail
dangereux. La liberté de mouvement accrue du capital et la
mondialisation induite par l'OMC ont déclenché une course vers le
bas où les pays et les zones d'exportation spéciales se font
concurrence pour réprimer les droits du travail afin d'attirer les
investissements. Les chaînes d'approvisionnement mondiales (le
modèle de production dominant aujourd'hui) sont caractérisées par
l'esclavage, le travail des enfants et toutes les formes
d'exploitation du travail, qui touchent les femmes de manière
disproportionnée. En outre, les entreprises mondiales cherchent à
introduire de nouvelles règles pour conférer davantage de droits
aux investisseurs au détriment des travailleurs, des régulateurs et
du public en général, par le biais de négociations en vue d'un
accord sur la &quot;facilitation des investissements&quot;. Nous
avons plutôt besoin d'un système commercial mondial qui se
concentre sur l'augmentation des possibilités d'emploi décent et du
niveau de vie des personnes qui travaillent.
<p>


<ul>
	<li>
	Tout
	accord actuel ou futur doit se concentrer sur l'utilisation du
	commerce pour promouvoir et étendre le travail décent avec un
	nouveau contrat social et un plancher de protection du travail,
	comme le demande la déclaration du centenaire de l'OIT, et sur la
	protection de l'espace politique des pays pour poursuivre une
	prospérité partagée par le plein emploi, par la transformation
	structurelle et l'industrialisation et par des salaires plus élevés
	et le dialogue social.
	<li>
	Il
	faut mettre un terme aux discussions visant à introduire de
	nouvelles règles en matière d'investissement au sein de l'OMC par
	le biais de la &quot;facilitation des investissements&quot;.
	<li>
	En
	outre, nous demandons des évaluations de l'impact social, notamment
	sur les inégalités, la qualité de l'emploi et la création
	d'emplois pour les changements actuels et futurs potentiels des
	politiques et des négociations de l'OMC. Nous demandons aussi les 
	modifications des règles existantes nécessaires pour garantir un
	système commercial multilatéral favorisant le plein emploi et le
	travail décent.
</ul>
<p><b>2.
Nous exigeons un espace politique pour l'industrialisation numérique
et la capacité d'utiliser les données dans l'intérêt public</b>
<p>Aujourd'hui,
les plus grandes entreprises cherchent à utiliser l'OMC pour
réécrire les règles afin de consolider légalement leurs pratiques
monopolistiques par l'accès et le contrôle permanent de la
ressource la plus précieuse du monde, les données. Mais les données
ne doivent pas être contrôlées par des sociétés privées pour la
maximisation du profit privé. Au contraire, de nombreuses personnes
et de nombreux gouvernements commencent à réaliser la nécessité
d'une propriété nationale et communautaire des données, et de la
souveraineté des données. Tous les pays et les communautés
devraient avoir le droit d'utiliser leurs données, ainsi que la
technologie et la numérisation en général, pour favoriser
l'industrialisation, les emplois décents et l'intérêt public
général, comme l'amélioration de l'accès à des services publics
de qualité. En outre, les entreprises cherchent à utiliser les
négociations commerciales numériques pour consolider leur modèle
commercial néfaste qui consiste à éviter la réglementation, à
exploiter la main-d'œuvre, à éluder le paiement des impôts (tant
l'impôt sur les sociétés que les taxes ou les droits de douane sur
les échanges) et à faire preuve d'un manque total de responsabilité
envers les communautés dont elles tirent profit. Une gouvernance
d’internet appropriée, démocratique et participative, ainsi que
les droits à la vie privée, les droits économiques des personnes à
leurs données et la protection des données ne doivent pas être
subsumés aux intérêts commerciaux privés. Les négociations
actuelles en vue d'un accord sur le commerce numérique vont bien
au-delà du commerce électronique et mettraient en péril notre vie
privée et les droits économiques des personnes à leurs données ;
exacerberaient les inégalités, notamment par la fracture numérique
; favoriseraient l'évasion fiscale des entreprises numériques ;
décimeraient les micro, petites et moyennes entreprises (MPME) ;
cimenteraient le contrôle monopolistique des premiers arrivants ;
nous rendraient moins sûrs en interdisant une surveillance
appropriée des codes sources et des algorithmes ; et abrogeraient à
jamais l'espace politique de l'industrialisation numérique. Les
négociateurs revendiquent de prétendus avantages pour les femmes
entrepreneurs et les MPME, alors que les négociations actuelles
comprennent des dispositions qui seraient extrêmement préjudiciables
à la grande majorité des femmes et des petites entreprises. Nos
exigences :
<p>


<ul>
	<li>
	Les
	négociations plurilatérales et multilatérales (ainsi que
	bilatérales) en vue d'un accord contraignant sur le commerce
	numérique (&quot;e-commerce&quot;) doivent être interrompues.
	<li>
	Il
	faut abandonner l'appropriation de «&nbsp;l'émancipation des
	femmes&nbsp;» et des MPME au nom de la promotion des règles du
	commerce numérique.
	<li>
	Les
	gouvernements devraient convenir, en dehors de l'OMC, d'une norme
	mondiale de haute qualité pour la protection des données
	personnelles, la propriété nationale et communautaire de leurs
	données, ainsi que de principes exécutoires pour l'utilisation des
	données, de l'intelligence artificielle et de l'apprentissage
	machine de manière éthique et favorisant l'équité et la justice
	sociale. 
	
	<li>
	Il
	faut stopper les efforts visant à imposer de nouvelles obligations
	aux membres en acceptant l'équivalence de &quot;neutralité
	technologique&quot; des engagements en matière de services par le
	biais des nouvelles technologies.
	<li>
	De
	nouvelles politiques favorables à la concurrence doivent être
	adoptées et appliquées dans un cadre extérieur à l'OMC.
	<li>
	Les
	règles actuelles, notamment au sein de l'OMC, qui facilitent
	l'évasion fiscale, la fraude fiscale et d'autres flux financiers
	illicites, en particulier pour les sociétés numériques, doivent
	être modifiées pour que les pays disposent d'une base fiscale
	appropriée pour financer la réalisation des ODD.
	<li>
	En
	outre, il faudrait supprimer toute contrainte actuelle de l'OMC sur
	l'espace politique de l'industrialisation numérique, l'utilisation
	des données pour le bien public et la réalisation des ODD sur
	l'accès aux technologies de l'information et des communications, y
	compris la diversification industrielle et la valeur ajoutée -
	comme les limites des exigences de performance telles que le
	transfert de technologie, l'embauche de travailleurs et de cadres
	locaux, l'utilisation d'intrants locaux.
</ul>
<p><b>Nous
exigeons le droit de protéger l'espace politique pour le
développement </b>

<p>Le
commerce peut contribuer à une prospérité partagée, dans le cadre
d'un ensemble de règles appropriées, qui diffèrent selon le niveau
de développement d'un pays. Mais le commerce n'est pas le but en
soi. Si le résultat réel du commerce dans le cadre des règles
actuelles ou proposées entrave la capacité des pays à se
développer et des travailleurs à prospérer grâce à leur travail,
alors de nouvelles règles sont nécessaires. Les pays en
développement ont subi les effets néfastes des règles de l'OMC
depuis qu'ils ont commencé à les appliquer et ils ont avancé une
myriade de propositions pour leur permettre d'accéder aux avantages
du commerce, tout en conservant une marge de manœuvre politique pour
le développement. Ces &quot;questions de mise en œuvre&quot;, qui
figurent désormais dans les propositions du G90 sur le Traitement
spécial et différencié (TSD), ont été conçues pour remédier à
certaines asymétries et préjugés de l'Uruguay Round. Au lieu de
cela, certains pays développés s'attaquent aux quelques
flexibilités qui nuisent aux règles existantes de l'OMC et qui sont
autorisées pour les pays en développement, en appelant à la
&quot;différenciation&quot; des pays à forte population qui les
obligeraient à adopter des disciplines encore plus onéreuses les
empêchant de développer leurs économies. Selon les dernières
recherches de la CNUCED, cette &quot;différenciation&quot; est tout
à fait inappropriée et n'a aucun fondement dans la réalité
économique. En outre, certains pays proposent d'imposer des charges
encore plus lourdes aux pays en développement membres sous la
bannière de la &quot;transparence et de la notification&quot;. En
même temps, lors des négociations visant à discipliner les
subventions dans le secteur de la pêche, certains pays font pression
pour limiter les dispositions de TSD (qui sont mandatées par l'ODD
14.6) qui empêcheraient les pays en développement de bénéficier
de leurs ressources halieutiques et d'assurer la sécurité des
moyens de subsistance des petits pêcheurs, tout en accordant des
avantages de premier arrivé aux grandes flottes de pêche
commerciale qui sont à l'origine de la grande majorité des cas de
surpêche. En outre, les préoccupations commerciales des pays les
moins avancés (PMA) ne devraient pas être victimes des demandes
extrêmes des STN pour un meilleur accès aux marchés des pays en
développement à forte population. Ainsi :
<ul>
	<li>Un
	ensemble solide de propositions pour une marge de manœuvre
	politique en faveur du développement, connu sous le nom de
	Traitement spécial et différencié, doit être recentré en tant
	qu'axe principal de l'OMC et faire l'objet d'un accord immédiat. 
	
	<li>Les
	tentatives visant à empêcher les pays en développement d'accéder
	aux droits et aux flexibilités de l'OMC par le biais de la
	&quot;différenciation&quot; doivent être abandonnées.
	<li> Il
	faut abandonner l'élargissement des engagements contraignants
	visant à réduire les droits de douane dans le cadre des
	négociations sur l'accès aux marchés pour les produits non
	agricoles (NAMA), de l'accord sur les biens environnementaux (EGA)
	ou des pressions exercées sur les pays pour qu'ils adhèrent à
	l'accord élargi sur les technologies de l'information (ITA-II).
	<li>Tout
	accord visant à limiter les subventions dans le secteur de la pêche
	ne doit pas porter atteinte aux droits souverains déjà énoncés
	dans la Convention des Nations unies sur le droit de la mer. Tout
	résultat doit comporter une forte composante de dispositions en
	matière de TSD afin de garantir que les pays en développement
	puissent poursuivre le développement durable de leur secteur de la
	pêche tout en disciplinant les subventions néfastes aux grandes
	flottes industrielles.
	<li>Le
	paquet PMA, qui comprend un accès en franchise de droits et de
	contingents pour les PMA, une résolution équitable et immédiate
	des demandes des pays producteurs de coton et une exemption des
	services pour les PMA, devrait être mis en œuvre immédiatement.
	<li>Les
	propositions visant à accroître les charges administratives des
	pays en développement sous la rubrique &quot;transparence et
	notification&quot; sont tout à fait inappropriées et devraient
	être remplacées par des changements visant à faire fonctionner
	l'institution d'une manière plus transparente et démocratique qui
	respecte les contraintes de capacité des pays membres. Pour cela il
	faudrait notamment ne pas tenir de réunions simultanées, veiller à
	ce que les réunions soient correctement documentées et à ce que
	l'interprétation soit universellement assurée.  
	
	<li>Si
	les PMA cherchent à adhérer à l'OMC, leur entrée ne doit pas
	être conditionnée par des demandes d'accès au marché, mais ils
	devraient pouvoir adhérer à des conditions qui leur permettent
	d'utiliser le commerce en fonction de leurs besoins de
	développement. Ces conditions ne devraient pas aller au-delà de
	celles appliquées aux PMA membres de l'OMC à l'origine. Les pays
	qui &quot;sortent&quot; du statut de PMA devraient bénéficier
	d'une certaine flexibilité pour adopter de nouvelles règles de
	manière compatible avec leur trajectoire de développement.
	<li>Il
	faut procéder à une évaluation approfondie des contraintes qui
	pèsent actuellement sur la capacité des pays à mener des
	politiques intérieures et à utiliser le commerce pour le
	développement, afin que les règles de l'OMC relatives à la
	propriété intellectuelle, à l'agriculture, aux investissements,
	aux services et aux biens puissent ensuite être transformées
	conformément aux politiques de développement de la transformation
	structurelle.
</ul>
<p><b>4.
Nous exigeons l'accès à la santé et à des médicaments
abordables, et non des monopoles de brevets</b>
<p>Les
monopoles de brevets sont la classe la plus protégée par les règles
de l'OMC dans le cadre de l'accord sur les aspects des droits de
propriété intellectuelle qui touchent au commerce (ADPIC). L'accord
ADPIC a entraîné un transfert massif de richesses des patients et
des budgets de santé des États vers les entreprises, et du Sud vers
les sociétés pharmaceutiques, presque exclusivement basées au
Nord. Comme les entreprises cherchent à prolonger les brevets plutôt
qu'à innover dans les médicaments nécessaires, il en est résulté
une crise de l'innovation. L'abolition de l'accord sur les ADPIC
sauverait des millions de vies dans le monde en facilitant l'accès
aux médicaments essentiels pour les populations de tous les pays.
Les défenseurs de l'accès aux soins de santé et à des médicaments
abordables ont remporté une victoire importante en 2001 grâce à
l'adoption de la Déclaration de Doha sur la santé publique et les
ADPIC. Cette déclaration a réaffirmé les flexibilités contenues
dans l'accord sur les ADPIC et son utilisation pour répondre aux
besoins de santé publique des États membres de l'OMC. Cependant,
les négociations qui ont suivi la déclaration et qui devaient
faciliter l'exportation de médicaments sous licence obligatoire se
sont conclues sans établir un mécanisme viable pour traduire les
droits convenus en un accès réel. La santé publique a souffert en
raison des monopoles de brevets complexes et stricts dont la priorité
est de garantir l'accès aux médicaments. Pendant la pandémie de
Covid-19, l'absence d'accord immédiat sur la dérogation sur les
ADPIC telle que proposée en octobre 2020 a entraîné des millions
de décès et de maladies inutiles. Ainsi, dans l'immédiat :
<p>


<ul>
	<li>
	Les
	Membres doivent accepter immédiatement la dérogation sur les
	ADPIC, telle qu'elle a été proposée par plus de 60 pays, afin de
	sauver des vies pendant la pandémie mondiale de Covid-19.
</ul>
<ul>
	<li>
	Les
	États membres doivent revoir la décision du 30 août 2003 sur les
	ADPIC et la décision ultérieure de modifier les ADPIC en 2005 et
	améliorer ces décisions pour les rendre applicables.
</ul>
<ul>
	<li>
	Les
	membres doivent convenir d'un moratoire permanent sur les plaintes
	de non-violation des ADPIC.
	<li>
	Les
	membres doivent rendre permanente la dérogation aux obligations des
	ADPIC pour les PMA, tant qu'ils sont PMA, sans aucune condition, y
	compris en permettant aux membres PMA de revenir sur la mise en
	œuvre actuelle des ADPIC.
	<li>
	En
	outre, les membres doivent procéder à une évaluation de l'impact
	de l'accord sur les ADPIC sur l'innovation et l'accès aux
	médicaments dans le monde entier, et doivent ensuite transformer
	fondamentalement la réglementation des médicaments sur la base du
	droit à la santé et de la promotion de la recherche et du
	développement des vaccins et des médicaments et de l'accès à
	ceux-ci.
</ul>
<p><b>5.
Nous exigeons des règles commerciales qui soutiennent la sécurité
et la souveraineté alimentaires</b>
<p>La
persistance de près d'un milliard de personnes souffrant de la faim
dans le monde, la consolidation croissante des entreprises dans les
industries agricoles et les multiples crises alimentaires mondiales
ont mis en évidence la rupture de notre système de commerce des
denrées alimentaires. Les agriculteurs pauvres sont exposés à des
inondations d'importations lorsque les prix sont trop bas (souvent en
raison d'exportations faisant l'objet d'un dumping injuste de la part
des pays riches) alors qu'en même temps, les rangs des humains
souffrant de la faim s'élargissent lorsque les prix des denrées
alimentaires montent en flèche. Cette volatilité est le résultat
d'une spéculation excessive sur les marchés des matières premières
et des règles alimentaires mondiales de l'Accord sur l'agriculture
(AsA) - rédigé en grande partie pour satisfaire les entreprises de
l'agrobusiness - qui traitent la nourriture comme un produit destiné
au profit des entreprises plutôt que comme un droit humain. En
outre, les règles actuelles contraignent les pays en développement
à se cantonner aux échelons inférieurs des chaînes de valeur
mondiales dans le domaine de l'agriculture. L'ODD 2 - &quot;Éliminer
la faim, assurer la sécurité alimentaire et une meilleure nutrition
et promouvoir une agriculture durable&quot; - est un élément clé
pour parvenir à un développement durable dans tous les pays, en
particulier dans les pays en développement et les Pays les moins
avancés. Les cibles de l'objectif 2 concernent, entre autres, la
garantie de l'accès à la nourriture pour tous les groupes, en
particulier les pauvres et les marginalisés, l'amélioration de la
nutrition, le doublement de la productivité des petits agriculteurs,
le maintien de la biodiversité et l'utilisation des ressources
génétiques. Il est temps de procéder à une transformation
fondamentale du système alimentaire. Les modifications suivantes des
règles du commerce mondial sont essentielles pour permettre aux pays
d'assurer la souveraineté et la sécurité alimentaires et de
promouvoir les moyens de subsistance en milieu rural :
<ul>
	<li>
	Tous
	les pays devraient avoir le droit de subventionner l'alimentation,
	de protéger les moyens de subsistance des agriculteurs et de
	promouvoir le développement rural, tant que leurs subventions et
	autres politiques ne portent pas préjudice aux agriculteurs
	d'autres pays.
</ul>
<ul>
	<li>
	Le
	droit de subventionner l'alimentation des pauvres, y compris par le
	biais de paiements directs aux agriculteurs pauvres, tel que proposé
	dans les négociations actuelles (par le groupe des 47 pays du G33),
	devrait être immédiatement reconnu comme essentiel pour promouvoir
	la souveraineté alimentaire afin d'assurer la sécurité
	alimentaire.
	<li>
	Les
	pays en développement devraient avoir le droit d'utiliser des
	instruments commerciaux tels que les droits de douane et d'autres
	mesures pour protéger les moyens de subsistance des agriculteurs,
	le développement rural et la sécurité alimentaire, en particulier
	lorsqu'ils sont confrontés à des poussées d'importations
	provenant de la production subventionnée. Cette possibilité,
	appelée Mécanisme spécial de sauvegarde (MSS) à l'OMC, doit être
	acceptée, mais d'une manière beaucoup plus souple que ne le
	reflètent les propositions actuelles de l'OMC pour qu'elle soit
	utile à la réalisation de la sécurité alimentaire.
	<li>
	Aucun
	engagement supplémentaire sur l'agriculture ne doit être demandé
	aux PMA.
	<li>
	Les
	pays en développement devraient pouvoir exempter des produits de
	toute réduction tarifaire future éventuelle s'ils le jugent
	nécessaire pour protéger les revenus des agriculteurs, la sécurité
	alimentaire et le développement rural ; la désignation des
	produits spéciaux à l'OMC doit être élargie.
	<li>
	Les
	subventions pour les produits exportés devraient être
	disciplinées, y compris les subventions dites de la &quot;boîte
	verte&quot; et les droits à la Mesure globale du soutien (MGS).
	<li>
	Les
	règles commerciales doivent être modifiées pour faciliter une
	réglementation adéquate des produits de base afin d'éviter une
	spéculation et une volatilité excessives sur les marchés
	mondiaux.
	<li>
	En
	outre, les membres devraient procéder à une évaluation de
	l'impact de l'AsA sur la sécurité alimentaire, les moyens de
	subsistance des agriculteurs, le détournement des terres pour les
	agro carburants et le développement rural, puis repenser
	complètement les règles du commerce agricole mondial afin de
	permettre aux pays d'exercer leur souveraineté alimentaire.
</ul>
<p><b>6.
Nous exigeons la protection de la biodiversité et l'interdiction du
brevetage du vivant</b>
<p>L'un
des résultats importants de la déclaration de Doha a été
d'examiner la relation de l'accord sur les ADPIC avec la Convention
des Nations unies sur la diversité biologique (CDB). Au cours de dix
ans de délibérations, certains membres ont fait des propositions
concrètes pour modifier l'accord sur les ADPIC afin de prévenir le
bio piratage, mais d'autres ont empêché leur adoption. Nos
exigences :
<ul>
	<li>Les
	membres doivent modifier l'accord sur les ADPIC afin de garantir la
	cohérence avec les ODD 2.5, 2. A, 15.6, 15.9, 15.a pour exiger la
	divulgation du pays et la preuve du consentement préalable en
	connaissance de cause et le partage juste et équitable des
	avantages, au moins aussi fort que celui convenu dans la CDB /
	Protocole de Nagoya et le Traité international sur les ressources
	phytogénétiques pour l'alimentation et l'agriculture. Cet
	amendement doit également couvrir les informations numériques sur
	les séquences (INS), comme l'exigent de nombreux pays en
	développement riches en biodiversité.
	<li>Le
	réexamen obligatoire de l'obligation de breveter le vivant en vertu
	de l'article 27.3 (b) de l'accord sur les ADPIC doit être achevé.
	Nous réitérons l'appel à une interdiction du brevetage des formes
	de vie, comme l'ont proposé de nombreux pays en développement.
</ul>
<p><b>7.
Nous exigeons la stabilité financière au lieu de la
déréglementation financière</b>
<p>Les
règles de déréglementation et de libéralisation de l'OMC dans le
secteur des services financiers ont contribué à préparer le
terrain pour la débâcle de la crise financière mondiale. Pourtant,
alors que le G20, la plupart des gouvernements et même le Fonds
monétaire international (FMI) ont reconnu la nécessité d'une
re-réglementation financière dans le sillage de la crise
financière, les règles de l'Accord général sur le commerce des
services (AGCS) de l'OMC peuvent en fait entraver les efforts de
régulation financière.  Et les propositions actuelles dans le cadre
des négociations de l'AGCS étendraient de fait les règles d'accès
au marché qui établissent des limites encore plus importantes à la
réglementation intérieure des services financiers. La numérisation
de la finance et les monnaies numériques posent quant à elles
d'autres risques qui ne sont pas bien compris et pour lesquels les
règles commerciales - y compris dans le cadre des nouvelles
propositions sur le commerce numérique - limiteront la surveillance
réglementaire appropriée. Des changements immédiats sont
nécessaires aux règles actuelles de l'AGCS concernant les services
financiers, et aucune déréglementation ou libéralisation
supplémentaire des services financiers ne devrait être entreprise
en dehors ou au sein de l'OMC. Plus précisément:
<ul>
	<li>
	Les
	pays ne doivent prendre aucun engagement supplémentaire pour la
	libéralisation du commerce des services financiers dans le cadre
	des négociations de l'AGCS ou de tout autre accord commercial.
	<li>
	Les
	pays doivent conserver le pouvoir, dans toute disposition ou accord
	commercial, d'exiger le stockage des données financières au niveau
	local aux fins d'une réglementation financière efficace, de
	l'examen des actifs à des fins fiscales et de la prévention des
	pratiques frauduleuses ou prédatrices.
	<li>
	En
	outre, les règles existantes de l'AGCS doivent être revues et
	évaluées à la lumière de la crise financière, puis modifiées
	afin de garantir une marge de manœuvre politique à tous les pays
	pour utiliser des mesures macroprudentielles, telles que les
	contrôles des capitaux et les taxes sur les transactions
	financières, ainsi que pour mettre en œuvre d'autres mesures de
	régulation financière.
</ul>
<p><b>8.
Nous exigeons que les règles commerciales ne limitent pas le rôle
de l'État démocratique dans la fourniture de services publics
universels de qualité ni ne limitent la réglementation de
l'économie dans l'intérêt public.</b>
<p>Les
règles actuelles de l'AGCS donnent aux entreprises le droit d'opérer
sur les marchés dans des conditions qui leur sont favorables, tout
en limitant la surveillance réglementaire publique, et elles ne
devraient pas être étendues. Les entreprises ont un contrôle trop
important sur l'élaboration des politiques, y compris dans les pays
étrangers. Malgré la nécessité de réduire l'influence des
entreprises sur l'élaboration des politiques d'intérêt public,
certains pays proposent de nouvelles disciplines dans le cadre des
négociations sur la &quot;réglementation intérieure&quot;. Nos
exigences :
<ul>
	<li>Les
	efforts visant à obtenir des disciplines supplémentaires sur la
	réglementation intérieure doivent être abandonnés.
	<li>Les
	services publics actuels et futurs doivent être exclus efficacement
	de tout accord existant ou futur sur la libération des services.
	Cela doit inclure les services qui pourraient devoir être
	re-municipalisés à l'avenir.
	<li>En
	outre, les États membres et l'OMC devraient procéder à une
	évaluation fondamentale des implications de l'AGCS sur la garantie
	d'une surveillance réglementaire adéquate des services et l'accès
	à des services publics de qualité. Toute contrainte à la
	réalisation des ODD en matière de santé et de bien-être,
	d'éducation de qualité inclusive et équitable, d'eau propre et
	d'assainissement, d'énergie propre et abordable et d'autres
	services devrait être supprimée sans contestation du règlement de
	l'OMC.
</ul>
<p><b>9.
L'OMC n'est pas le lieu pour établir une politique en matière de
changement climatique</b>
<p>L'expansion
du commerce est une cause majeure du changement climatique, qui
entraîne des crises environnementales, économiques et sociales dans
le monde entier. Toutefois, inscrire le changement climatique à
l'ordre du jour de l'OMC représenterait un grave danger pour la
stabilisation du climat, car les règles de l'OMC favorisent
structurellement l'augmentation des flux commerciaux - qui
contribuent largement aux gaz à effet de serre à l'origine du
changement climatique - au détriment de la durabilité
environnementale. Idéalement, l'accord de Paris devrait fixer un
plancher pour le respect des normes environnementales et des
objectifs climatiques dans la production et le commerce. Au lieu de
cela, les règles de l'OMC contraignent actuellement les pays à
écologiser leurs industries et, par conséquent, à promouvoir une
&quot;transition juste&quot;, une approche du travail décent centrée
sur la transition vers des technologies respectueuses du climat.
Plusieurs aspects des règles actuelles de l'OMC devraient être
modifiés afin de donner aux pays une plus grande marge de manœuvre
pour protéger l'environnement et mener des politiques de
développement durable. D'autres agences multilatérales sont des
lieux bien plus appropriés que l'OMC pour discuter et mettre en
œuvre les règles relatives au climat mondial.
<ul>
	<li>Les
	règles ADPIC devraient être modifiées afin d'accorder aux pays
	des flexibilités supplémentaires par rapport au régime de
	propriété intellectuelle en ce qui concerne le transfert de
	technologie, y compris dans d'autres enceintes multilatérales.
	<li>Les
	politiques relatives au changement climatique, y compris celles qui
	mettent l'accent sur la création d'emplois dans le domaine des
	technologies énergétiques propres, ne devraient pas être
	contestées par le biais du mécanisme de règlement des différends
	de l'OMC.
</ul>
<p><b>L'OMC,
y compris le Secrétariat, doit mettre fin à son fonctionnement
secret et antidémocratique</b>
<p>L'OMC
n'est pas en mesure de régir le commerce mondial. En attendant la
création d’un nouveau système, des changements immédiats dans la
manière dont l'OMC fonctionne sont également nécessaires,
notamment 

<ul>
	<li>L'OMC,
	y compris les négociations et autres réunions, doit être ouverte
	à une participation adéquate de la société civile et la
	faciliter.
	<li>Les
	documents et les textes de l'OMC doivent être accessibles au
	public. 
	
	<li>Les
	&quot;collaborations&quot; actuelles entre le secteur des affaires
	et l'OMC doivent être arrêtées. 
	
	<li>Le
	rôle du Secrétariat ne doit pas être élargi au-delà du mandat
	actuel. 
	
	<li>Le
	Secrétariat ne doit pas agir en faveur des demandes de certains
	membres, à l'exclusion des demandes d'autres membres dans les
	négociations. 
	
	<li>Un
	examen des décisions du MRD doit être effectué en vue d'atténuer
	celles qui font passer les préoccupations commerciales avant
	l'intérêt public.
</ul>
<p>De
nombreux autres changements doivent être apportés au système
commercial mondial, notamment un examen fondamental, l'abrogation ou
la transformation des accords bilatéraux et régionaux en matière
de commerce et d'investissement. Ce qui précède représente les
changements immédiats minimums qui doivent être apportés au
système commercial multilatéral afin de donner à tous les pays une
plus grande marge de manœuvre politique pour mettre en œuvre des
solutions visant à promouvoir une prospérité partagée au profit
de tous.
