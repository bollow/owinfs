Isabelle Durant, Deputy Secretary General acting in the capacity as Officer in Charge<br>
UN Conference on Trade and Development (UNCTAD)
<p>
April 6, 2021
<p>
Dear Deputy SG Durant,
<p>
We write to you as networks and mechanisms engaging the majority of civil society organizations
(CSOs) working on many of the same issues of UNCTAD, including trade, development, technology,
finance, debt, tax and others. All of our platforms have enjoyed a long history of engagement with
UNCTAD in the past, as well as with other UN agencies, and non-UN multilateral agencies as well.
Together, we are now facilitating a diverse array of CSOs in advocacy and engagement in the lead up
to UNCTAD XV. Based on our understanding that you have a strong commitment to engagement
with civil society, we are writing to ensure that the historical mechanisms by which UNCTAD
welcomed civil society input in the past, can be upheld even in the absence of an official CSO
engagement unit.
<p>
We would thus request UNCTAD-CSO engagement prior to UNCTAD XV through the following:
<ul>
<li> A series of regular official briefings to be held by the UNCTAD Secretariat for CSOs online on
the progress of the negotiations for the outcome document, highlighting the areas of
differences and convergence among member States.
<li> An online portal in the UNCTAD website through which CSOs can make submissions and
written inputs officially on the process, text, and outcome of UNCTAD, with such
submissions and inputs to be then circulated and made available by the UNCTAD Secretariat
virtually to Member States for their consideration during the negotiations.
<li> Requesting the coordinators of the various negotiating groups for UNCTAD XV – e.g. G77,
Group B (or separately EU and JUSCANZ), Group D – to hold regular official briefings online
for CSOs on their group’s respective positions and views in the course of the negotiations;
<li> Allowing accredited CSOs to be observers during the virtual Member State-driven
negotiations on the UNCTAD 15 outcome document that will be taking place during the
UNCTAD XV Preparatory Committee, as is normally the case for in-person negotiations;
<li> Having a specific venue at UNCTAD XV for CSOs to have stalls for exhibitions of their
publications and work, as well as provided dedicated rooms for CSOs to use during UNCTAD
XV to provide briefings to delegates during the conference;
<li> Ensuring that speaking placements on the official panels during the UNCTAD-convened
events during the UNCTAD XV are reserved for CSO delegates and can be self-determined by
civil society through its coordination mechanisms;
<li> Ensuring that the venue for CSO side events during UNCTAD XV is in close proximity to the
venue of the official negotiations so as to enable effective interaction between delegates
and CSOs during UNCTAD XV.
</ul>
We appreciate UNCTAD’s history of engagement with civil society, as well as your own history and
commitment. Thus we truly hope that the above will be received as uncontroversial requests, so that
we can work together to ensure the success of the incredibly important meeting of UNCTAD XV.
<p>
Sincerely,
<p>
