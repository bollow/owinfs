<b>Support for the request by Least Developed Countries (LDCs) to the WTO TRIPS
Council on the LDC transition period</b>
<p>
Dear President, dear Commissioners,
<p>
The European Union has repeatedly expressed its commitment to sustainable
development and fair globalization, and this should also be advanced at the World
Trade Organisation.
<p>
We are therefore writing to urge you to favourably consider the request by Least
Developed Countries (LDCs) to the TRIPS Council of the World Trade Organization
(IP/C/W/668). We urge you to support the LDC transition period for as long as a
country remains an LDC and show flexibility in extending the transition period post
graduation to ensure smooth transition.
<p>
Article 66.1 of the WTO-TRIPS Agreement has granted LDC Members an automatic
right of further extensions of their transition period in view of their special needs and
requirements, including their economic, financial and administrative constraints as
well as their need for flexibility to create a viable technological base. Accordingly, the
Preamble of the Agreement explicitly recognizes the need for LDCs to enjoy
“maximum flexibility” in the domestic implementation of laws and regulations.
<p>
LDCs represent the most vulnerable section of the international community. They
account for just 14% of the world’s population, but they comprise more than 50% of
the world’s extremely poor (i.e. those living on less than $1.90 a day). Their
economies also account for just 1.3% of the world total. They also lack the basicprerequisites to benefit from stringent intellectual property standards of the TRIPS
Agreement such as skilled labour, productive capacities, access to secondary
education, electricity, internet access etc. With the outbreak of COVID-19, their
constraints and development challenges have magnified manifold.
<p>
According to the UNCTAD LDC Report 2020, LDCs are experiencing the worst
economic performance in 30 years, with extreme poverty in LDCs expected to rise to
35%. The crisis may potentially drive an additional 32 million people into extreme
poverty, reaching a high of 377 million people. The LDC current account deficit is
expected to deepen from 3.8% of GDP in 2019 to 5.6% of GDP in 2020, the highest
ever.
<p>
The short transition periods granted in the past have proven to be impractical and
insufficient, as well as failed to recognize that LDCs continue to face the obstacles
and constraints after graduation. The present crisis dramatically highlights the
vulnerability of LDCs to exogenous shocks beyond their control. Ensuring LDCs are
on a sustainable development pathway that includes development of a viable
technology base, requires the EU to grant LDCs some flexibility even post-
graduation. The international community through UN resolutions 59/209 of 20
December 2004 and 67/221 of 21 December 2012, have also called for the
continuation of existing special and differential measures post-graduation, to ensure
a smooth transition for LDCs.
<p>
We are of the view that the LDCs’ request in IP/C/W/668 should be considered
favorably especially given that COVID-19 is expected to reverse decades of social
and economic progress in those countries, including progress on poverty, healthcare
and education.
<p>
