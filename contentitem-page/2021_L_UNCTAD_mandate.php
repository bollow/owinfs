<p>

<p>September 23,
2021
<p>Dear Member
States, 

<p>The fifteenth
conference of the UN Conference on Trade and Development (UNCTAD 15)
will occur October 3-7 of 2021 and will set the mandate of the
institution for the next quadrennial. While much of the text has
already been concluded, significant disparities remain. As civil
society organizations (CSOs) from both developed and developing
countries, we believe that important gaps remain that must be
remedied for the text to ensure that UNCTAD can best serve developing
countries as the focal point for trade and development and the
interrelated issues of finance, technology, investment, and
sustainable development. 

<p>First, the
consensus-building and technical cooperation pillars of UNCTAD must
be based on the outcomes and evidence identified by the <b>analysis
and research pillar</b>. Paragraph 96.bis.primus is a naked attempt
to censor, <i>a priori</i>, the independence of the analytical
pillar. We criticize the certain members of B who have, in recent
months, engaged in censoring research publications of UNCTAD that
were well within its mandate. In no other UN institution is its
research subject to a “biennial review,” in which topics for
research that are well within the mandate must be re-litigated before
the board. This paragraph must be removed for the institution to
function according to its mandate. Rather, it is the policy
recommendations of the other pillars which should be subject to
review as to whether they are coherent with the outcomes of research
and analysis, to ensure they are evidence-based. We call on all
governments to go back to the negotiating table to remove the
provisions of 86.bis.primus which endorse censorship. 

<p>The texts
fails to acknowledge the single most important issue facing
developing countries in the current crisis: that of <b>vaccine
apartheid</b>. Intellectual property barriers on vaccines, as well as
treatments and diagnostics, enforced by the WTO are the single
biggest cause of harm to developing countries, in addition to
severely constraining their ability to use trade for development.
Thus, UNCTAD should call for an immediate agreement to the TRIPS
waiver proposal so that barriers to trade in under the rubric of
intellectual property can be removed to end the pandemic for all. The
acknowledgement that it “is important to ensure timely, global and
equitable access to safe, effective and affordable, Covid-19 tools
(vaccines, therapeutics, diagnostics, and personal protective
equipment), recognizing extensive COVID-19 immunization as a global
public good to help overcome the Covid-19 pandemic worldwide and
recover the momentum for sustainable development. Sharing of
information and technology for the detection, prevention, treatment
and control of the pandemic is necessary..” in Para 7 is helpful in
this regard. But it is insufficient. The text should include a
recognition of the barriers that trade rules in the form of
intellectual property have played in preventing billions of people to
access to vaccines, therapeutics, diagnostics, and PPE, and the need
for UNCTAD to work across the three pillars to remove these barriers
to support recovery and development. 

<p>On <b>trade</b>,
text fails to acknowledge the ways in which the current system of
trade rules constrains, rather than facilitates, countries’ ability
to create jobs, ensure food security and financial stability, and
protect environmental sustainability, and must thus be transformed.
Thus, 

<p>


<ol>
	<li>Rather than lauding the
	WTO (an institution with liberalization, not development, at its
	center) and calling for its expansion, the document should call for
	a multilateral system with sustainable development and shared
	prosperity at its core. (Paras 16.bis and 77.quin)
</ol>
<p>


<ol start="2">
	<li>Likewise, UNCTAD’s
	mandate should never call for the continued liberalization of trade,
	but rather work to make the global trade system better able to allow
	developing countries to use trade for their development (Para
	77.quin);
</ol>
<p>Thus, the
mandate of UNCTAD should work across all three pillars to:
<p>


<ol start="3">
	<li>Assess the asymmetries
	and imbalances in the rules multilateral system which constrain the
	ability of developing countries to use trade for their development,
	and provide technical cooperation and build consensus towards
	ameliorating them; 
	
</ol>
<p>


<ol start="4">
	<li>Work towards concluding
	the development agenda, a key goal of developing countries for two
	decades in the WTO and mandated by SDG 17.10 and which is
	inexplicably absent from the UNCTAD mandate draft text; 
	
</ol>
<p>


<ol start="5">
	<li>Strengthen Special and
	Differential Treatment (SDT) provisions, not only for LDCs (77quin);
</ol>
<p>


<ol start="6">
	<li>Remove barriers to
	achieving food security including through public stockholding
	provisions; 
	
</ol>
<p>


<ol start="7">
	<li>Condemn unilateral
	coercive measures and work for their removal under international
	law. This is especially important with respect to those that impede
	the full achievement of economic and social development,
	particularly in developing countries (Para 112.V). It is not
	particularly relevant if coercive measures that are illegal under
	international law affect commercial interests and that reference
	should be deleted (Para 82); 
	
</ol>
<p>


<ol start="8">
	<li>Assess critically the
	impacts on trade and development of various bilateral and regional
	agreements, such as the African Continental Free Trade Area, the
	Pacific Agreement on Closer Economic Relations Plus and the Regional
	Comprehensive Economic Partnership Agreement. Rather than
	inaccurately praising them as “noteworthy achievements”; (Para
	16.bis)
</ol>
<p>


<ol start="9">
	<li>Work to support
	developing countries in their structural transformation, including
	through the removal of barriers to the implementation of industrial
	policy; 
	
</ol>
<p>


<ol start="10">
	<li>Work to reduce economic
	concentration. Although this is one of the most significant problems
	of the global economy affecting developing countries’ prospects
	for using trade for developing, it is conspicuously absent from the
	text; 
	
</ol>
<p>


<ol start="11">
	<li>Provide technical
	assistance to acceding countries prior to, during and in the
	follow-up to the accession process, as the text mandates. However,
	it should also include a commitment from UNCTAD Members that
	acceding members should not be forced to comply with requirements
	that go beyond those of others in their economic category (ie
	“WTO-plus” provisions) (Para 77quin);
</ol>
<p>


<ol start="12">
	<li>While we are gratified
	at the G77’s success in ensuring the inclusion of language
	“recognizing that appropriate and effective special and
	differential treatment for developing countries and LDCs should be
	an integral part of the negotiation,” in the WTO’s negotiations
	on Fisheries, this simply reaffirms the mandate from the SDGs and
	should never have been opposed (Para 60).
</ol>
<p>We commend
the G77 in succeeding to ensure that the important work of UNCTAD to
support <b>South-South cooperation</b> should be strengthened
(64.bis.primus).
<p>On
<b>investment</b>, the negative impacts of investment policies,
including those promulgated by UNCTAD in the past, such as those with
investor to state dispute settlement (ISDS) mechanisms, must be
acknowledged. It is not only that the expansion of investment has not
benefitted all, but that developing countries have been actively
harmed, including according to UNCTAD’s research. Investment rules
must not only be “clear” and “predictable” but also
facilitate developing countries’ development priorities and the
right to regulate while ensuring that states maintain the ability to
hold corporations accountable. UNCTAD’s role should focus on
increasing foreign direct investment - not in volume terms but in
accordance with national development priorities - and in advocating
for frameworks that put development at the center of policies and
practices governing investment flows (Para 112.VIII);
<p>On <b>technology
and the digital divide</b>, it is not only increasing access to
technology but the digital economic divide, as demonstrated by
UNCTAD’s research, that must be addressed. Efforts to utilize
UNCTAD in the service of an agenda of digital behemoths based in
developed countries is deplorable and must be halted. All governments
need the policy space to pursue digital industrialization to create
jobs and spur innovation in the digital age, free of the pernicious
impacts of economic monopoly and concentration. They also need policy
space to manage the governance of data in the public interest, such
as in the strengthening of quality public services which are
essential for development and the achievement of the Sustainable
Development Goals (SDGs). Thus:
<p>


<ol>
	<li>References to “data
	flow with trust” must be excised (Para 43); 
	
</ol>
<p>


<ol start="2">
	<li>‘Multistakeholder
	approach’ is a code word for corporate driven policy making and
	this term should be removed; this reference opens the door to
	UNCTAD’s facilitation of Big Tech corporate lobbying of developing
	countries to open markets and create pro-corporate regulations under
	the guise of giving “advice” to developing countries (Para 42);
</ol>
<p>


<ol start="3">
	<li>UNCTAD’s mandate
	should include work across all three pillars on digital
	industrialization, the value of data for developing countries and
	its use and governance in the public interest, and ensuring shared
	prosperity including by reducing economic concentration.  
	
</ol>
<p>On <b>the
climate emergency and environmental crisis</b>, the text fails to
recognize the overwhelming responsibility of developed countries in
causation and falls short in a number of critical areas, including by
attempting to limit the work of UNCTAD to specific groups of
countries when all developing countries are affected. Thus, 

<p>


<ol>
	<li>When international
	instruments are mentioned, those that are most crucial to developing
	countries should be included; 
	
</ol>
<p>


<ol start="2">
	<li>Technology Transfer must
	be emphasized as a solution which UNCTAD’s three pillars should be
	mobilized to accelerate (Para 55), and references to “mutually
	agreed terms” should be excised (Para 112.XXVII); 
	
</ol>
<p>In addition,
several key environment issues that should be part of UNCTAD’s
mandate are not yet mentioned in the text: 

<p>


<ol start="3">
	<li>The efforts to support a
	transition to a just, sustainable and fully inclusive economy, such
	as UNCTAD’s work to promote a global green new deal, must be
	strengthened; 
	
</ol>
<p>


<ol start="4">
	<li>The potential impacts of
	climate-related tariffs on developing countries should be studied. 
	
</ol>
<p>


<ol start="5">
	<li>Language should be
	included to reference learning from UNCTAD’s long experience in
	commodity and supply management and cooperation for keeping global
	warming with the Paris Agreement goal of 1.5 degrees Celsius.
</ol>
<p>UNCTAD 15
will occur in a key moment in the ongoing Covid pandemic and
recovery. Developing countries have suffered extensively, in large
part because the international system’s trade, investment,
technology, and climate rules and practices have failed to provide an
environment conducive to sustainable development. Developing
countries will not be able to “build back better” unless the
global community recognizes the need to transform the current rules.
We look forward to working with you to ensure that these provisions
are included in UNCTAD’s mandate. 

