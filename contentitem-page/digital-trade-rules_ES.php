Las mayores empresas de la historia del mundo – Amazon, Facebook, Google, Apple y Microsoft – están tratando de utilizar las normas „comerciales“ para manipular las reglas de la economía global (digital) con objeto de recoger más datos, ejercer más control sobre nuestras vidas y sobre sus propios/as trabajadores/as, y amasar beneficios cada vez mayores. Más de 80 miembros de la Organización Mundial del Comercio (OMC) están negociando actualmente un nuevo acuerdo sobre el comercio elec- trónico, basado en propuestas encaminadas a lograr esos objetivos. En el presente documento se intenta explicar cómo operan dichas empresas para aumentar sus ganancias; cuáles serían los posibles efectos de las nuevas normas – en los/as trabajadores/as, los/as ciudadanos/as, las comunidades, los países en desarrollo, los servicios públicos, la seguridad y la protección, y la propia democracia; cuáles son las posibles alternativas; y qué podemos hacer para detener esta extensión masiva
