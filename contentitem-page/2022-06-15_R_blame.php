<p>June 15, 2022 

<p>
<p><b>Statement
of Civil Society Supporting Developing Countries Standing Firm for
the Interests of their Peoples, in Last Hours of Negotiations</b>
<p>Geneva - Civil
Society organizations (CSOs) from around the world are supporting
developing country governments to stand firm to resist bullying and
strong-armed tactics from developed countries and the director
general at the 12th Ministerial Conference (MC12) of the World Trade
Organization (WTO) in Geneva this week. Developing countries should
not be pressured into agreeing to binding, enforceable deals that are
against their interests and the interests of their people. 

<p>Certain countries,
including the US, Switzerland, the EU, and the UK have engaged in a
“Blame Game” to try to pin the blame on certain developing
countries, particularly India, for resisting pressure tactics and
standing up for their interests. 

<p>But it is these four
that should be blamed for the current impasse at the WTO. They have
refused to negotiate to end the Covid-19 pandemic through a
reasonable waiver from maximalist intellectual property (IP)
monopolies enforced by the WTO; they have refused to allow reasonable
flexibilities for developing countries on
agriculture and fisheries; and they have demanded a “carte blanche”
to launch new negotiations in the WTO on so-called “WTO reform”
which will instead entrench their power and that of their
corporations, and weaken that of developing countries, by abolishing
multilateralism and consensus as well as the development agenda. 

<p>Civil
society highlighted that the undemocratic, exclusionary “<u><a href="https://ourworldisnotforsale.net/2022-06-14_R_protest">Green
Rooms</a></u>,”
in which most developed countries are invited but from which most
developing countries are excluded, have resulted in proposed texts
for MC12 that are highly biased towards the proposals of rich
countries, providing further evidence that developing countries are
not to blame.
<p>&nbsp;
<p>“We
represent civil society organizations from two dozen countries around
the world, in rich and poor countries alike, and we are united in
supporting developing countries who maintain a strong stand in the
WTO in defense of their workers, farmers, fisherfolks, and citizens.
Instead of blaming developing countries, the responsibility
for the impasse in the WTO today is developed countries – in
particular UK, EU, Switzerland, and the US – who are too eager to
represent the interests of their corporate sector instead of their
peoples. We call for a halt to their demands – and a transformation
of the current trading system towards the <u><a href="https://ourworldisnotforsale.net/2021_WTO-Turnaround">Turnaround
Agenda</a></u> endorsed by over 200 global groups. 

<p>Civil society has
consistently made a series of demands of WTO members in the lead-up
to the 12th Ministerial Conference (MC12) of the World Trade
Organization (WTO). We have made demands that members: 

<ol>
	<li>agree
	immediately to a <u><a href="https://msfaccess.org/open-cso-letter-wto-trade-ministers-do-not-accept-current-draft-demand-real-waiver">full
	waiver of all intellectual property protections related to Covid-19</a></u>,
	so that developing countries can scale up manufacturing of vaccines
	but also treatments and tests, in order to contain the pandemic.
	What is on the table is not a real waiver, will increases
	restrictions on developing countries’ use of IP, and will not
	resolve the pandemic, so CSOs have urged governments to walk away
	from the proposed text. Likewise, the “WTO Response to the
	Pandemic” is a fig leaf to cover over the failure of the WTO to
	resolve this key issue; 
	
	<li>
	agree to <u><a href="https://ourworldisnotforsale.net/agriculture">permanent
	solution for public stockholding</a></u>, which many
	developing countries need in order to increase domestic food
	production for domestic consumption. The false solutions of the
	“Declaration on Food Security” and the potential agreement on
	the World Food Program do not address the fundamental issue of
	increasing production in developing countries; 
	
	<li>an outcome on
	<u><a href="https://ourworldisnotforsale.net/2022/L_Fisheries.pdf">fisheries
	that preserves the policy space for developing countries’
	artisanal fisherfolk</a></u>, some of the poorest and most
	marginalized people in many developing countries, to be supported.
	The current Fisheries Subsidies text preserves the rights of
	developed countries to continue their subsidies, and fails to hold
	those responsible for the collapse in fish stocks to account. 
	
	<li>Reject the
	false “<u><a href="https://www.ineteconomics.org/perspectives/blog/why-whats-going-on-right-now-at-the-wto-matters">WTO
	reform</a></u>” agenda, which would: abolish
	multilateralism and consensus in the WTO, flinging open the door for
	illegal “plurilaterals” among hyper-neoliberal countries;
	abolish forever the commitment to the Development Agenda within the
	WTO, as well as the fundamental WTO architecture of Special and
	Differential Treatment (SDT) for all developing members, without
	which they never would have agreed to allow the WTO to come into
	existence; and open the door for even more corporate power through
	“stakeholders.”  Instead, the real “WTO reform” agenda is
	the Development Agenda of flexibilities from harmful WTO rules which
	constrain developing countries’ policy space.
</ol>
