<p>Over 50 civil
society experts – trade unionists, farmers, development advocates,
and consumer activists – from 24 countries have traveled to Geneva
for the 12th Ministerial meeting of the World Trade Organization
(WTO) as part of the global Our World Is Not For Sale (OWINFS)
network. Civil society delegates are at the WTO from: India,
Indonesia, Pakistan, Philippines, Pacific Islands, Ghana, Liberia,
Malawi, Malaysia, Morocco, South Africa, Uganda, Zimbabwe, Argentina,
Brazil, Uruguay, USA, Belgium, France, Germany, Netherlands, Norway,
Switzerland, and the UK.
<p>Videos of previous
protests, and quotes from civil society, documents of analysis by
CSOs are available here: <u><a href="https://ourworldisnotforsale.net/MC12">https://ourworldisnotforsale.net/MC12</a></u>.

<p>On the TRIPS waiver
on intellectual property rules to save lives during the Covid
pandemic, civil society leaders urged countries to reject the
counter-proposal Director General text with its onerous new TRIPS+
provisions which would actually add new obligations to TRIPS
(Trade-Related Aspects of Intellectual Property Rights) on developing
countries rather than save lives. 

<p>They demanded a
permanent solution to public stockholding, so developing countries
can achieve food security through increased production. They urged
delegates to reject the anti-development fisheries text, and to start
again with a pro-development proposals. And they condemned the Green
Room tactics and false promise of “WTO Reform” which is actually
a developed country agenda to abandon multilateralism, consensus, and
development architecture of the WTO. 

