June 16, 2022 
<p><b>Geneva,
Switzerland--</b>Today, a group of civil society working together
through the global Our World Is Not For Sale (OWINFS) network,
dramatized the correction of the false media narrative of blaming
developing countries for what is happening in the WTO negotiations. 

<p>The vast majority of
members of the WTO, who are developing countries, have made
consistent and repeated contributions to the negotiations, to resolve
the challenges to their development from harmful WTO rules. The EU,
US, UK, and Switzerland have consistently blocked the proposals.
Instead, these countries have demanded expansion of the WTO’s
harmful policies on WTO reform, the false “WTO Response to the
Pandemic;” Fisheries subsidies disciplines that hurt developing
countries, and leaving in place onerous WTO rules on Agriculture.
