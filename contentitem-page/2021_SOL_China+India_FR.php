<p><u><b>Introduction</b></u>
<p>Le
présent document tente de clarifier la question de
l'autodétermination du statut de chaque membre de l'OMC en tant que
pays développé ou pays en développement (PED), qui a été
contestée par les Etats-Unis (EU), suivis par l'UE28, en visant
surtout la Chine et l'Inde, en comparant les données officielles
disponibles de six membres de l'OMC : quatre membres occidentaux –
EU, UE28, Canada, Japon –, la Chine et l'Inde en 2019 et 2020. 

<p>Dans
son discours inaugural du 13 février 2021, la nouvelle directrice
générale de l'OMC, Mme Ngozi Okonjo-Iweala, a déclaré vouloir
prendre au sérieux le préambule de l'OMC : &quot;<i>Le
préambule de l'Accord de Marrakech stipule que les objectifs de
l'OMC sont de relever les niveaux de vie, d'assurer le plein emploi,
d'augmenter les revenus, d'accroître la production et le commerce
des biens et des services, et de rechercher l'utilisation optimale
des ressources mondiales conformément à l'objectif du développement
durable&quot;. Le préambule dit tout ! L'OMC concerne les gens ! Il
s'agit de travail décent ! Mettons son objectif primordial au
premier plan en tant que force motrice de tout ce que nous cherchons
à réaliser pour le système commercial multilatéral... Les travaux
de l'OMC dans des domaines nouveaux ou novateurs ne signifient pas
que les sujets traditionnels tels que l'agriculture sont oubliés.
L'agriculture est particulièrement importante pour de nombreux pays
en développement et pays les moins avancés. L'amélioration de
l'accès aux marchés pour les produits d'exportation présentant un
intérêt pour ces pays est d'une importance capitale, tout comme le
traitement du soutien interne qui fausse les échanges. Les droits
croissants des Membres en matière de soutien interne </i>

<p><i>doivent
être abordés afin d'uniformiser les règles du jeu et d'offrir des
opportunités aux petits agriculteurs</i>&quot;<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym">1</a></sup>.
Et, lors d'une vidéoconférence organisée par la Commission
européenne le 26 avril 2021, &quot;<i>Okonjo-Iweala
a déclaré que l'une de ses trois priorités pour cette année sera
de s'attaquer aux subventions agricoles, qui sont principalement
accordées par les nations développées, notamment les États-Unis
et l'UE... Elle a déclaré que Pékin souhaite voir des progrès sur
les subventions agricoles, qui représentent actuellement environ 1
000 milliards d'euros et pourraient doubler d'ici 2030. &quot;J'aimerais
examiner les subventions dans leur ensemble&quot; et voir comment &quot;à
tous points de vue, nous créons des conditions de concurrence
équitables&quot;, a déclaré le chef de l'OMC</i>&quot;<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym">2</a></sup><i>.</i>
<p>Prenant
au sérieux le Dr Ngozi Okonjo-Iweala, selon laquelle l'OMC concerne
les gens, les comparaisons se feront essentiellement par habitant et
porteront sur cinq points : revenus et salaires, performances
sociales, performances environnementales, performances commerciales
et aides agricoles. Sur toutes ces questions, la Chine et l'Inde sont
fondées à revendiquer leur statut de pays en développement. 

<p><u><b>I
- Indicateurs macro-économiques des États-Unis, de l'UE28, du
Japon, du Canada, de la Chine et de l'Inde </b></u>

<p>Les
indicateurs macroéconomiques de l'UE28 n'étant pas disponibles,
mais uniquement ceux de la zone euro, on utilise les données des
trois principaux pays : Allemagne, France, Royaume-Uni (RU), à coté
de celles des EU, du Japon, du Canada, de la Chine et de l'Inde. Le
tableau 1 montre que le revenu par habitant des États-Unis (EU) en
PPA (parité de pouvoir d'achat) était en 2019 3,6 fois supérieur à
celui de la Chine et 9,3 fois supérieur à celui de l'Inde. En
termes de PIB nominal par habitant, le niveau américain était 5,8
fois plus élevé que celui de la Chine et 31,2 fois plus élevé que
celui de l'Inde. Et le salaire moyen américain en PPA était 3,4
fois plus élevé en 2019 qu'en Chine et 8,1 fois plus qu'en Inde.
<p>L'emploi
dans l'agriculture (sans la sylviculture et la pêche) était 18,1
fois plus élevé en Chine qu'aux EU en 2019 et 40,4 fois plus élevé
en Inde.
