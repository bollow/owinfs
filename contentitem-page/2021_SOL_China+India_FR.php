<p>Même
s'il est théoriquement discutable de considérer que les
importations plus élevées par habitant des EU et de l'UE28 sont un
signe de leur développement et, à l'inverse, que les importations
plus faibles par habitant de la Chine et de l'Inde sont un signe de
leur sous-développement, l'Accord de l'OMC donne la priorité à
l'accès aux marchés pour les Membres en développement :
&quot;<i>Reconnaissant
en outre qu'il est nécessaire de déployer des efforts positifs pour
faire en sorte que les pays en développement, et en particulier les
moins avancés d'entre eux, s'assurent une part de la croissance du
commerce international qui corresponde aux besoins de leur
développement économique</i>&quot;,
et plus encore l'Accord sur l'agriculture : &quot;<i>Les
pays développés Membres tiendraient pleinement compte des besoins
et de la situation particuliers des pays en développement Membres en
prévoyant une amélioration plus marquée des possibilités et
modalités d'accès pour les produits agricoles présentant un
intérêt particulier pour ces Membres</i>&quot;.
D'autre part, il est clair que les exportations nettement supérieures
des EU et de l'UE28 leur ont donné la capacité d'importer
davantage, de sorte que les importations par habitant de la Chine et
de l'Inde, inférieures à celles des EU et de l'UE28, justifient
leur statut de pays en développement à l'OMC.     

