<p><u><b>II
– Les performances sociales </b></u>

<p>Le
tableau 2 repose principalement sur l'indice de développement humain
(IDH) du PNUD (Programme des Nations Unies pour le Développement)
qui est un indice statistique composite de l'espérance de vie, de
l'éducation et du revenu par habitant, utilisé pour classer les
pays en fonction de leur développement humain. L'indice de
développement humain ajusté aux inégalités (IDHI) est le niveau
réel de développement humain tenant compte des inégalités de
revenu. L'IDH moyen non pondéré des 6 pays développés, qui
s'élevait à 0,926 en 2019 (qui était également celui des EU et
des trois pays de l'UE), était supérieur de 21,7 % à celui de la
Chine et de 43,6 % supérieur à celui de l'Inde. Et l'IHDI des EU
était 26,4 % plus élevé que celui de la Chine et 70,1 % plus élevé
que celui de l'Inde. 

<p>En
fait la répartition des revenus aux EU est plus inégale qu'en Chine
et en Inde si l'on compare la part de revenu des 1% les plus riches
et des 40% les plus pauvres : aux EU, les 1% les plus riches ont reçu
en 2019 20,5% du revenu national tandis que les 40% les plus pauvres
en ont reçu 15,4%, soit un écart de 5,1% entre les deux. En Chine,
les 40% les plus pauvres ont reçu 17,2% du revenu national et les 1%
les plus riches 13,9%, soit un écart de 3,3% entre les deux. Et si,
en Inde, les 1% les plus riches ont reçu 21,3% du revenu national
(plus qu'aux EU) contre 18,8% pour les 40% les plus pauvres, l'écart
entre les deux n'est que de 2,5%. En outre, à l'exception des EU,
les deux indicateurs d'inégalité des revenus sont plus faibles dans
les cinq autres pays occidentaux qu'en Chine et en Inde.    

