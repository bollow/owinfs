<p>L'indicateur
le plus significatif de l'OCDE est l'EST (estimation du soutien
total) agricole par unité de travail agricole (UTA), dont on exclut
le SPM (soutien des prix du marché) car il concerne essentiellement
la protection des importations, notamment depuis 2014 où toutes les
subventions explicites à l'exportation ont été supprimées. En
2019, l'EST-SPM des EU était de 92.966 Md$ qui, divisé par 2,363
millions d'UTA, impliquait une subvention moyenne de 39 342 dollars
par UTA. Dans le même temps, l'EST-SPM de l'UE28 était de 97,237
Md$ qui, divisé par 8,954 millions d'UTA, impliquait une subvention
moyenne de 10 860 dollars par UTA. Et l'EST-SPM de la Chine était de
93,386 Md$ qui, divisé par 211,780 millions d'UTA, impliquait une
subvention moyenne de 4 747 dollars par UTA. En d'autres termes, la
subvention par UTA était 8,3 fois supérieure à celle de la Chine
pour les EU et 2,3 fois supérieure pour l'UE28. Et l'EST-SPM par UTA
était 11,1 fois supérieure à celle de l'Inde pour les EU et 3,1
fois supérieure pour l'UE28.
<p>On
peut argumenter sur la nécessité d'exclure de l'EST des EU son aide
alimentaire intérieure considérable mais alors il faut le faire
aussi pour l'Inde et l'on fait en outre face à une incohérence
entre les données de l'OCDE et les notifications faites à l'OMC
pour les EU comme pour l'Inde : si l'aide alimentaire intérieure
américaine a été notifiée à 94,618 Md$ dans la boîte verte de
l'OMC pour 2018-19, les données de l'OCDE montrent que les
transferts autres que par produit aux consommateurs par les
contribuables pour l'aide alimentaire intérieure ont été de 40,192
Md$ en 2019. Et si l'Inde a notifié dans la boîte verte de l'OMC
17,212 Md$ pour les stocks publics à des fins de sécurité
alimentaire pour 2019, les données de l'OCDE montrent que les mêmes
transferts autres que par produit aux consommateurs par les
contribuables étaient de 16,932 Md$ en 2019, mais ont bondi à
58,148 Md$ en 2020 ! Mais la Chine n'a notifié que 1,149 Md$ d'aide
alimentaire intérieure pour 2016. 

<p>Si
l'on exclut de l'EST-SPM des EU les 40,192 Md$ enregistrés par
l'OCDE, la subvention par UTA tombe à 52 744 dollars et
l'EST-SPM/UTA des EU tombe à 22 333 dollars, de sorte que la
subvention agricole des EU était encore 4,7 fois celle de la Chine
et 6,3 fois celle de l'Inde. De même, si l'on exclut de l'EST-SPM de
l'Inde son aide alimentaire intérieure de 16,932 Md$, son
(EST-SPM)/UTA tombe à 2 730 dollars, de sorte que la subvention
agricole des EU était 8,2 fois supérieure à celle de l'Inde.
D'autre part, l'aide alimentaire intérieure de l'UE28 est très
faible car la plupart des Etats membres de l'UE bénéficient
d'importantes prestations sociales (dont des salaires minimaux) et,
en outre, la plupart des aides directes de l'UE ne sont pas notifiés
à l'OMC sous prétexte qu'ils ne fausseraient pas les échanges car
essentiellement découplés ou plafonnés. 

<p>Surtout
SOL a montré que la méthodologie absurde de l'Accord sur
l'agriculture (AsA) pour évaluer l'aide alimentaire intérieure peut
être appliquée aux EU au lieu d'être notifiée dans la boîte
verte de l'OMC, de sorte que, pour 8 produits seulement – trois
céréales (farine de blé, farine de maïs, riz), trois viandes
(bœuf, porc, volaille), produits laitiers en équivalent lait et
œufs – les EU auraient dû notifier à l'OMC 12,785 Md$ en 2012
dans la MGS par produit de leur aide alimentaire intérieure. Ce
montant aurait été 5,5 supérieur à la MGS indienne correspondante
pour le riz et le blé ! Pourtant, c'est l'Inde qui est condamnée
par les règles de l'OMC alors que les EU notifient toute leur aide
alimentaire intérieure dans la boîte verte !<sup><a class="sdfootnoteanc" name="sdfootnote3anc" href="#sdfootnote3sym">3</a></sup>
<p><u><b>Conclusion</b></u>
<p>Il
est clair que sur les cinq questions – niveaux de revenus et de
salaires, performances sociales, performances environnementales,
performances commerciales et soutien agricole – la Chine et l'Inde
sont en droit de revendiquer le statut de pays en développement
bénéficiant d'un traitement spécial et différencié (TSD) en
vertu des règles de l'OMC.  De nombreuses autres raisons plaident en
faveur de ce statut, que l'on peut voir dans d'autres études de SOL
ci-dessous.
<p>Cependant
cette justification du statut d'économies en développement de la
Chine et de l'Inde ne doit pas être considérée comme une
approbation de nombreux autres aspects de leurs politiques, tant sur
le plan interne – avec des régimes non démocratiques et la
persécution des Musulmans en Chine avec les Ouïghours et en Inde
depuis Narendra Mody – que sur le plan externe, notamment le
développement de l'impérialisme économique chinois avec les Routes
de la soie, et le risque d'un endettement trop important de l'Afrique
vis-à-vis de la Chine.
