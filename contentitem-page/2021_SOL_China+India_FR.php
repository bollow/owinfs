<p><b>Résumé</b>
<p>Le
présent document tente de clarifier la question de
l'autodétermination du statut de chaque membre de l'OMC en tant que
pays développé ou pays en développement (PED), qui a été
contestée principalement par les États-Unis (EU), suivis par l'UE,
contre la Chine et l'Inde. 

<p>Dans
son discours inaugural du 13 février 2021, la nouvelle directrice
générale de l'OMC, Mme Ngozi Okonjo-Iweala, a déclaré vouloir
prendre au sérieux le préambule de l'OMC : &quot;<i>Le
préambule de l'Accord de Marrakech stipule que les objectifs de
l'OMC sont de relever les niveaux de vie, d'assurer le plein emploi,
d'augmenter les revenus... L'OMC concerne les gens ! Il s'agit de
travail décent !</i>&quot;.
Et, le 26 avril 2021, elle a déclaré que l'une de ses trois
priorités serait de s'attaquer aux subventions agricoles, accordées
pour la plupart par les pays développés, dont les États-Unis et
l'Union européenne. C'est pourquoi le présent document établit des
comparaisons par habitant entre six membres de l'OMC : quatre membres
occidentaux – EU, UE28, Canada, Japon –, la Chine et l'Inde en
2019 et 2020, sur cinq points : revenus et salaires, performances
sociales, performances environnementales, performances commerciales
et soutien agricole. 

<p>Sur
tous ces points, la Chine et l'Inde sont fondées à revendiquer leur
statut de pays en développement. En quelques mots : 1) le revenu par
habitant des EU en PPA (parité de pouvoir d'achat) était en 2019
3,6 fois plus élevé que celui de la Chine et 9,3 fois plus élevé
que celui de l'Inde ; 2) l'indice de développement humain ajusté
aux inégalités des EU et de l'UE était de 22 % supérieur à celui
de la Chine et 44 % supérieur à celui de l'Inde ; 3) les émissions
cumulées de CO<sub>2</sub>
des EU étaient deux fois plus élevées que celles de la Chine et
8,3 fois plus élevées que celles de l'Inde, tandis que celles de
l'UE étaient 1,8 fois plus élevées que celles de la Chine et 7.7
fois plus que celles de l'Inde; 4) les exportations américaines de
tous les produits par habitant étaient 2,4 fois supérieures à
celles de la Chine en 2020 et 21,7 fois supérieures à celles de
l'Inde, et les exportations alimentaires américaines par habitant
étaient 9,5 fois plus importantes que celles de la Chine et 17,3
fois plus importantes que celles de l'Inde ; 5) le soutien agricole
total par unité de travail agricole (UTA) était 8,3 fois plus
important que celui de la Chine pour les EU et 2,3 fois plus
important que celui de la Chine pour l'UE28, et il était 11,1 fois
plus important que celui de l'Inde pour les EU et 3,1 fois plus
important que celui de l'Inde pour l'UE28.
<p>Cependant
cette justification du statut d'économies en développement de la
Chine et de l'Inde ne doit pas être considérée comme une
approbation de nombreux autres aspects de leurs politiques, tant sur
le plan interne – avec des régimes non démocratiques et la
persécution des Musulmans en Chine avec les Ouïghours et en Inde
depuis Narendra Mody – que sur le plan externe, notamment le
développement de l'impérialisme économique chinois avec la route
de la soie, et le risque d'un endettement trop important de l'Afrique
vis-à-vis de la Chine.
