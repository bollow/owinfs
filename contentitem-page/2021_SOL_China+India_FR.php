<p>Pour
les produits alimentaires (ainsi que pour ceux-ci plus les matières
premières agricoles), le Canada et l'Inde sont les seuls des 6 pays
à avoir un excédent en 2019 et 2020 (l'UE a un déficit à cause
des poissons et préparations). 

<p>Le
déficit alimentaire plus important de la Chine (65,2 Md$ en 2019 et
89,8 Md$ en 2020) que celui des EU (22,3 Md$ en 2019 et 17,9 Md$ en
2020) implique que celui des EU a représenté 34,3 % de celui de la
Chine en 2019 (19,9 % en 2020) mais, par habitant, le déficit
alimentaire des EU a été 1,5 fois supérieur à celui de la Chine
en 2019 (et 13,5 % inférieur en 2020). Par rapport à l'excédent
alimentaire de l'Inde de 13,7 Md$ en 2019 (de 14,4 Md$ en 2020,
malgré son grand nombre de personnes sous-alimentées !), le déficit
alimentaire des EU a été 1,6 fois inférieur à celui de l'Inde en
2019 (1,3 fois inférieur en 2020) et, par habitant, il a été 6,8
fois inférieur en 2019 (5,27 fois inférieur en 2020). Le déficit
alimentaire de l'UE28 n'a été que de 12,5% de celui de la Chine en
2019 (4,0% en 2020) mais, par habitant, il a été de 34,9% de celui
de la Chine en 2019 (11,2% en 2020). Le déficit alimentaire de
l'UE28 a été 6 fois inférieur à l'excédent alimentaire de l'Inde
en 2019 (29,5% inférieur en 2020) et, par habitant, le déficit
alimentaire de l'UE28 a été supérieur de 59% à l'excédent de
l'Inde en 2019 (71,2 supérieur en 2020).     

<p>Enfin
le déficit américain en produits alimentaires + matières premières
agricoles n'a été que de 13,6 % de celui de la Chine en 2019 (10,5
% en 2020) et, par habitant, il n'a été inférieur que de 59,1 % en
2019 (de 45,7 % en 2020). Et le déficit des EU a été 1,63 fois
inférieur à celui de l'Inde en 2019 (1,25 fois inférieur en 2020).
Le déficit alimentaire + matières premières agricoles de l'UE a
été de 9,24 % de celui de la Chine en 2019 (de 24,5 % en 2020) et,
par habitant, de 25,83 % de celui de la Chine en 2019 (6,18 % en
2020). Le déficit de l'UE28 a représenté 122,87% de l'excédent de
l'Inde en 2019 (et seulement 5,13% en 2020) et, par habitant, il a
été inférieur de 324,2% à l'excédent de l'Inde en 2019 (et
inférieur de 66,3%  à l'excédent de l'Inde en 2020).   

<p>Pour
conclure la comparaison des échanges, les déficits des EU et de
l'UE28 supérieurs à  l'excédent chinois pour tous les produits, y
compris par habitant, ne plaide pas pour le statut de pays en
développement de la Chine mais le contraire joue pour les échanges
de produits alimentaires où le déficit des EU par habitant est
inférieur à celui de la Chine, et le déficit alimentaire de l'UE28
est bien inférieur à l'excédent indien, ce qui plaide pour leur
statut de pays en développement, d'autant plus que les parts de
l'agriculture dans le PIB et l'emploi sont très supérieurs en Chine
et en Inde qu'aux EU et dans l'UE28.   

