<p>
28
avril 2021
<p>Chers
chefs d'État :
<p>Dans
tous les pays du monde, le régime d'hyper-mondialisation du
commerce, des investissements et des chaînes d'approvisionnement que
l'Organisation mondiale du commerce (OMC) a défendu et mis en place
au cours de ses 25 années d'existence est sur le point de
s'effondrer. La pandémie de COVID-19 a révélé comment le modèle
de l'OMC exacerbe l'insécurité, l'inégalité et l'instabilité.
Des règles commerciales internationales légitimes devraient
faciliter l'amélioration des moyens de subsistance, de la santé et
du bien-être de tous les peuples du monde et la survie à long terme
de la planète. Le système de l'OMC n'a pas atteint ces objectifs :
Il n'a jamais été adapté à ses objectifs et ne l'est certainement
pas aujourd'hui.
<p>Ce
constat intervient à un moment où l'OMC est confrontée à une
crise existentielle car, non seulement elle n'a pas réussi à
améliorer la vie des gens, mais dans de nombreux pays, elle a causé
des dommages importants en donnant aux intérêts des entreprises
pharmaceutiques, agroalimentaires, financières et autres des pays à
revenu élevé le pouvoir de dominer les économies, au détriment
des travailleurs des pays à revenu élevé et faible. Ses fonctions
de négociation et d'application sont paralysées, et les divisions
sont mises en évidence par le départ prématuré de l’ancien
directeur général de l'OMC.
<p>Nous,
les organisations syndicales, environnementales, de consommateurs et
autres soussignées, appelons à la transformation de l'OMC en un
cadre de commerce international entièrement nouveau et adapté au
21e siècle - ce qui implique de placer les gens et la planète au
premier plan.
<p>Cette
exigence n'est pas nouvelle. Comme nous l'avions prédit en 1995,
l'OMC a établi des règles pour l'économie mondiale qui profitent
principalement aux grandes sociétés transnationales au détriment
des économies nationales et locales, des travailleurs, des
agriculteurs et des peuples indigènes, de notre santé et sécurité,
et de l'environnement. Sans un plancher de protection du travail, un
nivellement par le bas a réprimé la croissance des salaires et
augmenté le travail précaire. Les crises du climat, de la
biodiversité et de la pauvreté ont été ignorées, les solutions
nécessaires étant limitées par les règles du &quot;commerce&quot;.
Il en va de même pour l'augmentation documentée des inégalités au
sein des pays et entre eux, car les gouvernements ont été privés
d'outils essentiels pour assurer le bien-être de leurs populations
et faire face aux effets négatifs de l'hyper-mondialisation. 

<p>Aujourd'hui,
les chaînes d'approvisionnement fragiles et profondément intégrées
créées par les règles de l'OMC au cours des dernières décennies
ont sapé la lutte de nombreux pays contre la pandémie mondiale de
COVID-19. Dans le monde entier, les pays ne peuvent pas fabriquer ou
obtenir des masques, des kits de test, des ventilateurs, des
médicaments et autres équipements nécessaires. Cela est dû au
fait que des décennies de règles de l'OMC ont donné la priorité
aux demandes des grandes entreprises de concentrer la production
mondiale afin de maximiser leurs profits et ont interdit aux pays
d'utiliser des outils politiques pour garantir la capacité de
production locale et la diversité des fournisseurs d'importation.
Les règles de l'OMC en matière de propriété intellectuelle, qui
sont conçues pour maximiser les profits des sociétés
pharmaceutiques plutôt que la santé publique, ont fait augmenter
les prix des médicaments qui sont essentiels pour combattre la
COVID-19 dans de nombreux pays et pourraient devenir un obstacle à
l'accès équitable et universel aux vaccins et aux traitements.
<p>L'OMC
elle-même est en train de se fracturer. L’ancien directeur général
est parti du jour au lendemain. Le système de règlement des
différends de l'OMC a déraillé fin 2019. Ses instances ont souvent
statué que les gouvernements devaient modifier des politiques
publiques légitimes destinées à promouvoir la santé publique,
encourager le développement, protéger l'environnement et lutter
contre la crise climatique, sous peine de sanctions commerciales
potentiellement paralysantes. La fonction de négociation de
l’organisation est mise sous pression depuis la première tentative
ratée de lancer un nouveau cycle de négociations à la réunion
ministérielle de Seattle en 1999. Les demandes des pays en
développement ont été ignorées, mais les tentatives des pays
riches d’imposer un programme auquel la plupart des pays s'opposent
ont également échoué. Puis, un cycle dit de &quot;développement&quot;
de Doha a été lancé en 2001, invoquant la nécessité de s’unir
face aux attaques du 11 septembre aux États-Unis. On a promis aux
pays en développement que leur besoin de mettre le commerce au
service du développement serait central, mais au cours des 19 années
qui ont suivi, le même programme d'expansion auquel la plupart des
pays se sont toujours opposés a été priorisé et le programme de
développement a été mis de côté.
<p>À
la lumière du dysfonctionnement de l'OMC, les pays riches et
puissants ont eu recours à des négociations plurilatérales de
nouvelles règles qui donnent la priorité aux droits et profits des
entreprises. Ces règles se heurtent à une forte opposition,
qu'elles soient promues à l'OMC ou ailleurs, et leurs échecs
constituent une preuve supplémentaire d'un paradigme qui n'a aucune
légitimité. L'accord sur le commerce des services (TiSA) et le
partenariat transatlantique pour le commerce et l'investissement
(TTIP) se sont totalement effondrés ; le partenariat transpacifique
(TPP) n'a pas pu obtenir la majorité du Congrès américain pendant
l'année qui a suivi sa signature et les États-Unis en sont restés
en dehors ; et l'Inde s'est retirée du partenariat économique
régional global (RCEP), pour ne citer que quelques exemples.
<p>Au
lieu de tirer les leçons de ces erreurs, ou de reconnaître le
gouffre qui sépare les résultats promis de la réalité, les
intérêts de l'OMC redoublent de puissance pour continuer à pousser
la même chose. Dans ce qui semble être une parodie de la parabole
selon laquelle, lorsqu'on ne voit que des clous, la réponse est
toujours un marteau, la réponse de l'OMC à la COVID-19 est de
maintenir et d'étendre les mêmes politiques de libéralisation qui
ont échoué, y compris un nouveau pacte de suppression des droits de
douane pour les produits liés à la COVID-19, totalement
contre-productif. Tout au long de la pandémie, les négociations se
sont poursuivies pour limiter la réglementation intérieure du
secteur des services, alors même que la concentration des
entreprises de services constitue un obstacle majeur à l'acquisition
et à la distribution rapides et rentables de biens essentiels. Les
négociations visant à limiter la réglementation et le contrôle
des investisseurs étrangers se poursuivent, malgré la nécessité
évidente de diversifier la production d'équipements de protection
individuelle (EPI) et de médicaments. Les négociations qui
permettraient aux grandes technologies de mieux contrôler les
données des citoyens et l'économie numérique, que les pays membres
de l'OMC ont explicitement rejetées, se poursuivent alors que la
plupart des citoyens et des gouvernements réclament à cor et à cri
des contrôles sérieux sur les grandes technologies et leur contrôle
non justifié des données.
<p>Cet
échec ne peut plus être ignoré. L'organisme commercial mondial
d'origine - l'Organisation internationale du commerce qui a été
envisagée dans la Charte de La Havane de 1948 en réponse aux
horreurs et au chaos de la Seconde Guerre mondiale - s'est concentré
sur le plein emploi, la limitation de la concentration des
entreprises, la concurrence loyale, la protection des travailleurs et
les normes visant à garantir que les politiques monétaires et
autres politiques connexes ne faussent pas le commerce. Cette vision
très différente d'un système commercial mondial fondé sur des
règles - actualisé pour tenir compte de la crise climatique, des
inégalités systémiques et du pouvoir inexplicable des grandes
technologies - reste réalisable, mais seulement si les pays
conviennent que les règles du commerce mondial sont censées
fonctionner pour les gens du monde entier, et non pour les plus
grandes entreprises du monde.
<p>Le
choix n'est pas entre le statu quo ou l'absence de commerce. C'est un
homme de paille colporté par ceux qui ne veulent rien changer. Le
changement est en train de se produire. La question est de savoir
quel cadre multilatéral peut être inclusif, promouvoir une réelle
durabilité, les droits humains et la prospérité pour tous, et
offrir les avantages d'un commerce élargi à la plupart des gens,
tout en offrant à nos représentants élus l'espace politique
nécessaire pour promouvoir l'intérêt public. <u><a href="https://unctad.org/en/pages/newsdetails.aspx?OriginalVersionID=2057">Les
principes de Genève pour un Green New Deal mondial</a></u>
en sont un exemple.
<p>Nous
appelons les gouvernements à saisir cette opportunité de changement
transformationnel.
