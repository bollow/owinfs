<p>

<p>19 November 2021
<p>Dear
Director-General Ngozi Okonjo-Iweala and all WTO Members,
<p>We
are writing with great urgency regarding the abrogation of process
leading up to the 12th Ministerial Conference of the WTO (MC12), and
to reinforce that the absence of a meaningful outcome on TRIPS Waiver
means that the WTO has failed to mount the required response to the
ongoing COVID-19 pandemic that continues to devastate countries
socially and economically.   

<p>Intellectual
property rules - enforced by the WTO’s Trade-Related Intellectual
Property Rights (TRIPS) agreement - are key barriers to containing
the COVID-19 pandemic. They confer monopolies, hinder the freedom to
operate, and the scaling up of production, directly contributing to
inequitable access around the world but especially in developing and
least developed countries.  

<p>The
TRIPS Waiver proposal (IP/C/W/669/Rev.1) was submitted in October
2020 and since has been supported by the majority of WTO Members,
international organizations (e.g. the WHO, UNAIDS, UNITAID, UNCTAD),
world leaders, faith leaders, civil society, parliamentarians and
trade unions. This proposal which calls for a waiver from certain
provisions of the WTO-TRIPS Agreement (with respect to patents,
protection of undisclosed information, copyright and industrial
designs) is globally viewed as being central to any effective WTO
response to COVID-19 and to achieving equitable access.  

<p>Despite
the significance of the TRIPS Waiver proposal, and after more than
one year of intense discussion there does not seem to be much
progress towards a meaningful outcome. Instead, scandalously the
General Council Chair Dacio Castillo unilaterally selected Ambassador
Walker of New Zealand to chair discussions on a declaration titled
“WTO response to the COVID-19 pandemic”. Ambassador Walker also
unilaterally tabled a proposed text which is clearly not designed to
resolve the pandemic. Rather, the draft text 

<p>promotes
the same liberalization demands made by developed countries in
various fora and interventions that will further constrain regulatory
space and policy tools available to WTO Members, while further
entrenching corporate influence in the institution, drastically
undermining the Member-driven character of WTO as mandated in the
Marrakesh Agreement. 

<p>Walker’s
text also calls on WTO Members to establish a “Work Plan on
Pandemic Preparedness and Resilience” that will carry the work post
MC12 on the WTO response to the COVID-19 pandemic and potentially
other future crises. However, the main intent of this work-plan
appears to be to push the liberalization and deregulatory interests
of developed countries and undermine existing mandates including the
Doha mandate of 2001, which has multiple issues of interest to
developing countries that were never delivered. Notably, the Walker
process specifically excludes discussion on the most important topic:
the TRIPS waiver. 

<p>The
reality is that the Walker process is a deplorable attempt by the WTO
to cover up what should be a grave humiliation: its inability to
agree to remove key obstacles to resolving the COVID-19 pandemic by
waiving intellectual property barriers as per the TRIPS waiver
proposal. Millions of people have died because of the WTO’s vaccine
apartheid and inequitable access.  

<p>The
WTO’s response to the pandemic, thus, must be understood as
attempting to ensure that the global pandemic not interfere with
corporate desires to maintain profits even at the expense of lives
and livelihoods globally. Instead, we call on governments and the WTO
to ensure that trade rules do not hinder the global effort to end the
pandemic and achieve global public health. 

<p>Another
process we are concerned about is the push for establishing a new
working group on WTO reform. Developing
countries and many civil society campaigns have long called for
reform of the multilateral trading system in favor of developing
countries and large vulnerable constituencies such as small farmers,
producers, workers, patient groups and indigenous peoples. However,
it is clear from their propositions that developed countries
are attempting to undermine the core fundamentals of the WTO's
mandate as a multilateral institution and alter decision-making
procedures. Instead, they wish to normalize the “Joint Statement
Initiatives (JSIs)” launched at the Buenos Aires Ministerial and
henceforward, which will undermine the
multilateral nature of the organization and its ability to deliver
anything useful for developing countries and LDCs. They also
attack developing country self-designation, a key legal principle of
the WTO, thus eventually limiting the
availability of special and differential treatment to many developing
countries and LDCs. The reform narrative is also being
utilized by some developed countries to inject
into the WTO agenda issues that will further constrain the policy
tools available to developing countries and open up more space for
big business to influence the WTO agenda.
<p>We
are gravely concerned with the further marginalization of existing
multilateral mandates for negotiations, such as the overdue Doha
Development mandate, in particular to strengthen special and
differential treatment flexibilities and make them more precise,
effective and operational; deliver an expedited solution on cotton;
and public stockholding for food security purposes, amongst other
mandated issues. These attempts seem to seek to further entrench the
WTO as a power-based rather than rules-based organization.
<p>Different
preparatory processes towards MC12 are being convened in a way that
excludes the vast majority of WTO Members, while over-privileging the
participation of developed country members. Exclusionary “Green
Room” processes should be anathema to WTO Members and must be
eliminated.
<p>In
addition, for the first time at a Ministerial Conference, the WTO
Secretariat is drastically restricting the very minimal access that
is allowed to civil society organizations (CSOs) and has even gone so
far as to abolish the very tiny provision of facilities at the NGO
center. 

<p>We
urgently call on WTO Members to engage in a course correction: to
agree immediately to the TRIPS waiver as proposed; to halt the sham
Walker process’ on further liberalization and imposition of
regulatory constraints and instead focus on real solutions; to focus
WTO reform efforts on removing barriers to development; to halt all
“Green Room” processes; to ensure full access to participation by
all WTO Members to all negotiations; and to restore at least the very
minimum levels of participation of civil society to the Ministerial
Conference.
<p>Endorses as of
November 19, 2021:
