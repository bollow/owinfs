<p>
Los movimientos sociales y la sociedad civil que luchan por la justicia económica hace tiempo que han desviado su atención de la OMC para enfocarse en los acuerdos de libre comercio bilaterales y regionales, así como en las luchas contra la deuda y la financierización. Pero el resultado de la 12ª Conferencia Ministerial de la OMC (MC12) llevada a cabo el pasado junio en Ginebra, Suiza, probablemente implique un nuevo resurgimiento de la necesidad de centrar esfuerzos de la sociedad civil y los sindicatos en la OMC.
<p>
La 12ª Conferencia Ministerial concluyó con resultados favorables para las grandes empresas, pero contrarios a los trabajadores y trabajadoras y al desarrollo con respecto a todos los asuntos importantes referidos al acceso a medicamentos, la agricultura, las subvenciones a la pesca, el comercio digital y el futuro de la propia OMC.
<p>
Los acuerdos anunciados deben servir de advertencia para todas y todos: los gobiernos de los países ricos que profesan nuevos compromisos en pos del comercio sustentable centrado en las/os trabajadoras/es pueden igualmente presionar a favor e imponer sin resquemores resultados contrarios al desarrollo y respuestas cosméticas cuando se trata de proteger las ganancias de las grandes empresas por encima del interés público.
<p>
Estos resultados auguran nuevas luchas en el horizonte: sobre cuestiones ambientales, en cinco acuerdos plurilaterales que se están negociando actualmente en la OMC; luchas venideras en torno a las inversiones y el papel magnificado de las grandes empresas en la elaboración de políticas nacionales en materia de servicios; y mucho más.
<p>
Reúnete con integrantes de la sociedad civil que participaron en la MC12 e infórmate con ellas/os de primera mano y de sus reflexiones sobre temas cruciales de cara al futuro, incluso para la sociedad civil que trabaja en cuestiones no relacionadas con la OMC.
