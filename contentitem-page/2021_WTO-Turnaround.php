<p>

<p>
September 2021
<p>
1. People around the world need a global
economy that facilitates shared prosperity with human, labor, social
and economic rights to facilitate the achievement, at a minimum, of
the 2030 Agenda for Sustainable Development, adopted by United
Nations member states in September 2015 as the Sustainable
Development Goals (SDGs). The multilateral trade system should
contribute to that vision; but instead, it is in crisis because it
has failed to deliver shared prosperity, while trampling over
people’s universal rights. This is because corporate elites have
hijacked “trade” to rig the rules of the multilateral system to
distribute income upwards, while reducing protections for people who
work. The promise of increased trade creating win-wins has proven to
be a lie and instead the majority of people have experienced
increased trade as a race to the bottom on wages, precarity, public
services, consumer standards and the environment. The crisis of the
real harm to people around the world from the neoliberal economic
order, encapsulated by the WTO, and domestic policies of austerity,
have led to uprisings on four continents, mass migrations, and the
election of right-wing populist governments in many countries. Social
instability, which particularly affects women, marginalized peoples,
the poor, and indigenous peoples, is a result of these failures, just
as the WTO’s crisis is of its own making. Governments must address
the fundamental flaws of the current system of corporate-led
globalization, not expand and entrench power imbalances. The solution
to the current attacks on the corporate version of trade policy is
not false nationalism but a new vision of multilateralism which will
result in ecological stability and shared prosperity. One blueprint
for achieving such an economy is outlined in “A New Multilateralism
for Shared Prosperity: Geneva Principles for a Green New Deal.”<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym"><sup>1</sup></a></sup>
 

<p>
2. The
COVID-19 pandemic has exposed how the WTO model exacerbates
insecurity, inequality and instability. Now, the deeply integrated
supply chains created by WTO rules over the past several decades have
undermined numerous countries’ fights against the global COVID-19
pandemic. Around the world, countries cannot make or obtain masks,
test kits, ventilators, medicines, vaccines, and other necessary
equipment. Decades of WTO rules have prioritized large corporations’
demands to concentrate global production to maximize their profits
and banned countries’ use of policy tools to ensure local
production capacity and diversity of import suppliers. WTO
intellectual property rules that are designed to maximize
pharmaceutical corporations’ profits instead of public health have
driven up prices, and created shortages, of medicines that are
essential to combat COVID-19 in scores of countries and are resulting
in death and increased sickness across the globe that could have been
prevented.
<p>
3. While a multilateral system to govern
trade and discipline corporate behavior is essential, the specific
system of rules embodied in the WTO should be under attack because,
in the 25 years of its existence, it has failed to deliver
development, promote shared prosperity, and ensure sustainability in
trade. Predictions of shared increases in growth under the WTO system
have failed abysmally, as many of our organizations predicted at the
time of its founding, while inequality has soared, leaving hundreds
of millions impoverished. Most developing countries that have gained
from trade have done so by exporting to China, whose growth is
usually attributed to its divergences with the WTO model, including
through the use of industrial policy. While some developing countries
have increased their share of global trade, their share of income
from global trade has been reduced by transnational corporations
(TNCs) who keep the high-value segments for themselves, while
outsourcing low-value operations to developing countries that are
trapped in sweatshop-led development strategies. Thus, expanding
participation in global markets has failed to result in the promised
economic development in most developing countries.
<p>
4. The WTO was founded on a mandate that
trade should serve as a means to raise standards of living and as a
means to ensure full employment. However, even as productivity has
skyrocketed since the inception of the WTO, workers’ wages and the
incomes of people who work have at best stayed flat or even decreased
with regards to capital. That is because corporations have used the
increased profits they have derived to invest in dominating the
policymaking process, particularly through the WTO as well as
bilateral and regional trade agreements, and investment agreements,
to ensure that policies shape the economy to distribute income to
them. It should not just be dependent on national redistribution
policies to “make up to the losers” of the harmful rules of trade
agreements. TNCs must be required, under national and international
law, to embed human rights due diligence throughout the supply chain,
and ensure grievance mechanisms, access to remedy and the full
implementation of the UN Guiding Principles on Business and Human
Rights (UNGPs) and the ILO Fundamental Principles and Rights at Work
as a minimum standard. Development policies that promote good jobs
and industrialization must not be constrained under global trade
rules. We need a new social contract with a labor protection floor,
including decent work, universal quality public services and social
protection coverage, human rights due diligence and corporate
accountability, social dialogue, and a just transition to a zero
carbon economy, and new anti-trust policies globally, all of which
must not be undermined by global trade rules. 

<p>
5. At the same time, the future of humans
on earth is threatened by our own vast over-consumption of carbon
which has resulted in catastrophic climate change that is currently
resulting in environmental calamities causing devastating loss of
human, animal, and plant life. Trade is a major contributor to
climate change, and WTO rules that facilitate high-carbon production
systems while limiting green technology transfer and subsidies
towards a sustainable energy economy are no longer sustainable.
<p>
6. As corporations, through ‘trade’
agreements, amass rights at the expense of people who work, they are
also doing so at the expense of democratic states. This has resulted
in an erosion of democracy, as states are failing to deliver on the
social contract. Corporations have influenced trade rules in the WTO
to allow for trade mis-invoicing which has allowed them to avoid or
evade taxes which are required for a functioning society. This
combined with the broader concentration of wealth facilitated by
trade rules has created unacceptable concentrations of power that is
fueling dangerous levels of inequality. By requiring commitments to
deregulation and foreign ownership of services, trade rules also
contribute to privatization which has eroded the provision of public
services, and limited states right to regulate TNCs - both further
increasing private corporate power over all aspects of human life.
Excessive corporate influence is also increasingly evident in other
institutions such as the United Nations and particularly in SDG 17
which is aimed to facilitate further corporate influence over global
governance. 

<p>
7. The state has a primary obligation to
realize the human rights of its citizens. Its ability to do this
depends on its ability to adapt to future challenges; exercise policy
prerogatives; establish, maintain and expand public services; and
freely regulate capital. The current trade regime does not safeguard
these essential functions of the state and subverts them to the logic
and needs of commerce.   

<p>
8. WTO rules on agriculture limit
countries’ ability to protect their own rural development, farmers’
livelihoods, and food security and sovereignty, around the world and
have contributed to multiple food crises and eroded net food security
in many countries.
<p>
9. Financial and economic stability and
predictability are essential for shared prosperity. However, evidence
shows that while the current WTO rules may have created more
predictability for investors and trading corporations, workers and
communities face increasing precarious work, outsourcing, cuts to
public services and a race to the bottom in which they are left to
face unprecedented dislocations in the global economy, often caused
by financial crises and rapid changes in trading patterns, while very
few of them enjoy an inadequate social protection.
<p>
10. The WTO is unique among multilateral
organizations in the complete lack of facilitation of the
participation of civil society, trade unions, and grassroots
communities that are negatively affected by WTO policies, while many
corporate representatives have close access to the policymaking
process, including the drafting of many agreement texts. WTO Members
are increasingly secretive about texts and negotiations, which
precludes accountability and public debate. The Secretariat is now
actively engaging in partnerships with the global corporate community
to increase its influence, while failing to remedy the exclusion of
civil society representatives from the deliberative process in the
WTO. Even more egregiously, the Secretariat is openly and publicly
advocating for expanded WTO disciplines in areas like the digital
economy, to the benefit of a few members and the Big Tech TNCs based
in those countries, while completely failing in its obligation to
facilitate the conclusion of long-blocked talks to improve the
damaging existing WTO rules to allow trade to promote real
development, sustainability, and shared prosperity. Finally, the real
crisis in the Dispute Settlement Mechanism (DSM) is in the fact it
adjudicates cases using rules that privilege corporate trading rights
over the public interest: in 80 cases involving public interest
policies, the WTO ruled in favor of corporations’ right to trade 73
times.<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym"><sup>2</sup></a></sup>
In addition, powerful countries have been far more able to enforce
their rights in the WTO through the DSM than have poor countries, a
fact that is rarely mentioned in the debates on the judicial branch
of the WTO. It is vastly inappropriate to consider expanding the
failed WTO model’s reach over human life. 

<p>
<b>Thus, as members and allies of the Our
World Is Not for Sale (OWINFS) global civil society network,
committed to a sustainable, socially just, democratic and accountable
multilateral trading system, we call for a new system of global
multilateral rules.</b>
<p>
11. Many of the below organizations have
long criticized the WTO’s rules as harmful and inappropriate for
global trade governance. At this point, the results of nearly 25
years of the WTO’s existence has proved its critics right; it is
not the right system for governing trade for shared prosperity. Thus,
a new system of multilateral trade rules must be envisioned that will
put shared prosperity, environmental sustainability, and the
achievement of the SDGs in Agenda 2030 before the commercial
interests of powerful corporations. Given the history of the WTO’s
lack of resolution of interests of developing countries; its
consecration of rights for TNCs over citizens’ and workers’
rights and environmental obligations; and the Secretariat’s biased
role in shepherding the interests aligned with powerful Members and
elites over the interests of developing countries and working people,
there is no evidence that the WTO itself is able to make the
fundamental transformational changes required in the global trade
system at this time. Thus, a new vision for multilateralism is
necessary in order to create the necessary jobs, infrastructure, and
services that are essential to achieve the SDGs, and also to
safeguard our planet for future generations. This new multilateralism
must involve a new set of rules for governing global trade.
<p>
12. In the interim, governments must make
<i>transformational changes to
existing rules </i>while a
fundamentally new institution is envisioned. The WTO rules too often
limit governments’ ability to use traditional development policies
– which were used by all industrialized countries in their
development – to promote jobs and domestic industries. Despite
hypocritical claims by developed countries and global elites in the
WTO, they have stalled the resolution of the development agenda in
the WTO for nearly 20 years. Special and Differential Treatment
(S&amp;DT) rules must be strengthened and operationalized for all
developing countries, focusing on freedom from existing harmful WTO
rules which limit the use of evidence-based development policies.
Immediately, this would mean that governments would enjoy policy
space to ensure domestic food security; to utilize technology
transfer and data rights; to make use of job-creating performance
requirements such as local content and local labor requirements; and
have more flexibility to ensure affordable access to medicines, among
other urgent priorities.
<p>
13. We also call for an immediate <i>halt
to efforts to expand</i> the scope
and coverage of existing harmful rules, or to expand them to new
arenas. In particular, our governments must halt the effort by Big
Tech corporations to use the WTO to gain new pro-corporate rights in
the digital economy, and to handcuff appropriate regulatory
oversight. We call for a stop to the efforts: to deregulate the
entire digital economy through the inappropriately named “e-commerce”
negotiations; to bring more disciplines on investment policies
through “investment facilitation” negotiations; to further
encroach on regulatory sovereignty through “domestic regulation”
negotiations; to restart talks on services liberalization; and to
efforts to limit development flexibilities in the fisheries subsidies
negotiations. Corporate boosters are undertaking these negotiations
through “plurilateral” negotiations which are illegal under the
WTO because they have no mandate, as most developing countries are
opposed to them - for good reason. 

<p>
14. Current WTO rules provide rights to
trade and limit the way that trading can be regulated. But
governments don’t trade; corporations do. So, the “rights” in
the WTO are exercised by corporate interests, while the disciplines
in the WTO limit government authority to regulate that trade in the
public interest. “Writing trade rules” is in fact often a
misnomer for “restricting regulation while empowering corporate
privileges”. This is the wrong approach to “regulating” trade.
Instead, we need a system that promotes shared prosperity by
safeguarding human, labor, economic, and social rights; allows
countries the policy space to pursue evidence-based strategies for
decent job creation and industrialization; and holds corporations
accountable under penalties for all the abuses in their supply chains
and trading practices. We call, as part of the new multilateral
architecture, for the immediate adoption of a Binding Treaty on
Transnational Corporations and Human Rights, currently being
negotiated in the United Nations.
<p>
Specifically, we call on governments to
transform the global trade system through the following: 

<p>
<b>1. We Demand Decent Work and
Industrial Development Policy Space</b>
<p>
Rather than using trade to increase
employment and prosperity, the WTO rules focus on reducing tariffs
and regulation and other policies which increase the bargaining power
of capital at the expense of labor. This forces workers into unfair
competition, resulting in a lack of minimum living wage and labor
protections, increased job precariousness, the exploitation of women,
and unsafe working environments. Capital’s increased freedom of
movement and WTO-led globalization has triggered a race to the bottom
where countries and special export zones compete in repressing labor
rights in order to attract investment. Global supply chains, the
dominant production model today, are beset with slavery, child labor,
and all forms of labor exploitation that affect women workers
disproportionally. In addition, global corporations are seeking to
bring in new rules to guarantee further rights for investors at the
expense of workers, regulators, and the public generally, through
talks towards an agreement on “Investment Facilitation.” Instead,
we need a global trade system that focuses on increasing decent job
opportunities and living standards of people who work.
<ul>
	<li>
	Any current or future agreement must
	focus on using trade to promote and expand Decent Work with a new
	social contract and a labor protection floor as called for in the
	ILO Centenary Declaration, and protecting policy space of countries
	to pursue shared prosperity through full employment, through
	structural transformation and industrialization and through higher
	wages and social dialogue.
	<li>
	Talks to bring further investment rules
	in the WTO through “Investment Facilitation” must be stopped.
	<li>
	In addition, we call for Social Impact
	Assessments including on inequality, the quality of employment and
	on job creation with regards to current, and any potential future
	changes to WTO policies and negotiations, and the implementation of
	changes to existing rules that are necessary to ensure a
	multilateral trade system that promotes full employment and decent
	work.
</ul>
<p>
<b>2. We Demand Policy Space for Digital
Industrialization and the Ability to Use Data for the Public Interest</b>
<p>
Now, the largest corporations are seeking
to use the WTO to rewrite the rules to legally entrench their
monopolistic practices through access to and control of the world’s
most valuable resource, data, forever. But data must not be
controlled by private corporations for private profit maximization.
Instead, many people and governments are starting to realize the need
for national and community ownership of data, and data sovereignty.
All countries and communities should have the right to use their
data, as well as technology and digitalization generally, to foment
industrialization, decent jobs, and for the general public interest,
such as improving access to quality public services. In addition,
corporations are seeking to use digital trade negotiations to
consolidate their damaging business model of avoiding regulation,
exploiting labor, evading the payment of taxes (both corporate taxes
and trade taxes or tariffs) and demonstrating a total lack of
accountability to the communities from which they profit.
Appropriate, democratic, and participatory internet governance, and
rights to privacy, people’s economic rights to their data, and data
protection, must not be subsumed to private commercial interests.
Current negotiations towards an agreement on digital trade go far
beyond e-commerce and would jeopardize our privacy and people’s
economic rights to their data; exacerbate inequalities including
through the digital divide; promote tax avoidance by digital
corporations; decimate micro-, small-, and medium enterprises
(MSMEs); cement monopolistic control of first-movers; make us less
safe by proscribing appropriate oversight over source codes and
algorithms; and forever abrogate policy space for digital
industrialization. Negotiators are claiming alleged benefits to women
entrepreneurs and MSMEs while the actual negotiations include
provisions that would be extremely harmful to the vast majority of
women and small businesses. We demand that:
<ul>
	<li>
	Plurilateral and multilateral (as well
	as bilateral) negotiations towards a binding agreement on digital
	trade (“e-commerce”) must be halted.
	<li>
	The appropriation of “women’s
	empowerment” and MSMEs in the name of promoting digital trade
	rules must be abandoned.
	<li>
	Governments should agree outside the WTO
	a global high-quality standard for personal data protection,
	national and community ownership over their data, as well as
	enforceable principles for the use of data, artificial intelligence
	and machine learning in ways that are ethical and promote equity and
	social justice. 
	
	<li>
	Efforts to enforce new obligations on
	members through accepting the “technological neutrality”
	equivalence of services commitments through new technologies must be
	stopped.
	<li>
	New pro-competition policies must be
	agreed and enforced, in a venue outside of the WTO.
	<li>
	Current rules, including in the WTO,
	which facilitate tax avoidance, tax evasion, and other illicit
	financial flows, particularly by digital corporations, must be
	altered to ensure that countries have an appropriate fiscal base to
	fund the achievement of the SDGs.
	<li>
	In addition, any current WTO constraints
	on policy space for digital industrialization, the use of data for
	the public good, and the achievement of the SDGs on access to
	information and communications technology, including industrial
	diversification and value addition – such as limits in performance
	requirements such as technology transfer, hiring of local workers
	and managers, use of local inputs – should be removed.
</ul>
<p>
<b>3. We Demand the Right to Protect the
Policy Space for Development</b>
<p>
Trade can contribute to shared
prosperity, under the right set of rules, which are different
depending on a country’s level of development. Trade is not the
goal <i>per se</i>.
If the actual result of trade under the current or proposed rules
hinders the ability of countries to develop and of workers to prosper
from their labor, then new rules are needed. Developing countries
have experienced harmful impacts of WTO rules since they began
implementing them, and have put forward myriad proposals to allow
them to access the benefits of trade while maintaining policy space
for development. These “Implementation Issues,” now packaged in
the G90 proposals on Special and Differential Treatment (S&amp;DT)
were designed to address some of the asymmetries and biases of the
Uruguay Round. Instead, some developed countries are attacking the
few flexibilities from damaging existing WTO rules that are allowed
for developing countries, through calls for “differentiation” of
countries with large populations that would force them to adopt even
more onerous disciplines that would foreclose their ability to
develop their economies. According to the latest research by UNCTAD,<sup><a class="sdfootnoteanc" name="sdfootnote3anc" href="#sdfootnote3sym"><sup>3</sup></a></sup>
this “differentiation” is entirely inappropriate and has no basis
in economic reality. Even further, some countries propose even more
onerous burdens on developing country members under the banner of
“transparency and notification.” At the same time, in
negotiations to discipline subsidies in the Fisheries sector, some
countries are pushing to limit SDT provisions (which are mandated by
SDG 14.6) which would prevent developing countries from benefiting
from their fisheries resources and ensuring livelihood security for
small-scale fishers while allowing first-mover advantages for large
commercial fishing fleets which have caused the vast majority of
overfishing. In addition, the trade concerns of the Least Developed
Countries (LDCs) should not fall victim to the extreme demands of
TNCs for more market access in developing countries with larger
populations. Thus:
<ul>
	<li>
	A strong package of proposals for policy
	space for development, known as the Special and Differential
	Treatment agenda, must be re-centered as the main focus in the WTO
	and agreed to immediately. 
	
	<li>
	Attempts to prevent developing countries
	from accessing rights and flexibilities in the WTO through
	“differentiation” must be abandoned.
	<li>
	The expansion of binding commitments to
	cut tariffs through the Non-Agricultural Market Access (NAMA)
	negotiations, the Environmental Goods Agreement (EGA), or the
	pressuring of countries to join the expanded Information Technology
	Agreement (ITA-II) must be abandoned.
	<li>
	Any agreement to limit subsidies in the
	Fisheries sector must not undermine sovereign rights already set out
	in the UN Convention on the Law of the Sea. Any outcome must include
	a strong component of S&amp;DT provisions to ensure that developing
	countries can pursue sustainable development of their fishing
	sectors while the harmful subsidies of large industrial fleets must
	be disciplined.
	<li>
	The LDC package, including Duty-Free
	Quota-Free access for LDCs, a fair and immediate resolution to the
	demands of the cotton-producing countries, and a services waiver for
	LDCs, should be implemented immediately.
	<li>
	Proposals to increase administrative
	burdens of developing countries under the rubric of “transparency
	and notification” are wholly inappropriate and should be replaced
	with changes to make the institution function in a more transparent
	and democratic manner that is respectful of capacity constraints of
	member countries, including through not holding simultaneous
	meetings; ensuring that meetings properly documented and that
	interpretation is universally provided.  
	
	<li>
	If LDCs pursue accession into the WTO,
	their entry must not be conditioned on market access demands, but
	they should be able to accede on terms that allow them to use trade
	according to their development needs. These terms should not go
	beyond those applied to original LDC Members of the WTO. Countries
	that are “graduating” from LDC status should be provided
	flexibility to undertake new rules in ways that are compatible with
	their development trajectory.
	<li>
	A thorough assessment of existing
	constraints to countries’ ability to pursue domestic policies and
	use trade for development must be conducted so that rules across the
	WTO on intellectual property, agriculture, investment, services, and
	goods can then be transformed in accordance with structural
	transformation development policies.
</ul>
<p>
<b>4. We Demand Access to Health and
Affordable Medicines, Not Patent Monopolies</b>
<p>
Patent monopolists are the most protected
class within the rules of the WTO through the Trade-Related Aspects
of Intellectual Property Rights (TRIPS) agreement. The TRIPS
agreement has resulted in a massive transfer of wealth from patients
and health budgets of states to corporations, and from the global
South to pharmaceutical corporations, almost exclusively based in the
global North. As corporations pursue patent extensions rather than
innovating needed medicines, a crisis in innovation has resulted. The
abolition of the TRIPS agreement would save millions of lives around
the world by facilitating access to essential medicines for people in
all countries. Advocates for access to health care and affordable
medicines gained an important victory in 2001 through the adoption of
the Doha Declaration on Public Health and the TRIPS. This declaration
reaffirmed the flexibilities contained in the TRIPS agreement and its
use to address the public health needs of WTO member states. However,
the negotiations that followed the Declaration that were to have
facilitated export of medicines under compulsory licensing for
countries concluded without establishing a workable mechanism to
translate the agreed rights into real access. Public health has
suffered due to the complex and stringent patent monopolies that are
prioritized above ensuring access to medicine. During the Covid-19
pandemic, the lack of immediate agreement to the TRIPS waiver as
proposed in October 2020 has resulted in millions of unnecessary
deaths and illnesses. Thus, in the immediate term:
<ul>
	<li>
	Members must agree immediately to the
	TRIPS waiver, as it has been proposed by more than 60 countries, in
	order to save lives during the global pandemic of Covid-19. 
	
	<li>
	Member states must review the August
	30<sup>th</sup>
	decision of 2003 on TRIPS and the subsequent decision to amend the
	TRIPS in 2005 and improve these decisions to make them workable.
	<li>
	Members should agree to a <i>permanent</i>
	moratorium on non-violation TRIPS complaints.
	<li>
	Members must make permanent the waiver
	of TRIPS obligations for LDCs, <i>as
	long as they are LDCs,</i>
	without any conditions, including allowing LDC members to roll back
	existing TRIPS implementation.
	<li>
	In addition, members should conduct an
	assessment of the impact of the TRIPS agreement on innovation and
	access to medicines worldwide, and should then fundamentally
	transform the regulation of medicines based on the right to health
	and the promotion of research and development of, and access to,
	vaccines and medicines.
</ul>
<p>
<b>5. We Demand Trade Rules that Support
Food Security and Food Sovereignty</b>
<p>
The persistence of nearly one billion
hungry people worldwide, increasing corporate consolidation in the
agriculture industries, and multiple global food crises, have
highlighted our broken system of trade in food. Poor farmers are
exposed to floods of imports when prices are too low (often due to
unfairly dumped exports from rich countries) while at the same time
the ranks of humans suffering from hunger expands when food prices
spike. This volatility is a result of excessive speculation in the
commodities markets, and global food rules in the Agreement on
Agriculture (AoA) – written largely to satisfy corporate
agribusiness – that treat food as a product for corporate profit
instead of a Human Right. In addition, current rules constrain
developing countries to the lower rungs of the global value chains in
agriculture. SDG 2 – “End hunger, achieve food security and
improved nutrition and promote sustainable agriculture” – is a
key component of achieving sustainable development in all countries,
especially developing and least developed countries. Goal 2 targets
relate to, among others, ensuring access to food for all groups
especially the poor and marginalized, improving nutrition, doubling
productivity of small farmers, maintaining biodiversity, and use of
genetic resources. It is time for a fundamental transformation of the
food system. The following changes to the global trade rules are
essential to allow countries to ensure Food Sovereignty and Food
Security and promote rural livelihoods:
<ul>
	<li>
	All countries should have the right to
	subsidize food, to protect farmers’ livelihoods and to promote
	rural development, as long as their subsidies and other policies do
	not hurt farmers in other countries.
	<li>
	The right to subsidize food for the
	poor, including through direct payments to poor farmers, as proposed
	in the current negotiations (by the G33 group of 47 countries)
	should be immediately agreed as essential to promote food
	sovereignty to ensure food security.
	<li>
	Developing countries should have the
	right to use trade instruments such as tariffs and use other
	measures to protect farmers’ livelihoods, rural development, and
	food security, especially when facing import surges from subsidized
	production. This ability, called the Special Safeguard Mechanism
	(SSM) in the WTO, must be agreed to, but in a way that is far more
	flexible than is reflected in current WTO proposals for it to be
	useful in achieving food security.
	<li>
	No additional commitments on Agriculture
	should be sought from LDCs.
	<li>
	Developing countries should be able to
	exempt products from any potential future tariff cuts as they deem
	necessary to protect farmers’ income, food security and rural
	development; the Special Products designation in the WTO must be
	expanded.
	<li>
	Subsidies for products that are exported
	should be disciplined, including so-called “Green Box” subsidies
	and Aggregate Measure of Support (AMS) entitlements.
	<li>
	Trade rules must be modified to
	facilitate proper regulation of commodities to prevent excessive
	speculation and volatility in the global markets.
	<li>
	In addition, members should conduct an
	assessment of the impact of the AoA on food security, farmers’
	livelihoods, land diversion for agro-fuels, and rural development,
	and then completely redesign global agricultural trade rules to
	allow countries to exercise food sovereignty.
</ul>
<p>
<b>6. We Demand Protection of
Biodiversity and the Banning of the Patenting of Life</b>
<p>
One of the important outcomes of the Doha
declaration was to examine the relation of TRIPS agreement with the
UN Convention on Biological Diversity (CBD). Through ten years of
deliberation, some Members have made concrete proposals to amend the
TRIPS agreement to prevent biopiracy, but others have blocked these
from being agreed. We demand that:
<ul>
	<li>
	Members must amend the TRIPS agreement
	in order to guarantee coherence with SDG 2.5, 2. A, 15.6, 15.9, 15.a
	to require disclosure of country of, and proof of, prior informed
	consent and fair and equitable benefit sharing, at least as strong
	as that agreed in the CBD / Nagoya Protocol and the International
	Treaty on Plant Genetic Resources for Food and Agriculture
	(ITPGRFA). This amendment must also cover digital sequence
	information (DSI) as demanded by many biodiverse rich developing
	countries.
	<li>
	The mandatory review of the patenting of
	life obligation under Article 27.3 (b) of the TRIPS Agreement must
	be completed. We reiterate the call for a ban on patenting of life
	forms as many developing countries have proposed.
</ul>
<p>
<b>7. We Demand Financial Stability
Instead of Financial Deregulation</b>
<p>
The deregulation and liberalization rules
of the WTO in the financial services sector helped set the stage for
the debacle of the global financial crisis. Yet, while the G20, most
governments and even the International Monetary Fund (IMF) have
recognized the need for financial re-regulation in the wake of the
financial crisis, the WTO’s General Agreement on Trade in Services
(GATS) rules can actually hinder financial regulation efforts.  And
current proposals in the GATS negotiations would actually extend the
market access rules which establish even further limits on domestic
regulation of financial services. Digitalization of finance and
digital currencies specifically pose further risks that are not being
understood adequately, and for which trade rules – including under
new digital trade proposals – will constrain proper regulatory
oversight. Immediate changes are needed to current GATS rules
regarding financial services, and no further deregulation or
liberalization of financial services should be undertaken outside of
or within the WTO. Specifically:
<ul>
	<li>
	Countries should not take any additional
	commitments for liberalization of trade in financial services within
	GATS negotiations or through any other trade agreement.
	<li>
	Countries must maintain the power, in
	any trade provision or agreement, to require the storage of
	financial data locally for the purpose of effective financial
	regulation, reviewing assets for tax purposes and for the prevention
	of fraudulent or predatory practices.
	<li>
	In addition, existing GATS rules should
	be reviewed and evaluated in light of the financial crisis, and then
	modified in order to ensure policy space for all countries to use
	macroprudential measures, such as capital controls and financial
	transaction taxes, as well as to implement other financial
	regulation measures.
</ul>
<p>
<b>8. We Demand that Trade Rules Not
Limit the Role of the Democratic State to Provide Universal Quality
Public Services nor Limit Regulation of the Economy in the Public
Interest. </b>

<p>
The existing GATS rules provide
corporations rights to operate in markets under conditions favorable
to them, while constraining public regulatory oversight, and should
not be expanded. Corporations have too much control over policymaking
including in foreign countries. In spite of the need to roll-back
corporate influence over public interest policymaking, some countries
are proposing further disciplines in the “Domestic Regulation”
negotiations. Thus, we demand that:
<ul>
	<li>
	Efforts to achieve further disciplines
	on Domestic Regulation should be abandoned.
	<li>
	Current and future public services in
	particular must be excluded from any existing or future agreements
	that include services with a broad and authentic carve-out. This
	must include services that may need to be re-municipalized in
	future.
	<li>
	In addition, a fundamental assessment of
	the implications of the GATS on ensuring proper regulatory oversight
	of services and ensuring access to quality public services should be
	undertaken by member states and the WTO. Any constraints on
	achieving the SDGs with regards to good health and well-being,
	inclusive and equitable quality education, clean water and
	sanitation, affordable and clean energy, and other services should
	be removed without challenge from the WTO rulebook.
</ul>
<p>
<b>9. The WTO Is Not the Venue to
Establish Climate Change Policy</b>
<p>
Expanded trade is a major cause of
climate change, which is resulting in environmental, economic, and
social crises worldwide. However, putting climate change on the
agenda of the WTO would represent a grave danger to climate
stabilization, as WTO rules structurally favor the increasing of
trade flows – a huge contributor to the greenhouse gases that cause
climate change – over environmental sustainability. Ideally, the
Paris Agreement should set a floor for the respect of environmental
standards and climate goals in production and trade. Instead of this,
WTO rules currently constrain countries from greening of industries
and consequently promoting a “Just Transition,” a decent
work-centered approach to transitioning to climate-friendly
technologies. Several aspects of current WTO rules should be altered
to allow more policy space for countries to protect the environment
and pursue sustainable development policies. Other multilateral
agencies are far more appropriate venues for the discussion of and
implementation of rules related to the global climate than the WTO.
<ul>
	<li>
	TRIPS rules should be altered to allow
	countries additional flexibilities to the intellectual property
	regime, regarding technology transfer, including in other
	multilateral venues.
	<li>
	Climate change policies, including those
	mandating a job-creation focus to clean energy technologies, should
	not be subject to challenge through the WTO’s Dispute Settlement
	Mechanism.
</ul>
<p>
<b>10. The WTO, Including the
Secretariat, Must Stop Secretive, Undemocratic Functioning</b>
<p>
The WTO is not fit for purpose to govern
global trade. While a new system is being created, immediate changes
to the way in which the WTO functions are also required, including: 

<ul>
	<li>
	The WTO, including negotiations and
	other meetings, must be open to and facilitate proper civil society
	participation.
	<li>
	Documents and texts in the WTO must be
	publicly available. 
	
	<li>
	Current “collaborations” between the
	business sector and the WTO must be halted. 
	
	<li>
	The Secretariat’s role must not be
	expanded beyond the current mandate. 
	
	<li>
	The Secretariat must not act in favor of
	certain members’ demands to the exclusion of other members’
	demands in the negotiations. 
	
	<li>
	A review of the decisions of the DSM
	must be conducted with a view towards abridging those which place
	commercial concerns over the public interest.
</ul>
<p>
There are many more changes that must be
made to the global trading system, including a fundamental review,
abrogation, or transformation of bilateral and regional trade and
investment agreements. The above represent a minimum of immediate
changes that must be made in the multilateral trade system to provide
more policy space for all countries to implement solutions towards
promoting shared prosperity for the benefit of all.
