14 April 2021
<p>
More than 170 former heads of state and government and Nobel laureates, including former Prime Minister of the United Kingdom Gordon Brown, former President of Colombia Juan Manuel Santos, former President of Liberia Ellen Johnson Sirleaf, former President of France François Hollande and Nobel Laureates Professor Joseph Stiglitz and Professor Francoise Barre-Sinoussi, today called on President Biden to support a waiver of intellectual property rules for COVID-19 vaccines and pursue a people’s vaccine to end the pandemic in an open letter today. A waiver of intellectual property rules would allow for a scale up in manufacturing in the U.S. and around the world, overcoming artificial supply constraints.
<p>
The former world leaders and Nobel Laureates encourage President Biden to take the urgent action only he can and “let this moment be remembered in history as the time we chose to put the collective right to safety for all ahead of the commercial monopolies of the few.”
<p>
The letter specifically asks President Biden to support a proposal from the South African and Indian governments at the World Trade Organization (WTO) to temporarily waive intellectual property rules related to COVID-19 vaccines and treatments. At the current pace of vaccine production, most poor nations will be left waiting until at least 2024 to achieve mass COVID-19 immunization.

 <p>
<b>Gordon Brown, former Prime Minister of the United Kingdom said:</b>
<p>
<i>“President Biden has said that no one is safe until everyone is safe, and now with the G7 ahead there is an unparalleled opportunity to provide the leadership that only the U.S. can provide and that hastens an end to the pandemic for the world.”</i>
<p>
<i>“An urgent temporary waiver of intellectual property rules at the World Trade Organization would help us ramp up global supply of vaccines together with a global multi-year burden sharing plan to finance vaccines for the poorest countries”.</i>
<p>
<i>“This would be in the strategic interests of the U.S., and of every country on the planet".</i>
<p>
<b>Joseph Stiglitz, Nobel Economics Prize Laureate, said:</b>
<p>
<i>“While the U.S. has made enormous progress in vaccinating its own population, thanks to the efforts of the Biden administration, that alone is unfortunately not enough”.</i>
<p>
<i>“New mutations of the virus will continue to cost lives and upend our interconnected global economy until everyone, everywhere has access to a safe and effective vaccine. Intellectual property is the utmost artificial barrier to global vaccine supply. We as a nation must lead with our allies to back the South Africa and India waiver at the WTO, insist on technology transfer, and strategically invest in production”.</i>
<p>
<b>François Hollande, former President of France, said:</b>
<p>
<i>“The extreme inequality in access to vaccines around the world creates an unbearable political and moral situation. It is above all sanitary and economic nonsense as we are all concerned. That the Biden administration is considering waiving barriers related to intellectual property rules offers hope for the international community. If the United States supports the lifting of patents, Europe will have to take its responsibilities. In the face of this devastating pandemic, world leaders must prioritize the public interest and international solidarity”.</i>
<p>
Other signatories include Mary Robinson, the former President of Ireland, Fernando Henrique Cardoso, the former President of Brazil, and Helen Clark, the former Prime Minister of New Zealand, together with over 60 other former heads of state and heads of government that span every continent.
<p>
The leaders also called for the intellectual property waiver to be accompanied by the open sharing of vaccine know-how and technology, and by coordinated and strategic global investment in research, development, and manufacturing capacity, especially in developing countries, underscoring that threats to public health are global and require global solidarity-based solutions.       
<p>
These actions would expand global manufacturing capacity, unhindered by industry monopolies that are driving the dire supply shortages blocking vaccine access. The resulting vaccine inequality, the leaders warned, means that the U.S. economy already risks losing $1.3 trillion in GDP this year, and if the virus is left to roam the world, the increased risk of new viral variants means even vaccinated people in the U.S. could be unprotected once more. 

 
<p>
The letter, which was coordinated by the People’s Vaccine Alliance, a coalition of more than 50 organizations including Club de Madrid, Public Citizen, Oxfam and UNAIDS, warned that at the current global immunization rate, it was likely that only 10 percent of people in the majority of poor countries will be vaccinated in the next year. 
<p>
<b>Françoise Barré-Sinoussi, Nobel Prize in Physiology or Medicine Laureate said:</b>
<p>
<i>“We will not end today’s global pandemic until rich countries – most especially the United States – stop blocking the ability of countries around the world to mass produce safe and effective vaccines”.</i>
<p>
<i>“Global health is on the line. History is watching. I, with my fellow laureates and scientists across the globe, urge President Biden to do the right thing and to support the TRIPS waiver, insist on pharmaceutical corporations to share vaccine technologies with the world, and strategically invest in distributed production”.</i>
<p>
<b>Muhammad Yunus, Nobel Peace Prize Laureate said:</b>
<p>
“Big pharmaceutical companies are setting the terms of the end of today's pandemic – and the cost of allowing senseless monopolies is only more death and more people being pushed into poverty”.
<p>
“Only government action - not philanthropy, and not the private sector - can solve today’s unprecedented crisis. We together urge President Biden to stand on the right side of history – and ensure a vaccine is a global common good, free of intellectual property protections". 
