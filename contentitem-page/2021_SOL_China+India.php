Even if the US life expectancy at birth and maternal and infant
mortality rates are significantly higher than in the 3 EU countries,
Canada and Japan, they are much lower than in China and India. Again
these two sets of indicators on income and health inequalities
justify their developing country status at the WTO.     

<p>
<u><b>III – Environmental performance</b></u>
<p>
Table 3 shows first the amount and share of each
country (and EU28) in global cumulative greenhouse gas (GHG)
emissions in CO<sub>2</sub>
equivalent since 1751 and then in total and from agriculture
emissions in 2019. For cumulative emissions the US 24.82% share was
twice that of China and 8.3 times that of India while the EU28 22.0%
share was 1.8 times that of China and 7.7 times that of India. 

<p>
For 2019 the US total GHG was of 49% that of China and twice that of
India and, per capita, 2.3 times that of China and 9.6 times that of
India. For the EU28 the total GHG was 35% that of China and 1.4 times
that of India and, per capita, 92% that of China and 3.8 times that
of India. For the GHG emissions of agriculture the US level was 57%
of that of China and 60% that of India but, per capita, il was 2.5
times higher than that of China and India. For the EU28 the EU
emissions were at 60% of those of China and 64% of those of India
but, per capita, they were 1,7 times higher than those of China and
India.     

<p>
Clearly on climate change China and India can claim their developing
country status. 

