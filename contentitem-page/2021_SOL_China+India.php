<p>
<u><b>Summary</b></u>
<p>
The present paper attempts to clarify the issue of self-determination
of the WTO status of each Member as a developed country or developing
country (DC), which has been challenged mainly by the US, followed by
the EU, against China and India. 

<p>
In her inaugural speech of 13 February 2021, the
WTO new Director General, Dr <span style="background: #ffffff">Ngozi
Okonjo-Iweala, </span>said she
wanted to take the WTO preamble seriously: &quot;<i>The
preamble of the Marrakesh Agreement states that the objectives of the
WTO are to raise living standards, ensure full employment, raise
incomes… </i><i>The WTO is about people!
It’s about decent work!</i>&quot;. And,
on  26 April 2021, she said that one of her three priorities will be
to tackle agricultural subsidies, mostly given by developed nations
including the US and the EU. It is why this paper makes its
comparisons on a per capita basis of six WTO Members: four Western
Members – USA, EU28, Canada, Japan –, China and India in 2019 and
2020, on five issues: income and wages; social performance;
environmental performance; trade performance and agricultural
support. 

<p>
On all these issues China and India are justified
to claim their developing country status. In few words: 1) the US per
capita income at PPP (purchasing power parity) was in 2019 3.6 times
higher than that of China and 9.3 times higher than that of India; 2)
t<span style="background: #ffffff">he
Inequality-adjusted Human Development Index </span>of
the US and EU was 22% larger than that of China and 44% larger than
that of India; 3) the cumulative CO<sub>2</sub>
emissions of the US was twice that of China and 8.3 times that of
India while those of the EU were 1.8 times that of China and 7.7
times that of India; 4) per capita US exports of all products were
2.4 times larger than those of China in 2020 and 21.7 larger than
those of India and per capita US food exports were 9.5 times higher
than those of China and 17.3 times higher than those of India; 5) the
total agricultural support per agricultural working unit (AWU) was
8.3 times that of China for the US and 2.3 times for the EU28, and 
it was 11.1 times that of India for the US and the 3.1 times that of
India for the EU28. 

<p>
However, this justification of China's and India's
status of developing economies should not be seen as an endorsement
of many other aspects of their policies both internally –
particularly in terms of undemocratic regimes and the persecution of
Muslims in China with Uighurs and in India since Narendra Mody –
and externally, notably the development of Chinese economic
imperialism with the Silk Roads, especially the risk of too large
indebtedness of Africa to China.
<p style="text-align: center">* &nbsp; *<br>*</p>
<p>
<b>Introduction</b>
<p>
The present paper attempts to clarify the issue of self-determination
of the WTO status of each Member as a developed country or developing
country (DC), which has been challenged mainly by the US, followed by
the EU, against China and India, by comparing available official data
of broad macroeconomic indicators and trade performance of six WTO
Members: four Western Members – USA, EU28, Canada, Japan –, China
and India in 2019 and 2020. 

<p>
In her inaugural speech of 13 February 2021, the
WTO new Director General, Dr <span style="background: #ffffff">Ngozi
Okonjo-Iweala, </span>said she
wanted to take the WTO preamble seriously: &quot;<i>The
preamble of the Marrakesh Agreement states that the objectives of the
WTO are to raise living standards, ensure full employment, raise
incomes, expand production and trade in goods and services, and seek
the optimal use of the world's resources in accordance with the
objective of sustainable development. </i><i>The
preamble says it all! The WTO is about people! It’s about decent
work!</i>
<i>Let's put its overarching
objective at the forefront as the driving force behind everything we
seek to achieve for the multilateral trading system… T</i><i>he
WTO’s work in new or innovative areas does not mean that
traditional topics such as agriculture are forgotten. Agriculture is
particularly important for many developing and least developing
countries. Improving market access for export products of interest to
these countries is of paramount importance, as is dealing with trade
distorting domestic support. The growing domestic support
entitlements of Members </i><i><br/>
</i><i>must
be addressed to level the playing field, so as to provide
opportunities for small </i><i><br/>
</i><i>scale
farmers</i>&quot;<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym">1</a></sup>.
And, in a videoconference organised by the European Commission on 26
April 2021), &quot;<i>Okonjo-Iweala said
that one of her three priorities for this year will be to tackle
agricultural subsidies, which are mostly given by developed nations
including the US and the EU… She said Beijing wants to see progress
on agricultural subsidies, which currently represent around €1
trillion and could double by 2030. &quot;I would like to look at
subsidies across the board” and see how “from all perspectives we
are creating a level playing field” the WTO chief said</i>&quot;<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym">2</a></sup>.
<p>
Taking Dr <span style="background: #ffffff">Ngozi
Okonjo-Iweala</span> seriously that
WTO is about people, most comparisons will be made on a per capita
basis. These analyses will cover five issues: levels of income and
wages; social performance; environment performance; trade performance
and agricultural support. On all these issues China and India are
justified to claim their developing country status. 

<p>
<u><b>I – Macro-economic indicators of the US, EU28, Japan, Canada,
China and India </b></u>

<p>
As macroeconomic indicators for the EU28 are not
available but only for the Eurozone, we use the data for the three
main countries: Germany, France, United Kingdom (UK), together with
the US, Japan, Canada, China and India. Table 1 shows that the US per
capita income at PPP (purchasing power parity) was in 2019 3.6 times
higher than that of China and 9.3 times that of India. In per capita
nominal GDP the US level was 5.8 times higher than that of China and
31.2 times that of India. And the US average wage at PPP was 3.4
times higher in 2019 than in China and 8.1 times than in India.
<p>
The percentage of US employment in agriculture (without forestry and
fishing) was 18.1 times lower than in China in 2019 and 30.4% lower
than in India (25% lower than in Nigeria).
<p>
Those economic indicators justify the developing country status of
China and India at the WTO.
