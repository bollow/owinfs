all products was 5.9 times higher in 2019 (10.5 times higher in 2020)
and, per capita, the US deficit was 24.6 times that of India (43.9
times in 2020!). The EU28 deficit of all products of $230bn in 2019
was 65% of the China surplus of $430bn (the EU deficit of $188bn in
2020 was 35% of the China surplus in 2020) and, per capita, the EU
deficit of $545 in 2019 ($366 in 2020) was 82% higher that the China
surplus of $300 (98.5% higher in 2010. The EU deficit was 56% higher
than that of India in 2019 (twice higher in 2020) and, per capita, it
was 4.8 times higher in 2019 (5.5 times higher in 2020). 

<p>
For food products as well as for food + agricultural raw materials
Canada and India were the only of the 6 countries to have a positive
balance in 2019 and 2020 (the EU balance is negative because of fish
and preparations). The larger food deficit of China ($65.2bn in 2019
and $89.8bn in 2020) than those of the US ($22.3bn in 2019 and 17.9
bn in 2020) implies it was 34.3% of that of China in 2019 (19.9% in
2020) but, per capita, the US food deficit was 1.5 times higher than
that of China in 2019 (and 13.5% lower in 2020). Compared to India
food surplus of $13.7bn in 2019 and $14.4bn in 2020 (despite its
large number of undernourished people!), the US food deficit was 1.6
times lower than the India surplus in 2019 (1.3 times lower in 2020)
and, per capita, it was 6.8 times lower in 2019 (5.27 times lower in
2020). The EU28 food deficit was only 12.5% that of China in 2019
(4.0% in 2020) but, per capita, it was 34.9% that of China in 2019
(11.2% in 2020). The EU28 food deficit was 6 times lower than the
India food surplus in 2019 (29.5% lower in 2020) and, per capita, the
EU food surplus was 59% higher than the Indian food surplus in 2019
(71.2 higher in 2020).     

<p>
Finally, the US deficit in food + agricultural raw materials was of
only 13.6% that of China in 2019 (10.5% in 2020) and, per capita, it
was only lower by 59.1% in 2019 (45.7% in 2020). With India the US
deficit was 1.63 times lower than that of China in 2019 (1.25 times
lower in 2020). The EU balance of food + agricultural raw materials
was 9.2% of that of China in 2019 24.5% lower in 2020) and, per
capita, (2.2% in 2020) but, per capita, of 34.9% of that of China in
2019 (11.2% in 2020). It was 121.9% lower than that of India in 2019
(24.5% lower in 2020) and, per capita, it was 324.2% lower than that
of India in 2019 (65.9% lower in 2020).   

