<u><b>V – Comparison of the agricultural supports of the 6
countries in 2019</b></u>
<p>
Despite the theoretical and operational limitations of OECD
indicators of agricultural supports, as they are used worldwide let
us compare their levels in the major Western economies – USA, EU28,
Canada, Japan – with those of China and India. 

<p>
The OECD most significant indicator is the TSE
(total agricultural subsidies) per agricultural working unit (AWU),
but the MPS (market price support) is excluded because it concerns
essentially import protection, particularly since 2014 when all
explicit export subsidies were eliminated. In 2019 the US TSE-MPS was
at $92.966 billion (bn)<sup><a class="sdfootnoteanc" name="sdfootnote3anc" href="#sdfootnote3sym">3</a></sup>
which, divided by 2.363 million (mn) AWU, implied an average subsidy
of $39,342 per AWU. At the same time the EU28 TSE-MPS was of $97.237
bn which, divided by 8.954 mn AWU, implied an average subsidy of
$10,860 per AWU. And China TSE-MPS was of $93.386 bn which, divided
by 211.780 mn AWU, implied an average subsidy of $4,747 per AWU. In
other words the US subsidy per AWU was 8.3 times that of China and
that of the EU28 2.3 times. And the TSE-MPS per AWU was 11.1 times
that of India for the USA and 3.1 times higher that to India for the
EU28. 

<p>
We could argue that we should delete the US domestic food aid but in
that case we must do the same for India but here we have a problem of
inconsistency between the data of OECD and the notifications made to
the WTO for the US as for India: if the US domestic food aid was
notified at $94.618 bn in the WTO green box for 2018-19, the OECD
data show that the US non-commodity specific transfers to consumers
from taxpayers for domestic food aid were of $40.192 bn in 2019. And
if India has notified to the WTO $17.212 bn of Public stockholding
for food security purposes for 2019 in the green box (plus $5.004 bn
of market price support to rice in the AMS), the OECD data show that
the same non-commodity specific transfers to consumers from taxpayers
were of $16.932 bn in 2019 (but jumped to $58.148 bn in 2020). But
China notified only $1.149 bn of domestic food aid for 2016. 

<p>
If we exclude from the US TSE-MPS the $40.192 bn the subsidy per AWU
falls to $52.744 bn and the US (TSE-MPS)/AWU falls to $22,333 so that
the US agricultural subsidy was still 4.7 times larger than that of
China and 6.3 times that of India. In the same way if we exclude from
the Indian TSE-MPS its domestic food aid of $16.932 its (TSE-MPS)/AWU
falls to $2,730 so that the US agricultural subsidy was 8.2 times
that of India. On the other hand the EU28 domestic food aid is very
low because most EU members have large social security benefits (of
which minimal wages) and, furthermore, most EU direct payments are
not notified to the WTO as they are allegedly decoupled and non-trade
distorting. 

