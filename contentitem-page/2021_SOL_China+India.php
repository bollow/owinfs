<u><b>II – Social performance </b></u>

<p>
<span style="background: #ffffff">Table 2 rests
mainly on the UNDP (United Nations Development Programme)'s Human
Development Index&nbsp;(HDI) which is a statistic composite index
of&nbsp;</span><a href="https://en.wikipedia.org/wiki/Life_expectancy"><span style="text-decoration: none"><span style="background: #ffffff">life
expectancy</span></span></a><span style="background: #ffffff">,&nbsp;</span><a href="https://en.wikipedia.org/wiki/Education"><span style="text-decoration: none"><span style="background: #ffffff">education</span></span></a><span style="background: #ffffff">
and&nbsp;</span><a href="https://en.wikipedia.org/wiki/Per_capita_income"><span style="text-decoration: none"><span style="background: #ffffff">per
capita income</span></span></a><span style="background: #ffffff">&nbsp;indicators,
used to rank countries on&nbsp;</span><a href="https://en.wikipedia.org/wiki/Human_development_(humanity)" target="Life expectancy"><span style="text-decoration: none"><span style="background: #ffffff">human
development</span></span></a><span style="background: #ffffff">.&nbsp;The
Inequality-adjusted Human development index (IHDI) is the actual
level of human development accounting for&nbsp;income </span><a href="https://en.wikipedia.org/wiki/Social_inequality" target="Social inequality"><span style="text-decoration: none"><span style="background: #ffffff">inequality</span></span></a>.
The unweighted average HDI of the 6 developed countries of 0,926 in
2019 (which was also that of the US and of the three EU countries)
was 21.7% larger than that of China and 43.6% larger than that of
India. And the US IHDI was 26.4% higher than that of China and 70.1%
than that of India. In fact the US income distribution is more
unequal than those of China and India if we compare the income share
of the 1% richest and of the 40% poorest: in the US the 1% richest
gets 20.5% of national income while the 40% poorest get 15.4%, a gap
of 5.1% between the two. In China the 40% poorest get 17.2% of
national income and the 1% richest 13.9%, a gap of 3.3% between the
two. And, if in India the 1% richest gets 21.3% of national income
(more than in the US) against 18.8% for the 40% poorest, the gap
between the two is of only 2.5%. Apart from the US these indicators
are lower in the other 5 Western countries than in China and India.  
 

