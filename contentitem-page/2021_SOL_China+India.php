<u><b>Conclusion</b></u>
<p>
It is clear that on the five issues – levels of income and wages,
social performance, environment performance, trade performance and
agricultural support – China and India are entitled to claim a
status of developing country availing of a special and differential
treatment under WTO rules. 

<p>
Many other reasons plead for this status, which can be seen in the
following papers below.
<p>
However, this justification of China's and India's status of
developing economies should not be seen as an endorsement of many
other aspects of their policies both internally – particularly in
terms of undemocratic regimes and of the persecution of Muslims in
China with Uighurs and in India since Narendra Mody – and
externally, notably the development of Chinese economic imperialism
with the Silk Roads, and the risk of too large indebtedness of Africa
to China.
