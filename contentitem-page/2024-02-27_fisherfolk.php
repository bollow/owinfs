<p>
Today in Abu Dhabi at the World Trade Organization Ministerial Conference (MC13) fisherfolk
representatives have called on the global trading body to not trade away supports for small-scale
fishers.
<p>
Ministers are negotiating for prohibitions on subsidies that contribute to overfishing and
overcapacity based on the Sustainable Development Goal 14.6 but they are being contested as not
addressing the main culprits of overfishing.
<p>
Civil Society groups and fisherfolk representatives have traveled to Abu Dhabi to raise their
concerns directly to Ministers.
<p>
Speakers include:
<ul>
  <li>Olencio Simoes, National Fishworkers Forum, India
  <li>Fikerman Saragih, The People's Coalition for Fisheries Justice (KIARA), Indonesia
  <li>Maruf Barkat Ullah, COAST Foundation, Bangladesh
  <li>Adam Wolfenden, Pacific Network on Globalisation
</ul>
<p>
<b>Quotations form the press release:</b>
<p>
<b>Olencio Simoes</b> from the National Fishworkers Forum in India stated “In India we have more then
100 million fishers and approximately 61% are still living below poverty line. For India's small-
scale, artisanal and indigenous fishers, the WTO negotiations on fishers subsidies will directly
impact their livelihoods. While India has championed the cause of its small-scale fishers it must
secure a special and differential treatment exemption that extends beyond the current proposed limit
of 12 nautical miles. Given the inherently semi-formal nature of their work and the limitation of
advanced navigation equipment, small fishers operate beyond these proposed limits up 200 nautical
miles.
<p>
<b>Fikerman Saragih</b>, from The People's Coalition for Fisheries Justice (KIARA), Indonesia said “in
Indonesia there are 2.4 million fisherfolk and even more women involved in the sector. The
proposed subsidy prohibitions will risk breaking Indonesian laws and regulations, which clearly
state that small-scale fishers are entitled to subsidies from the Government including for fuel
subsidies, fisheries insurance, life insurance, fishing risk guarantees and realizing fish prices that
benefit fishers. This proposed agreement will harm fisherfolk”
<p>
<b>Maruf Barakat Ullah</b>, COAST Foundation, Bangladesh commented “the subsidies Bangladesh is
currently giving in fisheries are not for the capacity development of the fisherfolk for marine
fishing. Rather, it is given to the poor fishing families as a social safety net program to let them
survive during the three months of fishing ban in a year. The fishing ban is to resume the fish stock
in the sea. Thus, Bangladesh is subsidising the fish stock when the subsidy is discouraged, which is
said to affect the fish resource. Bangladesh has passed the threshold of 0.8% for marine fishing, so
it will not be allowed to subsidise marine fishing. This is so controversial.”
<p>
<b>Adam Wolfenden</b>, Pacific Network on Globalisation (PANG), added “The proposed text brought
to the Ministerial fails to target those large-scale fleets that are most responsible for the dire state of
global fish stocks. With those fleets being let through the net the worry is that small-scale fishers
and developing countries will bear the blame for the lack of an agreement.”
