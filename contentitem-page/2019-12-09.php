<p>(Geneva) Tomorrow, the WTO’s flagship dispute settlement mechanism (DSM) will cease to be functional, as the United States has blocked appointments to the Appellate Body (AB) and thus the outcomes of cases will no longer be able to be enforced.</p>
<p>“This is one more blatant example of the US attempting to assert an imperial role: if an entity does not do exactly what the US government thinks is in its interest, then the US will attempt to bypass, emasculate, or destroy that entity. Other states should recognize this for what it is, an abuse of power, and retaliate in any way they can, for example by announcing that they will no longer comply with TRIPS, will no longer support the moratorium on e-transmissions, and will join France and others in taxing the e-commerce giants,” said Richard Hill, President of the Association for Proper Internet Governance.</p>
<p>“We take note that most developing countries and LDCs have not been major users of the WTO’s dispute settlement mechanism. Indeed, 80 to 90 percent of all disputes involve either at least one high-income country or at least one upper-middle-income country. Many least developed countries (LDCs) have never participated in the dispute settlement system as a complainant or respondent. Nevertheless, the Appellate Body remains a critical part of the entire WTO rules-based system. Whether they actively use it or not, developing Members have the most to gain by a system where rules can be enforced” says Jane Nalunga, Country Director, SEATINI-Uganda.</p>
<p>“The real crisis in the WTO is the crisis of the tremendous harm to people around the world from the neoliberal economic order, encapsulated by the WTO. Now that the WTO’s enforcement is no longer operational, countries should restart using those policies which have been constrained by harmful WTO rules. For example, WTO rules constrain developing countries from supporting domestic farm production to feed their own people, but still allow rich countries to subsidize exported production. Now, food security can finally take precedence over WTO rules. Countries which have forked over millions of dollars to patent-holders can start not enforcing those development-starving patents - UNCTAD’s Trade and Development Report 2019 showed that patent receipts to OECD countries in 2017 to be upwards of $350 billion USD while developing countries received less than $10 billion. It’s time to stop enforcing TRIPS!” said Deborah James, facilitator of the global OWINFS network.  
</p>

<h4>Background Information</h4>

<p>At the time of the founding of the WTO, corporate elites hijacked "trade" and rigged the rules to distribute income upwards, while reducing protections for people who work. Highly paid professionals (like doctors) in rich countries are protected (by being able to regulate their own licensing) and businesses are given market access rights and predictability. Meanwhile workers are forced into unfair competition without a minimum floor for protections, and developing country workers have been kept at the lowest levels of the global value chains.</p>

<p>As rich countries have been allowed to maintain their level of agricultural subsidies (which are mostly handed out to large producers, not family farms), developing countries have not been allowed under WTO rules to subsidize food production for domestic consumption to guarantee food security, nor to protect their farmers from unfair dumping.</p>

<p>Unfair agriculture rules contribute to global food crises and the impoverishment of millions - there are still nearly a billion hungry people in the world - and keep developing countries from benefiting from fair trade. And yet the US is currently suing India in the WTO for implementing the largest food security programme in the history of the world.</p>

<p>In reality, the problem with the dispute system is that it adjudicates according to a set of rules guided by corporate interests. In the 45 cases in which members have tried to use public interest regulations as a defense in a case, commercial interests have won out 44 times.</p>

