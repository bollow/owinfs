<p>

<p>Septiembre
2021
<p>


<ol>
	<li>Los
	pueblos de todo el mundo necesitan una economía mundial que
	facilite una prosperidad compartida con derechos humanos, laborales,
	sociales y económicos para facilitar el logro, como mínimo, de la
	Agenda 2030 para el Desarrollo Sostenible, adoptada por los Estados
	miembro de la Organización de las Naciones Unidas en septiembre de
	2015 en la forma de Objetivos de Desarrollo Sostenible (ODS). El
	sistema multilateral de comercio debería contribuir a esta visión;
	pero en su lugar, se encuentra en crisis ya que no ha logrado
	brindar prosperidad compartida y ha pisoteado los derechos
	universales de los pueblos. Esto se debe a que las elites
	empresariales se han apoderado del “comercio” para amañar las
	normas del sistema multilateral con el fin de distribuir los
	ingresos hacia arriba y reducir las protecciones para las personas
	que trabajan. La promesa de que el crecimiento del comercio generará
	una situación en la que “todos ganan” ha demostrado ser una
	mentira y en su lugar, la mayoría de los pueblos viven el aumento
	del comercio como una carrera hacia al abismo en materia de
	salarios, precariedad, servicios públicos, medioambiente y las
	normas relativas al consumo. La crisis provocada por el daño real a
	los pueblos de todo el mundo como consecuencia del orden económico
	neoliberal representado por la OMC, y las políticas nacionales de
	austeridad, han provocado levantamientos en cuatro continentes,
	migraciones masivas y la elección de gobiernos populistas de
	derecha en muchos países. La inestabilidad social, que afecta
	particularmente a las mujeres, pueblos marginados, a la gente en
	situación de pobreza y los Pueblos Indígenas, es el resultado de
	estos fracasos, y asimismo puede decirse que la crisis de la OMC es
	de su propia responsabilidad. Los gobiernos deben encarar las fallas
	fundamentales del sistema actual de globalización dirigido por las
	grandes empresas, no extender ni consolidar los desequilibrios de
	poder. La solución ante los ataques actuales contra la versión
	empresarial de la política comercial no es un falso nacionalismo,
	sino una nueva visión de multilateralismo cuyo resultado sea la
	estabilidad ecológica y la prosperidad compartida. Un camino para
	lograr tal economía se describe en “Nuevo Multilateralismo
	-Prosperidad Compartida: Los Principios de Ginebra para un Nuevo
	Pacto Verde”.<sup><a class="sdfootnoteanc" name="sdfootnote1anc" href="#sdfootnote1sym"><sup>1</sup></a></sup>
	  
	
</ol>
<p>


<ol start="2">
	<li>La
	pandemia de COVID-19 ha puesto al desnudo cómo el modelo de la OMC
	exacerba la inseguridad, desigualdad e inestabilidad. Las cadenas de
	suministro profundamente integradas creadas por las normas de la OMC
	en el transcurso de las últimas décadas hoy están minando los
	esfuerzos de numerosos países en la lucha contra la pandemia
	mundial de COVID-19. En todo el mundo hay países que no pueden
	fabricar o adquirir mascarillas, dispositivos de pruebas,
	ventiladores, medicamentos, vacunas y otros equipos necesarios. Las
	normas de la OMC han priorizado durante décadas los intereses de
	las grandes empresas que exigen la concentración de la producción
	mundial a fin de maximizar sus ganancias y prohíben el uso de
	herramientas de política pública para garantizar que exista
	capacidad de producción a nivel local y diversidad de proveedores
	cuando sea necesario importar. Las normas de la OMC en materia de
	propiedad intelectual, diseñadas para maximizar las ganancias de
	las grandes empresas farmacéuticas en vez de la salud pública, han
	empujado los precios al alza y generado escasez de medicamentos
	esenciales para combatir la COVID-19 en muchos países y están
	provocando muertes y mayor incidencia de enfermedades en todo el
	mundo que podría haberse evitado.
</ol>
<p>


<ol start="3">
	<li>Si
	bien es esencial contar con un sistema multilateral que regule el
	comercio y controle el comportamiento de las grandes empresas, el
	sistema específico de normas representado por la OMC debe ser
	impugnado porque en sus 25 años de existencia no ha logrado
	propiciar el desarrollo, promover la prosperidad compartida ni
	garantizar la sustentabilidad del comercio. Las predicciones de un
	incremento compartido del crecimiento bajo el paraguas del sistema
	de la OMC han fracasado abismalmente, como varias de nuestras
	organizaciones vaticinaron al momento de su fundación, mientras que
	la desigualdad se ha disparado, empobreciendo a cientos de millones
	de personas. La mayoría de los países en desarrollo que se han
	beneficiado del comercio lo han conseguido exportando bienes a
	China, cuyo crecimiento se atribuye por lo general a sus diferencias
	con el modelo de la OMC, entre ellas su uso de políticas
	industriales. Si bien algunos países en desarrollo han incrementado
	su cuota de participación en el comercio mundial, la cuota parte de
	sus ingresos provenientes del comercio mundial se ha visto reducida
	por las empresas transnacionales (ETN) que se quedan con los
	segmentos de alto valor mientras que tercerizan las operaciones de
	bajo valor a los países en desarrollo que se ven atrapados en
	estrategias de desarrollo fundadas en la explotación laboral. Por
	eso, la participación ampliada de los países en desarrollo en los
	mercados mundiales no ha resultado en el desarrollo económico
	prometido en la mayoría de ellos.
</ol>
<p>


<ol start="4">
	<li>La
	OMC se fundó sobre la premisa de que el comercio debería servir
	como instrumento para mejorar las condiciones de vida y garantizar
	el pleno empleo. Sin embargo, a pesar de que la productividad se
	disparó desde la fundación de la OMC, los salarios de las
	trabajadoras/es y los ingresos de las personas que trabajan se han
	mantenido estables en el mejor de los casos o incluso han disminuido
	respecto del capital. Esto se debe a que las grandes empresas han
	usado el incremento de las ganancias obtenidas para invertirlas en
	controlar el proceso de elaboración de políticas, particularmente
	a través de la OMC así como de tratados comerciales bilaterales y
	regionales y acuerdos de inversiones, con el fin de garantizar que
	las políticas públicas configuren la economía de manera tal que
	garantice la concentración de los ingresos en sus manos. “Compensar
	a las/os que pierden” con las normas perjudiciales de los acuerdos
	comerciales no debería depender únicamente de políticas
	nacionales de redistribución. A las ETN se las debe obligar, en el
	marco del derecho nacional e internacional, a integrar la diligencia
	debida en materia de derechos humanos a lo largo de la cadena de
	suministro y garantizar que existan mecanismos de denuncia, acceso a
	reparaciones y la aplicación plena de los Principios Rectores de
	las Naciones Unidas sobre Empresas y Derechos Humanos (UNGP, por su
	sigla en inglés) y los Principios y derechos fundamentales en el
	trabajo, de la OIT como estándar mínimo. Las políticas de
	desarrollo que promueven buenos empleos e industrialización no
	deben verse limitadas por las normas del comercio mundiales.
	Necesitamos un nuevo contrato social que garantice la protección
	laboral como piso, que incluya empleo digno, servicios públicos
	universales de calidad y cobertura social, diligencia debida en
	materia de derechos humanos y rendición de cuentas por las
	empresas, diálogo social y una transición justa hacia una economía
	de carbono cero y nuevas políticas antimonopólicas a nivel
	mundial, todo lo cual no debe verse socavado por las normas
	mundiales del comercio. 
	
</ol>
<p>


<ol start="5">
	<li>Al
	mismo tiempo, el futuro de los seres humanos en el planeta está
	amenazado por nuestro propio y vasto consumo excesivo de carbono,
	que ha provocado un cambio climático catastrófico que está dando
	lugar actualmente a calamidades ambientales que causan pérdidas
	devastadoras de vidas humanas, fauna y flora. El comercio es uno de
	los factores principales de contribución al cambio climático, y
	las normas de la OMC que facilitan los sistemas de producción con
	altos niveles de carbono y al mismo tiempo limitan la transferencia
	de tecnologías verdes y los subsidios en pos de una economía de
	energía sustentable, ya no son sustentables.
</ol>
<p> 

<ol start="6">
	<li>Al
	mismo tiempo y del mismo modo que acumulan derechos a través de
	acuerdos ‘comerciales’ a costa de las personas que trabajan, las
	grandes empresas también lo hacen a expensas de los Estados
	democráticos. Esto ha llevado a la erosión de la democracia, ya
	que los Estados no logran cumplir con el contrato social. Las
	grandes empresas han influido en las normas del comercio en el marco
	de la OMC para permitir la facturación errónea del comercio que
	les ha permitido eludir o evadir impuestos que son necesarios para
	el funcionamiento de la sociedad. Esto, combinado la concentración
	de la riqueza a nivel más amplio facilitada por las normas del
	comercio ha generado concentraciones de poder inadmisibles que dan
	lugar a niveles peligrosos de desigualdad. Al exigir compromisos de
	desregulación y propiedad extranjera de los servicios, las normas
	del comercio también contribuyen a la privatización, que ha
	erosionado el suministro de servicios públicos y ha limitado el
	derecho de los Estados a regular a las ETN, lo cual acrecienta aún
	más el poder empresarial privado sobre todos los aspectos de la
	vida humana. La influencia excesiva de las grandes empresas y sus
	grupos de cabildeo también es cada vez más evidente en otras
	instituciones como las Naciones Unidas y particularmente en el ODS
	17 cuyo objetivo es facilitar una mayor influencia empresarial en la
	gobernanza mundial. 
	
</ol>
<p>


<ol start="7">
	<li>Es
	responsabilidad y obligación principalmente del Estado garantizar
	la materialización de los derechos humanos de sus ciudadanas/os. Su
	capacidad de hacerlo depende de su capacidad de adaptarse a desafíos
	futuros; ejercer prerrogativas en materia de políticas; establecer,
	mantener y ampliar los servicios públicos y reglamentar libremente
	al capital. El actual régimen de comercio no salvaguarda estas
	funciones esenciales del Estado y las subvierte a favor de la lógica
	y necesidades del comercio.   
	
</ol>
<p>


<ol start="8">
	<li>Las
	normas de la OMC sobre la agricultura limitan la capacidad de los
	países de proteger su propio desarrollo rural, los medios de
	sustento de las agricultoras/es y la seguridad y soberanía
	alimentaria en todo el mundo, y han contribuido a múltiples crisis
	alimentarias y erosionado la seguridad alimentaria neta en muchos
	países.
</ol>
<p>


<ol start="9">
	<li>La
	estabilidad y previsibilidad financiera y económica son esenciales
	para la prosperidad compartida. Sin embargo, la ciencia demuestra
	que aunque las normas actuales de la OMC pueden haber generado mayor
	previsibilidad para los inversionistas y las grandes empresas
	comerciales, las trabajadoras/es y comunidades enfrentan cada vez
	mayores niveles de precariedad laboral, tercerización, recortes de
	los servicios públicos y una carrera hacia el abismo en la cual se
	les deja a su suerte para enfrentarse a una desarticulación sin
	precedentes de la economía mundial, a menudo provocada por crisis
	financieras y cambios rápidos en los patrones comerciales, mientras
	que muy pocas de ellas y ellos gozan de protección social
	generalmente insuficiente.
</ol>
<p>


<ol start="10">
	<li>La
	OMC es única entre las organizaciones multilaterales en términos
	de su falta total de facilitación de la participación de la
	sociedad civil, sindicatos y comunidades de base que se ven
	negativamente afectadas por las políticas de la OMC, mientras que
	muchos representantes empresariales gozan de acceso cercano al
	proceso de elaboración de políticas, incluida la redacción de
	muchos textos de los acuerdos. Los Miembros de la OMC son cada vez
	más reservados respecto de los textos y las negociaciones, lo que
	imposibilita la rendición de cuentas y el debate público. La
	Secretaría se está involucrando activamente ahora en asociaciones
	con la comunidad empresarial mundial para aumentar la influencia de
	esta, mientras que no hace nada para remediar la exclusión de
	las/os representantes de la sociedad civil del proceso deliberativo
	de la OMC. Más indignante aún y escandaloso es que la Secretaría
	está abogando abierta y públicamente a favor de la ampliación de
	las disciplinas de la OMC en áreas como la economía digital, a
	beneficio de unos pocos miembros y las grandes empresas de
	tecnología con sede en esos países, a la vez que no cumple con su
	obligación de facilitar la conclusión de negociaciones para
	mejorar las normas perjudiciales vigentes de la OMC con el fin de
	permitir que el comercio promueva un desarrollo verdadero,
	sustentabilidad y prosperidad compartida, que hace mucho están
	bloqueadas. Finalmente, la verdadera crisis del Mecanismo de
	Solución de Diferencias (MSD) es que juzga causas usando normas que
	privilegian los derechos comerciales de las grandes empresas por
	encima del interés público: en 80 casos que implican políticas de
	interés público, la OMC falló 73 veces a favor del derecho de las
	empresas a comerciar.<sup><a class="sdfootnoteanc" name="sdfootnote2anc" href="#sdfootnote2sym"><sup>2</sup></a></sup>
	Además, los países poderosos han podido hacer respetar sus
	derechos en la OMC a través del MSD muchísimo más que los países
	pobres, un hecho pocas veces mencionado en los debates sobre el
	brazo judicial de la OMC. Es enormemente improcedente considerar la
	posibilidad de ampliar el alcance del modelo fallido de la OMC sobre
	la vida humana. 
	
</ol>
<p><b>Por
lo tanto, como miembros y aliados de la red mundial de la sociedad
civil Nuestro Mundo No Está En Venta (OWINFS, por su sigla en
inglés), comprometidas/os por un sistema multilateral de comercio
socialmente justo, sustentable, democrático y que rinda cuentas,
exigimos un nuevo sistema de normas multilaterales mundiales.</b>
<p>


<ol start="11">
	<li>Muchas
	de las organizaciones abajo firmantes vienen criticando desde hace
	tiempo las normas de la OMC por considerarlas perjudiciales e
	inadecuadas para la gobernanza mundial del comercio. A estas
	alturas, tras casi 25 años de existencia de la OMC, sus resultados
	demuestran que sus críticos tenían razón; no es el sistema
	correcto de gobernanza del comercio para lograr prosperidad
	compartida. Por lo tanto, debe concebirse un nuevo sistema de normas
	multilaterales de comercio que haga de la prosperidad compartida, la
	sustentabilidad ambiental y el logro de los ODS de la Agenda 2030 su
	preocupación central, por encima de los intereses comerciales de
	las grandes empresas poderosas. Dada la trayectoria de la OMC de no
	atender los intereses de los países en desarrollo; la consagración
	de los derechos de las ETN por encima de los derechos de las
	ciudadanas/os y trabajadoras/es y las obligaciones ambientales; y el
	papel sesgado de la Secretaría en promover intereses alineados con
	los miembros poderosos y las elites por encima de los intereses de
	los países en desarrollo y las trabajadoras/es, no hay pruebas que
	demuestren que la OMC en sí misma pueda lograr los cambios
	transformadores fundamentales en el sistema mundial de comercio
	necesarios en este momento. Por lo tanto, se requiere una nueva
	visión del multilateralismo para generar los empleos,
	infraestructura y servicios necesarios que son esenciales para
	lograr los ODS, y también para salvaguardar nuestro planeta para
	las generaciones futuras. Este nuevo multilateralismo tiene que
	incluir un nuevo conjunto de normas que rijan el comercio mundial.
</ol>
<p>


<ol start="12">
	<li>Entre
	tanto, mientras se concibe una institución fundamentalmente nueva,
	los gobiernos deben imponer <i>cambios
	transformadores a las normas vigentes</i>.
	Las normas de la OMC limitan demasiado a menudo la capacidad de los
	gobiernos de usar las políticas de desarrollo tradicionales -que
	usaron todos los países industrializados en su propio desarrollo-
	para promover el empleo y las industrias nacionales. A pesar de las
	afirmaciones hipócritas de los países desarrollados y las elites
	mundiales en la OMC, ellos han frenado la resolución del programa
	de desarrollo en la OMC por casi 20 años. Es imprescindible
	fortalecer las normas de Trato Especial y Diferenciado y ponerlas en
	funcionamiento para todos los países en desarrollo, haciendo
	hincapié en que se les libere de aplicar las normas vigentes de la
	OMC que son perjudiciales y limitan el uso de políticas de
	desarrollo basadas en pruebas. En lo inmediato, esto significaría
	que los gobiernos gocen del margen de maniobra en materia de
	políticas necesario para garantizar la seguridad alimentaria
	nacional; para utilizar el derecho a la transferencia de tecnología
	y al uso de sus datos; para aprovechar los requisitos de desempeño
	que generan empleo tales como los requisitos de contenido local y
	mano de obra local; y disponer de más flexibilidad para garantizar
	el acceso asequible a medicamentos, entre otras prioridades
	urgentes.
</ol>
<p>


<ol start="13">
	<li>También
	exigimos el <i>cese
	inmediato de las iniciativas de ampliación</i>
	del alcance y cobertura de las normas perjudiciales vigentes o su
	expansión a nuevos ámbitos. En particular, es imprescindible que
	nuestros gobiernos le pongan freno a la iniciativa de las grandes
	empresas de tecnología que quieren usar la OMC para asegurarse
	nuevos derechos favorables a las grandes empresas en la economía
	digital y obstaculizar un control regulatorio adecuado. Exigimos que
	se le ponga punto final a las iniciativas que pretenden:
	desreglamentar toda la economía digital a través de las mal
	llamadas negociaciones sobre “comercio electrónico”; imponer
	más disciplinas a las políticas de inversiones a través de las
	negociaciones sobre “facilitación de las inversiones”; coartar
	aún más la soberanía regulatoria a través de las negociaciones
	sobre “reglamentación nacional”; reiniciar las negociaciones
	sobre la liberalización de los servicios; y a las iniciativas que
	pretenden limitar las flexibilidades en materia de desarrollo en el
	contexto de las negociaciones sobre subsidios a la pesca. Los
	impulsores empresariales de estas negociaciones las están llevando
	a cabo como negociaciones “plurilaterales” que son ilegales en
	la OMC porque carecen de mandato, ya que la mayoría de los países
	en desarrollo se oponen a ellas, por buenas razones.
</ol>
<p>


<ol start="14">
	<li>Las
	normas actuales de la OMC dan derechos a comerciar y restringen las
	modalidades que puede adoptar la reglamentación del comercio. Pero
	los gobiernos no comercian, son las empresas las que lo hacen. Por
	eso los “derechos” en el marco de la OMC los ejercen las grandes
	empresas, mientras que las disciplinas en el marco de la OMC limitan
	la potestad de los gobiernos de reglamentar ese comercio en aras del
	bienestar general y el interés público. “Redactar las normas del
	comercio” es a menudo de hecho una denominación errónea que en
	realidad implica “restringir la regulación al mismo tiempo que se
	les conceden privilegios a las grandes empresas, empoderándolas”.
	Este es un enfoque errado de la “reglamentación” del comercio.
	En su lugar, necesitamos un sistema que promueva la prosperidad
	compartida salvaguardando los derechos humanos, laborales,
	económicos y sociales; que les conceda a los países el espacio de
	maniobra en materia de políticas necesario para aplicar estrategias
	demostradas de creación de empleos dignos e industrialización; y
	que les exija rendir cuentas a las grandes empresas y les imponga
	sanciones por todos los abusos a lo largo de sus cadenas de
	suministro y prácticas comerciales. Exigimos, como pieza de la
	nueva arquitectura multilateral, la adopción inmediata de un
	Tratado Vinculante sobre Empresas Transnacionales y Derechos
	Humanos, que se está negociando actualmente en el marco de la
	Organización de las Naciones Unidas.
</ol>
<p>Específicamente,
exigimos que los gobiernos transformen el sistema mundial de comercio
de la manera siguiente: 

<p><b>1.
Exigimos empleo digno y margen de maniobra en materia de políticas
para el desarrollo industrial </b>

<p>En
lugar de usar el comercio para aumentar los niveles de empleo y
prosperidad, las normas de la OMC se enfocan en reducir los aranceles
y la reglamentación y otras políticas que aumentan el poder de
negociación del capital a expensas de la fuerza de trabajo. Esto
obliga a las trabajadoras/es a incurrir en competencia desleal y
aceptar condiciones injustas, lo que lleva a la inexistencia de un
salario mínimo vital y protecciones laborales, mayor precariedad
laboral, explotación de las mujeres e inseguridad en los lugares de
trabajo. La libertad de movimiento creciente del capital y la
globalización liderada por la OMC desataron una carrera hacia el
abismo, donde los países y las zonas especiales de exportación
compiten en reprimir los derechos laborales con el fin de atraer
inversiones. Las cadenas de suministro mundiales que son el modelo de
producción dominante en la actualidad, están plagadas de
esclavitud, trabajo infantil y todas las formas de explotación
laboral que afectan desproporcionadamente a las mujeres trabajadoras.
Además, las grandes empresas globalizadas quieren imponer nuevas
normas que les garanticen más derechos a los inversionistas a
expensas de las trabajadoras/es, entes reguladores y el público en
general, a través de las negociaciones en pos de un acuerdo sobre
“facilitación de las inversiones”. En su lugar, necesitamos un
sistema mundial de comercio que haga hincapié en incrementar las
oportunidades de trabajo digno y las condiciones de vida de las
personas que trabajan.
<ul>
	<li>Cualquier
	acuerdo actual o futuro debe enfocarse en utilizar el comercio para
	promover y extender el trabajo digno con un nuevo contrato social y
	protección laboral como piso, tal como se exige en la Declaración
	del Centenario de la OIT, y proteger el margen de maniobra de los
	países en materia de políticas en pos de la prosperidad compartida
	a través del pleno empleo, mediante la transformación estructural
	y la industrialización, y a través de salarios más altos y
	diálogo social.
	<li>Deben
	cesar las negociaciones que buscan imponer más normas de
	inversiones en la OMC a través de la “facilitación de las
	inversiones”.
	<li>Además,
	exigimos que se lleven a cabo evaluaciones de impacto social –por
	ejemplo sobre desigualdad, la calidad del empleo y la creación de
	empleo— respecto de las políticas y negociaciones actuales de la
	OMC y cualquier cambio futuro, y la implementación de los cambios a
	las normas vigentes que son necesarios para garantizar un sistema
	multilateral de comercio que promueva el pleno empleo y el trabajo
	digno.
</ul>
<p><b>2.
Exigimos margen de maniobra en materia de políticas para la
industrialización digital, y potestad para usar los datos en aras
del bienestar general y el interés público</b>
<p>Actualmente,
las empresas más grandes quieren utilizar la OMC para reescribir las
normas con el fin de arraigar jurídicamente y perpetuar para siempre
sus prácticas monopólicas mediante el acceso a y el control del
recurso más valioso del mundo, los datos. Pero los datos no deben
ser controlados por grandes empresas privadas para maximizar sus
ganancias privadas. En su lugar, muchas personas y gobiernos están
empezando a percatarse de la necesidad de establecer y garantizar la
propiedad nacional y comunitaria de los datos y de la soberanía en
materia de datos. Todos los países y comunidades deben disponer del
derecho a usar sus datos, así como las tecnologías y la
digitalización en general, para fomentar la industrialización y
empleos dignos, y para el bienestar general y el interés público,
tal como mejorar el acceso a los servicios públicos de calidad.
Además, las grandes empresas quieren usar las negociaciones sobre el
comercio digital para consolidar su modelo empresarial perjudicial de
evasión de las reglamentaciones, explotación laboral, evasión de
impuestos (tanto de impuestos empresariales como impuestos al
comercio o aranceles) y falta total de rendición de cuentas a las
comunidades de las que obtienen sus ganancias. La gobernanza
adecuada, democrática y participativa de Internet y el derecho a la
privacidad, el derecho económico  de los pueblos a sus datos, y la
protección de los datos no deben estar supeditados a intereses
comerciales privados. Las negociaciones actuales en pos de un acuerdo
sobre el comercio digital van mucho más allá del comercio
electrónico y pondrían en riesgo nuestra privacidad y los derechos
económicos de los pueblos a sus datos; exacerbarían las
desigualdades ampliando incluso la brecha digital; promoverían la
evasión fiscal de las grandes empresas digitales; aniquilarían a
las micro, pequeñas y medianas empresas (MIPYME); consolidarían el
control monopólico de las empresas pioneras en el mercado;
redundarían en mayor inseguridad para nosotras/os al prohibir que
haya controles adecuados de los códigos fuente y los algoritmos; y
anularían para siempre el margen de maniobra en materia de políticas
para la industrialización digital. Los negociadores argumentan
supuestos beneficios para las mujeres emprendedoras y las MIPYME,
mientras que las negociaciones reales incluyen disposiciones que
serían sumamente perjudiciales para la amplia mayoría de las
mujeres y pequeñas empresas. Exigimos que:
<ul>
	<li>Cesen
	las negociaciones plurilaterales y multilaterales (así como
	bilaterales) en pos de un acuerdo vinculante sobre el comercio
	digital (“comercio electrónico”).
	<li>Cese
	la apropiación del “empoderamiento de las mujeres” y las MIPYME
	para promover dicho acuerdo sobre el comercio digital.
	<li>Los
	gobiernos acuerden fuera de la OMC una norma mundial de alta calidad
	para la protección de los datos personales, la propiedad nacional y
	comunitaria sobre sus datos, así como principios aplicables para el
	uso de los datos, la inteligencia artificial y el aprendizaje
	automático que sean éticos y promuevan la igualdad y la justicia
	social. 
	
	<li>Cesen
	las iniciativas en pos de aplicar nuevas obligaciones a los miembros
	conminándoles a aceptar el equivalente de nuevos compromisos en
	materia de servicios a través de la “neutralidad tecnológica”
	referida a las nuevas tecnologías.
	<li>Se
	deben acordar y aplicar nuevas políticas a favor de la competencia
	en un ámbito por fuera de la OMC.
	<li>Se
	deben modificar las normas actuales, incluso en la OMC, que
	facilitan la evasión fiscal, la elusión fiscal y otros flujos
	financieros ilícitos, particularmente de las grandes empresas
	digitales, para garantizar así que los países cuenten con una base
	fiscal adecuada para financiar el logro de los ODS.
	<li>Además,
	deben eliminarse cualesquier restricciones actuales que imponga la
	OMC al margen de maniobra en materia de políticas para la
	industrialización digital, el uso de datos para el bien común e
	interés público, y el logro de los ODS relativos al acceso a la
	tecnología de información y comunicaciones, incluida la
	diversificación industrial y la agregación de valor -tales como
	las limitaciones a los requisitos de desempeño como la
	transferencia de tecnología, la contratación de trabajadoras/es y
	administradoras/es locales, y el uso de insumos locales.
</ul>
<p><b>3.
Exigimos el derecho a proteger el margen de maniobra en materia de
políticas para el desarrollo</b>
<p>Con
arreglo a un conjunto adecuado de normas, que pueden diferir y variar
según el nivel de desarrollo de cada país, el comercio puede
contribuir a la prosperidad compartida. El comercio no es <i>en
sí mismo</i>
la meta. Si el resultado real del comercio en función de las normas
actuales o propuestas obstaculiza la capacidad de los países para
desarrollarse y de las trabajadoras/es para prosperar en base a su
trabajo, entonces se necesitan nuevas normas. Los países en
desarrollo han vivido los impactos perjudiciales de las normas de la
OMC desde que empezaron a aplicarlas y han planteado infinidad de
propuestas para poder acceder a los beneficios del comercio y a la
vez mantener el margen de maniobra en materia de políticas necesario
para el desarrollo. Estos “asuntos de implementación”, ahora
incluidos en el paquete de propuestas del G90 sobre el Trato Especial
y Diferenciado, se diseñaron para resolver algunas de las asimetrías
y sesgos de la Ronda Uruguay. En su lugar, algunos países
desarrollados están atacando las pocas flexibilidades disponibles
para los países en desarrollo a la aplicación de las perjudiciales
normas vigentes de la OMC, mediante sus llamados a favor de una
“diferenciación” de los países con grandes volúmenes de
población que les obligaría a adoptar disciplinas incluso más
onerosas que hipotecarían su capacidad de desarrollar sus economías.
Según el estudio más reciente de la UNCTAD,<sup><a class="sdfootnoteanc" name="sdfootnote3anc" href="#sdfootnote3sym"><sup>3</sup></a></sup>
esta “diferenciación” es completamente inadecuada y no tiene
sustento en la realidad económica. Peor aún, algunos países
proponen cargas incluso más onerosas a los países en desarrollo
miembros bajo la bandera de la “transparencia y la notificación”.
Al mismo tiempo, en las negociaciones para imponer disciplinas a los
subsidios en el sector pesquero, algunos países están presionando
para restringir las disposiciones de Trato Especial y Diferenciado
(que forman parte del mandato del ODS 14.6), impidiendo así que los
países en desarrollo se beneficien de sus recursos pesqueros y
garanticen la seguridad de los medios de sustento de las
pescadoras/es de pequeña escala, permitiendo a la vez que las
grandes flotas pesqueras comerciales, que son las mayores
responsables de la sobrepesca, obtengan ventajas competitivas por ser
las primeras en el mercado. Además, las preocupaciones e intereses
comerciales de los Países Menos Adelantados (PMA) no deberían ser
víctima de las demandas extremas de las ETN por mayor acceso al
mercado de los países en desarrollo con grandes volúmenes de
población. Por lo tanto:
<ul>
	<li>Es
	imprescindible que la OMC se re-enfoque en un paquete de propuestas
	robusto para garantizar el margen de maniobra en materia de
	políticas para el desarrollo –conocido como el programa de Trato
	Especial y Diferenciado— como agenda principal de la OMC, y que se
	lo apruebe de inmediato. 
	
	<li>Las
	iniciativas de “diferenciación” de los países en desarrollo
	para evitar que accedan a derechos y flexibilidades en la OMC deben
	abandonarse.
	<li>La
	ampliación de compromisos vinculantes para reducir aranceles a
	través de las negociaciones sobre acceso al mercado de productos no
	agrícolas (NAMA, por sus siglas en inglés), el acuerdo sobre
	bienes ambientales (EGA), y las presiones para que los países se
	sumen al acuerdo ampliado sobre Tecnología de la Información
	(ITA-II) deben abandonarse.
	<li>Ningún
	acuerdo para limitar los subsidios en el sector pesquero debe
	socavar los derechos soberanos ya establecidos en la Convención de
	la ONU sobre el Derecho del Mar. Todo resultado debe incluir un
	fuerte componente de disposiciones sobre Trato Especial y
	Diferenciado para garantizar que los países en desarrollo puedan
	proponerse el desarrollo sustentable de sus sectores pesqueros, al
	mismo tiempo que los subsidios perjudiciales a las grandes flotas
	industriales tienen que disciplinarse.
	<li>Debe
	implementarse inmediatamente el paquete para los PMA, incluido el
	acceso libre de gravámenes y libre de contingentes para los PMA,
	una resolución justa e inmediata a los reclamos de los países
	productores de algodón, y una exención en materia de servicios
	para los PMA.
	<li>Las
	propuestas para aumentar las cargas administrativas de los países
	en desarrollo bajo la rúbrica de “transparencia y notificación”
	son totalmente inadecuadas y deben sustituirse por cambios para
	hacer que la institución funcione de manera más transparente y
	democrática y que respete las limitaciones de capacidad de los
	países miembro, incluso evitando organizar reuniones simultáneas;
	garantizando que se documenten adecuadamente las reuniones y que
	siempre se brinden servicios de interpretación.  
	
	<li>Si
	los PMA buscan su adhesión a la OMC, su ingreso no debe estar
	condicionado por reclamos de acceso al mercado, sino que deben poder
	adherir con arreglo a términos que les permitan usar el comercio en
	función de sus necesidades de desarrollo. Dichos términos no deben
	ir más allá de aquellos aplicados a los miembros de la OMC que
	fueron originalmente PMA. Los países que se “gradúan” de su
	condición de PMA deben disponer de flexibilidades para adoptar las
	nuevas normas según modalidades que sean compatibles con su
	trayectoria de desarrollo.
	<li>Debe
	llevarse a cabo una evaluación exhaustiva de las restricciones
	vigentes a la potestad de los países de proponerse  políticas
	nacionales y usar el comercio para el desarrollo, para que las
	normas de la OMC sobre propiedad intelectual, agricultura,
	inversiones, servicios y bienes puedan transformarse así con
	arreglo a políticas de desarrollo de transformación estructural.
</ul>
<p><b>4.
Exigimos acceso a la salud y medicamentos asequibles, no patentes
monopólicas</b>
<p>Los
titulares de patentes monopólicas son la clase más protegida dentro
de las normas de la OMC a través del acuerdo sobre los Aspectos de
los Derechos de Propiedad Intelectual relacionados con el Comercio
(ADPIC). El acuerdo sobre los ADPIC ha conllevado una transferencia
masiva de riqueza de los pacientes y los presupuestos de salud de los
Estados hacia las grandes empresas, y del Sur Global hacia las
grandes empresas farmacéuticas que tienen sede  casi exclusivamente
en el Norte Global. Como lo que buscan las grandes empresas es la
extensión de sus patentes en lugar de innovar en medicamentos
necesarios, se ha dado lugar a una crisis de innovación. Derogar el
acuerdo sobre los ADPIC salvaría millones de vidas en todo el mundo
al facilitarse así el acceso a medicamentos esenciales para los
pueblos de todos los países. Los promotores del acceso a la salud y
medicamentos asequibles obtuvieron una importante victoria en 2001
con la adopción de la Declaración de Doha sobre Salud Pública y
los ADPIC. Esta declaración reafirmó las flexibilidades contenidas
en el acuerdo sobre los ADPIC y su uso para encarar las necesidades
de salud pública de los Estados miembro de la OMC. Sin embargo, las
negociaciones que tuvieron lugar tras la Declaración y que tendrían
que haber facilitado la exportación de medicamentos a los países
bajo la figura jurídica de licencias obligatorias concluyeron sin
establecer un mecanismo viable para traducir los derechos acordados
en acceso real. La salud pública ha sido víctima de los complejos y
estrictos monopolios de patentes a los que se les ha dado prioridad
en lugar que a garantizar el acceso a los medicamentos. La falta de
acuerdo inmediata en torno a la exención de los ADPIC durante la
pandemia de COVID-19, tal y como se propuso en octubre de 2020, ha
dado lugar a millones de muertes y personas enfermas
innecesariamente. Por lo tanto, en el plazo inmediato:
<ul>
	<li>Los
	Estados miembro deben revisar la decisión del 30 de agosto de 2003
	sobre los ADPIC y la decisión posterior de modificar los ADPIC en
	2005 y mejorar estas decisiones para que sean funcionales y viables.
	<li>Los
	miembros deben acordar de inmediato la exención a los ADPIC, tal y
	como la han propuesto más de 60 países, a fin de salvar vidas
	durante la pandemia mundial de COVID-19.
	<li>Los
	miembros deben acordar una moratoria <i>permanente</i>
	a las quejas de no violación de los ADPIC.
	<li>Los
	miembros deben volver permanente la exención de las obligaciones de
	los ADPIC para los PMA, <i>mientras
	sigan siendo PMA</i>,
	sin ninguna condición, inclusive permitirles a los miembros que son
	PMA revertir la implementación actual de los ADPIC.
	<li>Además,
	los miembros deben llevar a cabo una evaluación del impacto del
	Acuerdo sobre los ADPIC en la innovación y acceso a los
	medicamentos a nivel mundial, y deben luego cambiar fundamentalmente
	la regulación de medicamentos sobre la base del derecho a la salud
	y la promoción de la investigación y desarrollo y el acceso a
	vacunas y medicamentos.
</ul>
<p><b>5.
Exigimos normas del comercio que respalden la seguridad alimentaria y
la soberanía alimentaria</b>
<p>El
hecho que haya aún casi mil millones de personas con hambre a nivel
mundial, la concentración empresarial creciente de las industrias
agrícolas y las múltiples crisis mundiales de alimentación, han
puesto al descubierto nuestro sistema fallido de comercio de
alimentos. Los agricultores pobres se ven expuestos a avalanchas de
importaciones cuando los precios son demasiado bajos (a menudo a
causa del injusto <i>dumping</i>
de exportaciones provenientes de países ricos) a la vez que las
filas de seres humanos que padecen hambre se engrosan cuando los
precios de los alimentos se disparan. Esta volatilidad es resultado
de la especulación excesiva en los mercados de productos básicos
estandarizados (<i>commodities</i>)
y las normas alimentarias mundiales del Acuerdo sobre la Agricultura
–cuya redacción responde en gran medida a los intereses del
agronegocio empresarial corporativo- que trata a los alimentos como
una fuente de ganancias empresariales en lugar que como un derecho
humano. Además, las normas vigentes limitan a los países en
desarrollo a los peldaños más bajos de las cadenas mundiales de
valor en la agricultura. El ODS 2 - “Poner fin al hambre, lograr la
seguridad alimentaria y la mejora de la nutrición y promover la
agricultura sostenible” es un componente clave para lograr el
desarrollo sustentable en todos los países, especialmente los países
en desarrollo y los menos adelantados. Las metas del objetivo 2 se
refieren, entre otras cosas, a garantizar el acceso a los alimentos
para todos los grupos, especialmente para quienes están en situación
de pobreza y marginados, a mejorar la nutrición, duplicar la
productividad de los agricultores a pequeña escala, a mantener la
biodiversidad, y al uso de los recursos genéticos. Es hora de una
transformación fundamental del sistema alimentario. Los siguientes
cambios a las normas del comercio mundial son esenciales para
permitirles a los países garantizar su soberanía alimentaria y su
seguridad alimentaria y promover los medios de sustento rurales:
<ul>
	<li>Todos
	los países deben tener derecho a subsidiar alimentos, proteger los
	medios de sustento de los agricultores y promover el desarrollo
	rural, siempre que sus subsidios y otras políticas no perjudiquen a
	los agricultores de otros países.
	<li>El
	derecho a subsidiar alimentos para quienes están en situación de
	pobreza, incluso a través de pagos directos a los agricultores
	pobres, como se propone en las negociaciones actuales (por el grupo
	G33 de 47 países) debe aprobarse inmediatamente como una medida
	esencial para promover la soberanía alimentaria y garantizar así
	la seguridad alimentaria.
	<li>Los
	países en desarrollo deben tener derecho a usar instrumentos
	comerciales como los aranceles y usar otras medidas para proteger
	los medios de sustento de sus agricultores, el desarrollo rural y la
	seguridad alimentaria, especialmente cuando enfrentan avalanchas de
	importaciones de productos subsidiados. Esta potestad, denominada el
	Mecanismo de Salvaguardas Especiales (MSE) en la OMC, debe
	acordarse, pero con un formato mucho más flexible que lo que
	reflejan las propuestas actuales en la OMC, para que sea útil para
	lograr la seguridad alimentaria.
	<li>No
	debe demandársele ningún compromiso adicional en materia de
	agricultura a los PMA.
	<li>Los
	países en desarrollo deben poder eximir de cualquier posible
	recorte arancelario futuro a cualquier producto que consideren
	necesario para proteger los ingresos de los agricultores, la
	seguridad alimentaria y el desarrollo rural; y la designación de
	Productos Especiales en la OMC debe ampliarse.
	<li>Los
	subsidios a productos que se exportan deben disciplinarse, incluidos
	los subsidios de la llamada “Caja Verde” y los beneficios de la
	Medida Global de la Ayuda (AMS, por su sigla en inglés).
	<li>Las
	normas del comercio deben modificarse para facilitar la regulación
	adecuada de las <i>commodities</i>
	con el fin de prevenir la especulación excesiva y la volatilidad en
	los mercados mundiales.
	<li>Además,
	los miembros deben llevar a cabo una evaluación del impacto del
	Acuerdo sobre la Agricultura en términos de la seguridad
	alimentaria, los medios de sustento de los agricultores, el desvío
	de tierras para la producción de agrocombustibles, y el desarrollo
	rural, y luego rediseñar completamente las normas del comercio
	agrícola mundial para permitirles a los países ejercer su
	soberanía alimentaria.
</ul>
<p><b>6.
Exigimos la protección de la biodiversidad y que se prohíba el
patentamiento de la vida</b>
<p>Uno
de los resultados más importantes de la Declaración de Doha fue
examinar la relación del acuerdo sobre los ADPIC y el Convenio de la
ONU sobre la Diversidad Biológica (CDB). En el transcurso de diez
años de deliberaciones, algunos Miembros han presentado propuestas
concretas para modificar el acuerdo sobre los ADPIC para evitar la
biopiratería, pero otros han bloqueado su aprobación. Exigimos que:
<ul>
	<li>Los
	miembros modifiquen el acuerdo sobre los ADPIC para garantizar su
	coherencia con los ODS 2.5, 2. A, 15.6, 15.9, 15.a en el sentido de
	exigir que se revele el país de origen, y pruebas del
	consentimiento previo, libre e informado y de la distribución justa
	y equitativa de los beneficios, que sean al menos tan fuertes como
	lo acordado en el CDB / Protocolo de Nagoya y el Tratado
	Internacional sobre los Recursos Fitogenéticos para la Alimentación
	y la Agricultura (TIRFAA). Esta modificación también debe abarcar
	la información de secuencias digitales (ISD) tal como exigen muchos
	países en desarrollo ricos en biodiversidad.
	<li>Debe
	completarse la revisión obligatoria de las obligaciones en materia
	del patentamiento de la vida en el marco del Artículo 27.3 (b) del
	Acuerdo sobre los ADPIC. Reiteramos la necesidad de prohibir el
	patentamiento de todas las formas de vida como han propuesto muchos
	países en desarrollo.
</ul>
<p><b>7.
Exigimos estabilidad financiera en lugar de desreglamentación
financiera.</b>
<p>Las
normas de la OMC de desreglamentación y liberalización del sector
de los servicios financieros abonaron el terreno para la debacle de
la crisis financiera mundial. Sin embargo, mientras que el G20, la
mayoría de los gobiernos e incluso el Fondo Monetario Internacional
(FMI) han reconocido la necesidad de volver a reglamentar en materia
financiera a la luz de las crisis financieras, las normas del Acuerdo
General sobre el Comercio de Servicios (AGCS) de la OMC pueden de
hecho obstaculizar las iniciativas de reglamentación financiera.  Y
las propuestas actuales en las negociaciones del AGCS en realidad
ampliarían normas de acceso al mercado que establecen aún más
restricciones a la reglamentación nacional de los servicios
financieros. La digitalización de las finanzas y las monedas
digitales representan nuevos riesgos específicos que no se están
entendiendo adecuadamente y frente a los cuales las normas del
comercio -inclusive en el marco de las nuevas propuestas de comercio
digital- no harán más que restringir la necesaria fiscalización
reguladora. Se requieren cambios inmediatos a las normas vigentes del
AGCS relativas a los servicios financieros y no una mayor
desreglamentación o liberalización de los servicios financieros por
fuera o dentro de la OMC. Específicamente:
<ul>
	<li>Los
	países no deben asumir ningún compromiso adicional de
	liberalización del comercio de servicios financieros dentro de las
	negociaciones del AGCS o a través de ningún otro acuerdo
	comercial.
	<li>Los
	países deben preservar en cualquier disposición o acuerdo
	comercial la potestad de exigir el archivo local de los datos
	financieros a efectos de una reglamentación financiera eficaz, y de
	examinar los activos con fines fiscales y para evitar prácticas
	fraudulentas o depredadoras.
	<li>Además,
	deben revisarse y evaluarse las normas vigentes del AGCS a la luz de
	la crisis financiera y luego modificarse para garantizar el margen
	de maniobra en materia de políticas para que todos los países
	puedan aplicar medidas macroprudenciales, tales como controles de
	capital e impuestos a las transacciones financieras, así como
	implementar otras medidas de reglamentación financiera.
</ul>
<p><b>8.
Exigimos que las normas del comercio no limiten el papel del Estado
democrático como proveedor de servicios públicos universales de
calidad ni restrinjan la reglamentación de la economía en aras del
bienestar general y el interés público </b>

<p>Las
normas vigentes del AGCS conceden derechos a las grandes empresas
para operar en mercados con condiciones favorables para ellas y
restringen a la vez la fiscalización pública regulatoria, y no
deben ampliarse. Las grandes empresas tienen demasiada influencia en
la elaboración de políticas, inclusive en países extranjeros. A
pesar de la necesidad de revertir el control empresarial de la
elaboración de políticas de interés público, algunos países
están proponiendo nuevas disciplinas en las negociaciones sobre
“Reglamentación nacional”. Por lo tanto, exigimos que:
<ul>
	<li>Las
	iniciativas en pos de nuevas disciplinas en materia de
	Reglamentación nacional se dejen sin efecto.
	<li>Los
	servicios públicos existentes y especialmente los futuros deben
	excluirse de cualquier acuerdo actual o futuro que incluya
	servicios, mediante una excepción amplia y auténtica. Esto debe
	incluir servicios que quizás sea necesario remunicipalizar en el
	futuro.
	<li>Además,
	los Estados miembro y la OMC deben llevar a cabo una evaluación
	fundamental de las implicancias del AGCS respecto de las facultades
	para garantizar un control regulatorio adecuado de los servicios y
	garantizar el acceso a servicios públicos de calidad. Deben
	eliminarse cualesquier restricciones al logro de los ODS
	relacionados con la buena salud y el bienestar, la educación
	inclusiva y equitativa de calidad, el agua potable y saneamiento,
	las energías limpias y asequibles y otros servicios, sin recurso de
	impugnación según el libro de normas de la OMC.
</ul>
<p><b>9.
La OMC no es el lugar para establecer políticas sobre el cambio
climático</b>
<p>La
expansión del comercio es una causa importante del cambio climático
que provoca crisis ambientales, económicas y sociales a nivel
mundial. Sin embargo, incluir el cambio climático en la agenda de la
OMC representaría una grave amenaza para la estabilización del
clima, ya que las normas de la OMC favorecen estructuralmente el
aumento de los flujos comerciales que aportan una enorme cantidad de
los gases de efecto invernadero que provocan el cambio climático,
por sobre la sustentabilidad ambiental. Idealmente, el Acuerdo de
París debería sentar las bases para el respeto de las normas y
estándares ambientales y las metas climáticas en la esfera de la
producción y el comercio. Sin embargo, las normas de la OMC limitan
actualmente a los países a la hora de ecologizar sus industrias y,
por consiguiente, a la hora de promover una &quot;transición justa&quot;
–un enfoque de la transición a tecnologías respetuosas con el
clima centrado en el trabajo digno. Varios aspectos de las normas
vigentes de la OMC deben modificarse para concederles un mayor margen
de maniobra a los países en materia de políticas para que puedan
proteger el medioambiente y proponerse políticas de desarrollo
sustentable. Hay otros organismos multilaterales mucho más adecuados
que la OMC para discutir e implementar normas relacionadas con el
clima mundial.
<ul>
	<li>Las
	normas de los ADPIC deben modificarse para permitirles a los países
	flexibilidades adicionales al régimen de propiedad intelectual en
	materia de transferencia de tecnología, incluso en otros ámbitos
	multilaterales.
	<li>Las
	políticas relativas al cambio climático, incluidas aquellas que
	exigen hacer hincapié en la creación de empleo a la hora de
	adoptar tecnologías de energía limpia, no deben ser impugnables a
	través del mecanismo de solución de diferencias de la OMC.
</ul>
<p><b>10.
La OMC, incluida su Secretaría, debe abandonar el secretismo y su
funcionamiento reservado y antidemocrático</b>
<p>La
OMC no es apta para gobernar el comercio mundial. Mientras se
establece un nuevo sistema, también se requieren cambios inmediatos
referidos al funcionamiento de la OMC, entre ellos: 

<ul>
	<li>La
	OMC, incluidas las negociaciones y otras reuniones, debe abrirse a y
	facilitarle a la sociedad civil participación adecuada.
	<li>Los
	documentos y textos de la OMC deben ser públicamente disponibles. 
	
	<li>Las
	“colaboraciones” actuales entre el sector empresarial y la OMC
	deben cesar. 
	
	<li>No
	se debe ampliar el papel de la OMC más allá de su mandato actual. 
	
	<li>La
	Secretaría no debe actuar a favor de los reclamos de determinados
	miembros y excluir los reclamos de otros miembros en las
	negociaciones. 
	
	<li>Se
	debe llevar a cabo una revisión de las decisiones del MSD con miras
	a suprimir aquellas que anteponen las preocupaciones comerciales al
	interés público.
</ul>
<p>Hay
muchos más aspectos del sistema mundial de comercio que se deben
cambiar, incluida una revisión fundamental y la anulación o
transformación de los acuerdos bilaterales y regionales de comercio
e inversiones. Lo antedicho representa el nivel mínimo de cambios
inmediatos que a os que se debe someter el sistema multilateral de
comercio para conceder más margen de maniobra en materia de
políticas para que todos los países puedan implementar soluciones y
promover la prosperidad compartida para beneficio de todas/os.
