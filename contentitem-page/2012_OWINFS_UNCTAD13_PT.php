<p>

<p>
<b>Rumo a um desenvolvimento
sustentável e inclusivo,  não a mais crises.</b>
<p>
Domingo, 22 de abril de 2012.
<p>
	Aos Governos e Negociadores presentes na XIII UNCTAD em Doha, Catar.
<p>
	Desde a chegada da crise econômica e financeira global, a UNCTAD
desempenhou um papel importante em identificar as principais causas
da crise, apoiando os países em desenvolvimento na busca de soluções
para os impactos da crise e defendendo a reforma das políticas
econômicas e financeiras globais e da governança para prevenir a
recorrência de crises semelhantes. Estes são todos papéis
fundamentais que, na perspectiva do desenvolvimento, não são
cumpridos por nenhuma outra instituição econômica multilateral. De
fato, a UNCTAD é conhecida por ter previsto a crise com antecedência
e isso deve ser elogiado, particularmente dada a escassez de seus
recursos se comparada a instituições que falharam em fazer essa
previsão, como o Fundo Monetário Internacional (FMI), a Organização
Mundial do Comércio (OMC) e a Organização para a Cooperação e
Desenvolvimento Econômico (OCDE). Essa presciência se apoia na
longa história das contribuições da UNCTAD a políticas orientadas
para o desenvolvimento, tais como o Sistema Preferências
Generalizadas, os objetivos de ajuda ao desenvolvimento de 0,7%, a
anulação de dívidas, acordos internacionais sobre commodities, o
tratamento especial e diferenciado na OMC, o espaço para políticas,
entre muitas outras.
<p>
	Apesar dessas importantes contribuições, ao
longo das negociações que conduziram à XIII UNCTAD, os países
desenvolvidos tentaram rescindir o importante mandato da UNCTAD para
tratar de questões de políticas financeiras e macroeconômicas
globais, e particularmente seu mandato para <i>participar
da governança global</i> sobre estes
temas, tão essenciais à prosperidade global. Além disso, a UE e o
grupo “JUSSCKANNZ” (Japão, Estados Unidos, Suíça, Canadá,
Coreia do Sul, Austrália, Noruega, Nova Zelândia e Liechtenstein)
buscaram impor um mandato sobre a UNCTAD para forçar os países em
desenvolvimento a adotar políticas de proteção aos investidores e
políticas comerciais correspondentes aos interesses corporativos dos
países desenvolvidos, em vez de condizentes com os interesses de uso
exitoso do comércio e dos investimentos para fins de crescimento
sustentável e inclusivo internos aos próprios países em
desenvolvimento. 

<p>
Os resultados da conferência da XIII UNCTAD em Doha, Catar, de 21 a
26 de abril de 2012, devem contribuir para as transformações da
economia global necessárias para um desenvolvimento sustentável e
inclusivo para todos:
<p>


<ol>
	<li>É
	essencial que a declaração da UNCTAD 2012 não recue, e sim
	reafirme o progresso da XII UNCTAD em Acra. Isso inclui um acordo
	sobre a necessidade de crescimento sustentável e também inclusivo,
	assim como sobre a necessidade de que a UNCTAD trabalhe sobre as
	questões inter-relacionadas das finanças, tecnologia,
	investimentos e desenvolvimento sustentável, entre outros temas.
</ol>
<p>


<ol start="2">
	<li>A
	análise coletiva das políticas precisa reconhecer as causas
	profundas da crise global, seus impactos, e definir para UNCTAD um
	mandato de continuidade de suas excelentes pesquisas econômicas e
	financeiras e de análise crítica, para verdadeiramente assistir os
	países em desenvolvimento na criação de soluções para a crise –
	em vez de forçá-los a adotar mais das mesmas políticas de
	desregulação do comércio e dos investimentos que conduziram à
	origem da crise global.
</ol>
<p>


<ol start="3">
	<li>Finalmente,
	o papel da UNCTAD como voz alternativa ao paradigma do “Consenso
	de Washington” - sendo a única instituição econômica
	multilateral centrada no desenvolvimento – deve ser reforçado
	frente a OMC, o FMI, o Banco Mundial, a OCDE e o G20 na tomada de
	decisões da governança econômica global.
</ol>
<p>
	Para cumprir essas transformações, instamos os países
desenvolvidos a abandonar a pressão sobre os negociadores do G77, e
em vez disso trabalhar para garantir à UNCTAD um mandato de
perspectiva progressista, em conjunto com os países em
desenvolvimento, que deve, entre outras questões: 

<p>


<ul>
	<li>Identificar
	especificamente as origens, o alcance e os impactos da crise e
	determinar um papel à UNCTAD que inclua a assistência aos países
	em desenvolvimento bem como a promoção do crescimento sustentável
	e inclusivo na arena da governança global. 
	
	<li>Afirmar
	o papel fundamental das pesquisas e análises da UNCTAD sobre temas
	macroeconômicos e financeiros, incluindo as taxas de câmbio e os
	desequilíbrios globais, bem como as políticas fiscais
	contracíclicas que ajudaram a estabilizar as economias durante a
	crise global. 
	
	<li>Incluir
	análises que reconheçam tanto os custos quanto as oportunidades do
	comércio e que orientem a UCTAD na assistência a países em
	desenvolvimento na utilização do comércio para seu
	desenvolvimento, em vez de aconselhá-los a integrar-se à OMC e
	outros acordos de “livre” comércio. 
	
	<li>Conferir
	à UNCTAD o mandato de determinar as linhas de um marco global de
	comércio verdadeiramente orientado para o desenvolvimento, e,
	portanto, identificar as mudanças na OMC e nas negociações em
	curso necessárias na garantia de que os governos tenham espaço
	para políticas de comércio voltadas ao desenvolvimento sustentável
	e inclusivo e para legislar em favor do interesse público.
	<li>Afirmar
	a importância de regulamentação adequada e supervisão dos
	mercados financeiros, particularmente no que diz respeito à
	previsão e resolução de crises, e dar à UNCTAD o mandato de
	exercer um papel ativo na garantia de regras regulatórias nacionais
	e globais fortes.
	<li>Reconhecer
	os problemas das precauções de proteção aos investidores nos
	acordos de comércio e investimentos e dar à UNCTAD o mandato de
	ajudar os países em desenvolvimento a desenhar políticas de
	investimento que beneficiem seu crescimento sustentável e
	inclusivo, bem como de defender as melhores práticas orientadas
	para o desenvolvimento nas políticas globais de investimentos.
	<li>Reconhecer
	os fortes impactos da crise sobre o emprego, e dar à UNCTAD o
	mandato de trabalhar com os países em desenvolvimento em nível
	nacional em favor da criação de empregos, e no nível
	internacional em favor de uma Agenda Trabalho Decente concertada com
	a OIT.
	<li>Reafirmar
	a necessidade de soluções para o problema da volatilidade dos
	mercados globais de commodities e a necessidade de comércio justo
	no comércio agrícola, no sentido da Segurança Alimentar e da
	Soberania Alimentar, e conferir à UNCTAD um papel de pesquisa e
	incidência sobre esses temas, em conjunto com a FAO e
	particularmente com o Comitê de Segurança Alimentar.
	<li>Afirmar
	os compromissos dos países desenvolvidos com a Ajuda Oficial ao
	Desenvolvimento e a Ajuda ao Comércio, bem como o papel fundamental
	da UNCTAD na identificação de necessidades e desenvolvimento de
	mecanismos sustentáveis de resolução da dívida soberana,
	reunindo diferentes partes interessadas para criar princípios de
	concessão e contração responsável de empréstimos e esforços
	continuados de anulação de dívidas. 
	
	<li>Apresentar
	uma análise clara do impacto da mudança climática no
	desenvolvimento sustentável e inclusivo e conferir à UNCTAD o
	mandato de contribuir para o esforço de realização dos objetivos
	de desenvolvimento inclusivo e sustentável frente à mudança
	climática. 
	
</ul>
<p>
Estas são apenas algumas questões, que devem constituir a base da
declaração oficial que vai guiar o papel da UNCTAD nos próximo 4
anos. Uma análise mais abrangente de cada uma dessas questões se
encontra detalhada na Declaração da Sociedade Civil oficial da 
XIII UNCTAD. 

<p>
À luz da retórica em torno do compromisso com uma governança
global mais aberta, democrática e participativa, que se tornou comum
nos últimos anos, vemos como ultrajante o retorno dos países
desenvolvidos a uma abordagem semicolonial nas negociações da
UNCTAD. Congratulamos os antigos líderes e a equipe da UNCTAD que se
manifestaram na semana passada contra essa situação inaceitável,
bem como o G77 por sua declaração que chamou a UE e o JUSSCKANNZ à
responsabilidade por suas posições intransigentes. 

<p>
Como representantes de países desenvolvidos e em
desenvolvimento, em uma miríade de estágios de desenvolvimento,
sabemos que nossa própria prosperidade está profundamente
entrelaçada ao desenvolvimento sustentável de todos, e portanto
chamamos os governos do Norte e do Sul a unir-se para afirmar um
papel forte da UNCTAD no trabalho por um desenvolvimento sustentável
e inclusivo para todos. 

