4th February 2021
<p>
Dear Members of the World Trade Organization (WTO),
<p>
As civil society organisations concerned with access to health products, to educational resources, to
environmentally sound technologies (ESTs), and to other public goods and cultural creations and
further concerned with farmers’ rights, food security, human flourishing, sustainable and equitable
technological and industrial development in Least Developed Countries (LDCs), <b>we call on WTO
Members to grant the LDC Group the transition period requested in their duly motivated
request to WTO’s Council for TRIPS (IP/C/W/668).</b>
<p>
Article 66.1 of the TRIPS Agreement accorded LDC Members of the WTO an initial ten-year
transition period, with an automatic right of further extensions, that grants LDCs exemption from
implementing most TRIPS obligations in view of the special needs and requirements of the LDC
Members, including their economic, financial and administrative constraints and their need for
flexibility to create a viable technological base.
<p>
The general transition period was originally due to expire on 31 December 2005, but has since been
extended twice following LDCs’ requests. However, each time, the full demands of LDCs have not
been met, with developed countries only willing to give impractically short extensions. The most
recent extension of transition period granted in 2013 is again set to expire on 1 July 2021.
<p>
<b>On 1 October 2020, the LDC Group submitted a duly motivated request to the WTO TRIPS
Council (IP/C/W/668) seeking an extension of the LDC transition period, for as long as a
country remains an LDC <i>AND</i> an additional period of 12 years as a country graduates
from its LDC status to ensure smooth transition.</b>
<p>
<b>We are firmly of the view that LDCs’ request is fully justified.</b> Short extensions granted to date
have proven to be ill-considered and unrealistic, requiring LDCs to repeatedly seek extensions from
the TRIPS Council. They are also impracticable given that development of a viable technological
base and overcoming capacity constraints requires a long-term strategy. Further even as LDCs
graduate from the LDC category, these challenges persist. Nascent industries in LDCs require
assurances that they can copy and adapt existing technologies and mature sufficiently to survive in
globalized markets.
<p>
In recognition of this situation, UN General Assembly resolutions have called on WTO Members to
consider extending to graduated LDC Members the existing special and differential treatment
measures and exemptions available to LDCs.<sup><a href="#note1">1</a></sup>
<p>
By definition, LDCs represent the most vulnerable segment of the international community. Extreme
poverty, limited infrastructure, weak scientific and productive capacities, human and financial
constraints continues to widely persist in LDCs. They also lack affordable access to knowledge-based
goods crucial for sustainable development such as access to health products including for COVID-19
such as ventilators, educational materials, green technologies.
<p>
Historically most technological development in developing and even in developed countries has come
through a period of copying and adapting technologies initially invented elsewhere.<sup><a href="#note2">2</a></sup> Oftentimes,
LDCs are misguided into focusing on becoming TRIPS compliant. We believe that this focus is
misplaced as LDCs are not in a position to benefit from full TRIPS compliance. As stated in the
preamble of the TRIPS Agreement, LDCs require “maximum flexibility in the domestic
implementation of laws and regulations in order to enable them to create a sound and viable
technological base.” In any case, the main users and beneficiaries of a TRIPS compliant IP regime in
LDCs are foreign right holders that tend to set high monopoly prices, unaffordable to most of the
population.
<p>
This year marks the final year of implementation of the Programme of Action for the Least Developed
Countries for the Decade 2011–2020 (Istanbul Programme of Action). While some very modest
progress may have been made, the LDCs continue to face daunting development challenges, now
aggravated by the COVID-19 pandemic. Extremely limited testing<sup><a href="#note3">3</a></sup> , health services<sup><a href="#note4">4</a></sup> and sanitation<sup><a href="#note5">5</a></sup>,
makes curbing COVID-19 in LDCs a massive challenge. Moreover, because of the impact of COVID-
19 on commodity markets and LDC economies, resources for development are even more constrained
than before.
<p>
The United Nations Development Programme (UNDP) estimates that global human development, as
the combined measure of the world’s education, health and living standards, is on course to decline
for the first time in 30 years.<sup><a href="#note6">6</a></sup> The number of people living in extreme poverty could increase by
nearly 130 million by 2030.<sup><a href="#note7">7</a></sup> By all accounts, LDCs will be most affected by the COVID-19 pandemic
for many many years to come. In this context, the economic, financial and administrative costs
associated with TRIPS implementation are simply unacceptable.
<p>
<b>In conclusion we request that WTO Members honour their obligation under Article 66.1 and
unconditionally accord to the Least Developed Countries their properly motivated and
requested extension and adopt the decision text proposed in IP/C/W/668.</b>
<p>
<b>Any attempt to refuse or weaken the LDC’s request will be unconscionable given the social and
economic hardship already facing LDCs. Adopting the LDC decision is its entirety is crucial to
uphold the credibility of the WTO as an institution that can benefit the poorest and most
vulnerable segment of the international community.</b>
