          <h1 class="title">Services / GATS</h1>
        </div> <!-- /#content-header -->
<p>
The General Agreement on Trade in Services (GATS) is the agreement within the WTO that sets forth rules for how countries can regulate services, and gives transnational services suppliers rights not only to operate, but also rights in the way that they operate, in foreign countries. Countries have agreed to “bind” their services sectors to WTO rules in their Schedules in the GATS. This agreement was part of the founding of the WTO due to the lobbying of Big Finance and other transnational corporations (TNCs) in the services sector. The majority of developing countries have long opposed the expansion of the GATS, and talks faltered as the Doha round stalled. 
<p>
Proponents then launched talks towards a services-only pact, the Trade in Services Agreement (TiSA). OWINFS members, together with Public Services International, conducted a grassroots and advocacy campaign for four years against the TiSA. We were successful in December 2016 when the talks collapsed. 
<p>
Now, a dangerous corporate agenda is behind a new effort to have new rules limiting domestic regulation of services, within the General Agreement on Trade in Services (GATS) of the WTO. “Domestic Regulation” (DR) rules would restrict the types of rules that governments can make even if the rules are applied the same to foreign and domestic companies. They would even apply to domestic (non-traded) services (like construction) as well as services that are traded (like air travel). The proposed rules would apply to private companies as well as to public services not covered by the narrow public services exception. 
<p>
While proponents argue that the “right to regulate” would still be safeguarded, the WTO has ruled in the past that “Members’ regulatory sovereignty is an essential pillar of the progressive liberalization of trade in services, but this sovereignty ends whenever rights of other Members under the GATS are impaired.” Thus the “right to regulate” ends where the WTO rules begin. None of the documents on the negotiations have been made public, yet the text has been finalized and Members are intending to finalize the agreement before or during the upcoming 12th Ministerial Conference (MC12) in Geneva in December, 2021. 
<p>
In addition, some countries are seeking to restart the negotiations to expand the scope and coverage of the GATS in the WTO. This would involve countries agreeing to subject even more services sectors to the disciplines of the WTO, which would further privilege foreign transnational services providers at the expense of domestic services. Services sectors currently targeted include environmental (engineering, architecture, construction (water related), distribution, data processing, consulting); tourism and related; agriculture related (services incidental to agriculture such as wholesale trade in live animals, sale on contract of food products, wholesale trade in food and beverage, food retailing); and logistics. 
This agenda also overlaps with the digital trade negotiations, as many services sectors targeted relate to the information and communications sector, and rules on cross-border data transfer are also included in the GATS. 
<p>
One of the primary concerns of civil society with regards to the proposed DR rules, like those of Investment Facilitation, is the “Transparency” provisions which would give foreign TNCs greater input and oversight over domestic policymaking processes at the expense of public interest concerns of citizens.  
<p>
OWINFS members have long maintained core demands with regards to services in the WTO: 
<ul class="tail">
  <li>Countries should not take additional commitments to “discipline” their services sectors, through expanded GATS negotiations, Domestic Regulation rules, or through digital trade talks. 
  <li>Current and future public services in particular must be excluded from any existing or future agreements that include services with a broad and authentic carve-out. This must include services that may need to be re-municipalized in future.
  <li>In addition, a fundamental assessment of the implications of the GATS on ensuring proper regulatory oversight of services and ensuring access to quality public services should be undertaken by member states and the WTO. Any constraints on achieving the Sustainable Development Goals (SDGs) with regards to good health and well-being, inclusive and equitable quality education, clean water and sanitation, affordable and clean energy, and other services should be removed without challenge from the WTO rulebook.
</ul>

